<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0201&expand=day02">The Second Day, The First Novell</a> (Neifile)</p>
<p>Martellino counterfeitting to be lame of his members, caused himselfe to be set on the body of Saint Arriguo, where he made shew of his sudden recovery; but when his dissimulation was discovered, he was well beaten, being afterward taken prisoner, and in great danger of being hanged and strangled by the necke, and yet he escaped in the end. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0404&expand=day04">The Fourth Day, The Fourth Novell</a> (Emilia)</p>
<p>Gerbino, contrary to the former plighted faith of his Grand-father, King Gulielmo, fought with a Ship at Sea, belonging to the King of Thunis, to take away his Daughter, who was then in the same Ship. Shee being slaine by them that had the possession of her, he likewise slew them; and afterward had his owne head smitten off. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0405&expand=day04">The Fourth Day, The Fifth Novell</a> (Filomena)</p>
<p>The three Brethren to Isabella, slew a Gentleman that secretly loved her. His ghost appeared to her in her sleepe, and shewed her in what place they had buried his body. She (in silent manner) brought away his head, aid putting it into a pot of earth, such as Flowers, Basile, or other sweete hearbes are usually set in; she watered it (a long while) with her teares. Wherefore her Brethren having intelligence; soone after she dyed, with meere conceite of sorrow. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0409&expand=day04">The Fourth Day, The Ninth Novell</a> (Filostrato)</p>
<p>Messer Guiglielmo of Rossiglione having slaine Messer Guiglielmo Guardastagno, whom hee imagined to love his wife, gave her his heart to eate. Which she knowing afterward, threw her selfe out of a window to the ground; and being dead, was buried with her friend. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0501&expand=day05">The Fifth Day, The First Novell</a> (Panfilo)</p>
<p>Chynon, by falling in Love, became wise, and by force of Armes, winning his faire Lady Iphigenia on the Seas, was afterward imprisoned at Rhodes. Being delivered by anyone named Lysimachus, with him he recovered his Iphigenia againe, and faire Cassandra, even in the middest of their marriage. They fled with them into Candye, where after they had married them, they were called home to their owne dwelling. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0506&expand=day05">The Fifth Day, The Sixth Novell</a> (Pampinea)</p>
<p>Guion di Procida, being found familiarly conversing with a young Damosell, which he loved; and had beene given (formerly) to Frederigo, King of Sicilie: was bound to a stake, to be consumed with fire. From which h dan ger (neverthelesse) he escaped, being knowne by Don Rogiero de Oria, Lord Admirall of Sicilie, and afterward married the Damosell. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0507&expand=day05">The Fifth Day, The Seventh Novell</a> (Lauretta)</p>
<p>Theodoro falling in love with Violenta, the Daughter to his Master, named Amarigo, and she conceiving with child by him; was condemned to be hanged. As they were leading him to the Gallowes, beating and misusing him all the way: he happened to be knowne of his owne Father, whereupon he was released, and afterward enjoyed Violenta in marriage. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0707&expand=day07">The Seventh Day, The Seventh Novell</a> (Filomena)</p>
<p>Lodovico discovered to his Mistresse Madame Beatrix, how amorously he was affected to her. She cunningly sent Egano her Husband into his garden, in all respects disguised like her selfe, while (friendly) Lodovico conferred with her in the meane while. Afterward, Lodovico pretending a lascivious allurement of his Mistresse, thereby to wrong his honest Master, insted of her, beateth Egano soundly in the Garden. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0908&expand=day09">The Ninth Day, The Eight Novell</a> (Lauretta)</p>
