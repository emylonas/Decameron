<p>A young Gentleman being a Scholler, fell in love with a Ladie, named Helena, she being a Widdow, and addicted in affection to another Gentleman. One whole night in cold Winter, she caused the Scholler to expect her comming, in an extreame frost and snow. In revenge whereof, by his imagined Art and skill, he made her to stand naked on the top of a Tower, the space of a whole day, and in the hot moneth of July, to be Sunburnt and bitten with Waspes and Flies. </p>
