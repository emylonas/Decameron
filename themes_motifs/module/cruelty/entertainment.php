<h2>Cruelty employed for entertainment</h2>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0209&expand=day02">The Second Day, The Ninth Novell</a> (Filomena)</p>
<p>Bernardo, a Merchant of Geneway, being deceived by another Merchant, named Ambroginolo, lost a great part of his goods. And commanding his innocent Wife to be murthered, she escaped, and (in the habite of a man) became servant to the Soldane. The deceiver being found at last, shee compassed such meanes, that her Husband Bernardo came into Alexandria, and there, after due punnishment inflicted on the false deceiver, she resumed the garments againe of a woman, and returned home with her Husband to Geneway. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0404&expand=day04">The Fourth Day, The Fourth Novell</a> (Emilia)</p>
<p>Gerbino, contrary to the former plighted faith of his Grand-father, King Gulielmo, fought with a Ship at Sea, belonging to the King of Thunis, to take away his Daughter, who was then in the same Ship. Shee being slaine by them that had the possession of her, he likewise slew them; and afterward had his owne head smitten off. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0501&expand=day05">The Fifth Day, The First Novell</a> (Panfilo)</p>
<p>Chynon, by falling in Love, became wise, and by force of Armes, winning his faire Lady Iphigenia on the Seas, was afterward imprisoned at Rhodes. Being delivered by anyone named Lysimachus, with him he recovered his Iphigenia againe, and faire Cassandra, even in the middest of their marriage. They fled with them into Candye, where after they had married them, they were called home to their owne dwelling. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0807&expand=day08">The Eigth Day, The Seventh Novell</a> (Pampinea)</p>
<p>A young Gentleman being a Scholler, fell in love with a Ladie, named Helena, she being a Widdow, and addicted in affection to another Gentleman. One whole night in cold Winter, she caused the Scholler to expect her comming, in an extreame frost and snow. In revenge whereof, by his imagined Art and skill, he made her to stand naked on the top of a Tower, the space of a whole day, and in the hot moneth of July, to be Sunburnt and bitten with Waspes and Flies. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov1010&expand=day10">The Tenth Day, The Tenth Novell</a> (Dioneo)</p>
<p>The Marquesse of Saluzzo, named Gualtiero, being constrained by the importunate solliciting of his Lords, and other inferiour people, to joyne himselfe in marriage; tooke a woman according to his owne liking, called Grizelda, she being the daughter of a poore Countriman, named Janiculo, by whom he had two children, which he pretended to be secretly murdered. Afterward, they being grown to yeres of more stature, and making shew of taking in marriage another wife, more worthy of his high degree and Calling: made a seeming publique liking of his owne daughter, expulsing his wife Grizelda poorely from him. But finding her incomparable patience; more dearely (then before) hee received her into favour againe, brought her home to his owne Pallace, where (with her children) hee caused her and them to be respectively honoured, in despight of all her adverse enemies. </p>

