<h2>Cruelty</h2>
<p>Cruelty and violence, even though the majority of the stories are told by women, appear often in the Decameron. The reader will find men beating women, women beating men, and men beating men. Cruelty does not always entail direct physical abuse either. Verbal abuse can be quit cruel. Cruelty is a fair method of retaliation, an adequate contrapasso. What is most compelling about the episodes of cruelty and violence is the amount of detail that goes into describing the mode of pain infliction and the resulting physical harm. Cruelty seems to be an accepted method of gaining the upper hand in <a href="../../../society/sex-spouses.php">marriage</a> or punishing misdeeds. The characters who resort to violence often are not the most intelligent figures in the Decameron, because if they had wit and intellect, they would find better ways than cruelty and violence to attain power.</p>

