<h2>Religion</h2>
<p><a href="../../../religion/">Religion</a> is a favorite topic of mockery. Most of the religious characters that show up, such as Abbots, Nuns, Priests, Friars or Monks trick the men and women of the Novellas into following their schemes, be it for sex or money or other goals. A handful of the clergy characters fall into traps set by others. The Decameron definately takes a humorous and critical approach to describing the clergy in action. In this sense, the Decameron is supposed to act as a warning to the ladies against believing everything priests may tell them. Clergy members are only human, and often posses little morality and zealous carnal <a href="../../../society/sex-clergy.php">desires</a>. The ladies must watch out for their trickery and cunning. Besides Christianity, there is mention of Judaism and the Muslim faith, and corresponding characters. <a href="../../../society/magic/religious_superstition.php">Saints</a> in the Novellas appear as holy figures, often invoked by the characters for protection.</p>

