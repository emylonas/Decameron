<h2>Stories containing death by illness</h2>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0101&expand=day01">The First Day, The First Novell</a> (Panfilo)</p>
<p>Messire Chappelet du Prat, by making a false confession, beguyled an holy Religious man, and after dyed. And having (during his life time) bene a very bad man, at his death, was reputed for a saint, and called S. Chappelet. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0210&expand=day02">The Second Day, The Tenth Novell</a> (Dioneo)</p>
<p>Pagamino da Monaco, a roving Pyrate on the Seas, carried away the fayre Wife of Signior Ricciardo de Chinzica, who understanding where shee was, went thither; and falling into friendship with Pagamino, demanded his Wife of him; whereto he yeelde, provided, that shee would willing goe away with him. She denied to part thence with her Husband, and Signior Ricciardo dying, she became the wife of Pagamino. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0406&expand=day04">The Fourth Day, The Sixth Novell</a> (Panfilo)</p>
<p>A beautifull young Virgine, named Andreana, became enamoured of a young Gentleman called Gabriello. In conference together, she declared a dreame of hers to him, and he another of his to her; whereupon Gabriello fell downe sodainly dead in her armes. She, and her Chamber-maide were apprehended, by the Officers belonging to the Seigneury, as they were carrying Gabriello, to lay him before his owne doore. The Potestate offering violence to the Virgin, and she resisting him vertuously: it came to the understanding of her Father, who approved the innocence of his daughter, and compassed her deliverance. But she afterward, being weary of all worldly felicities, entred into Religion, and became a Nun. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0407&expand=day04">The Fourth Day, The Seventh Novell</a> (Emilia)</p>
<p>Faire Simonida affecting Pasquino, and walking with him in a pleasant garden, it fortuned, that Pasquino rubbed his teeth with a leafe of Sage, and immediately fell downe dead. Simonida being brought before the bench of Justice, and charged with the death of Pasquino, she rubbed her teeth likewise with one of the leaves of the same Sage, as declaring what shee saw him do, and thereon she dyed also in the same manner. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0408&expand=day04">The Fourth Day, The Eight Novell</a> (Neifile)</p>
<p>Jeronimo affecting a yong Maiden, named Silvestra, was constrained (by the earnest importunity of his Mother) to take a journey to Paris. At his return home from thence againe, he found his love Silvestra married. By secret meanes, he got entrance into her house, and dyed upon the bed lying by her. Afterward, his body being carried to Church, to receive buriall, she likewise died there instantly upon his coarse. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0505&expand=day05">The Fifth Day, The Fifth Novell</a> (Neifile)</p>
<p>Guidotto of Cremona, out of this mortall life, left a Daughter of his, with Jacomino of Pavia. Giovanni di Severino, and Menghino da Minghole, fell both in love with the young Maiden, and fought for her; who being afterward knowne to be the Sister to Giovanni, she was given in mariage to Menghino. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0509&expand=day05">The Fifth Day, The Ninth Novell</a> (Fiammetta)</p>
<p>Frederigo, of the Alberighi Family, loved a Gentlewoman, and was not requited with like love againe. By bountifull expences, and over liberall invitations, he wasted and consumed all his lands and goods, having nothing left him, but a Hawke or Faulcon. His unkinde Mistresse happeneth to come visite him, and he not having any other foode for her dinner; made a dainty dish of his Faulcone for her to feede on. Being conquered by this exceeding kinde courtesie; she changed her former hatred towardes him, accepting him as her Husband in marriage, and made him a man of wealthy possessions. </p>

