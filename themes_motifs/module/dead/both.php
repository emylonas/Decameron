<h2>Stories in which both lovers die</h2>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0401&expand=day04">The Fourth Day, The First Novell</a> (Fiammetta)</p>
<p>Tancrede, Prince of Salerne, caused the amorous friend of his daughter to bee slaine, and sent her his heart in a cup of Gold: which afterwards she steeped in an impoysoned water, and then drinking it, so dyed. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0403&expand=day04">The Fourth Day, The Third Novell</a> (Lauretta)</p>
<p>Three yong Gentlemen affecting three Sisters, fledde with them into Candie. The eldest of them (through jealousie) becommeth the death of her Lover; The second, by consenting to the Duke of Candies request, is the meanes of saving her life. Afterward, her owne Friend killeth her, and thence flyeth away with the elder Sister. The third couple, are charged with her death, and being committed prisoners, they confesse the fact; and fearing death, by corruption of money they prevaile with their Keepers, escaping from thence to Rhodes, where they dyed in great poverty. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0404&expand=day04">The Fourth Day, The Fourth Novell</a> (Elissa)</p>
<p>Gerbino, contrary to the former plighted faith of his Grand-father, King Gulielmo, fought with a Ship at Sea, belonging to the King of Thunis, to take away his Daughter, who was then in the same Ship. Shee being slaine by them that had the possession of her, he likewise slew them; and afterward had his owne head smitten off. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0405&expand=day04">The Fourth Day, The Fifth Novell</a> (Filomena)</p>
<p>The three Brethren to Isabella, slew a Gentleman that secretly loved her. His ghost appeared to her in her sleepe, and shewed her in what place they had buried his body. She (in silent manner) brought away his head, aid putting it into a pot of earth, such as Flowers, Basile, or other sweete hearbes are usually set in; she watered it (a long while) with her teares. Wherefore her Brethren having intelligence; soone after she dyed, with meere conceite of sorrow. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0407&expand=day04">The Fourth Day, The Seventh Novell</a> (Emilia)</p>
<p>Faire Simonida affecting Pasquino, and walking with him in a pleasant garden, it fortuned, that Pasquino rubbed his teeth with a leafe of Sage, and immediately fell downe dead. Simonida being brought before the bench of Justice, and charged with the death of Pasquino, she rubbed her teeth likewise with one of the leaves of the same Sage, as declaring what shee saw him do, and thereon she dyed also in the same manner. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0408&expand=day04">The Fourth Day, The Eight Novell</a> (Neifile)</p>
<p>Jeronimo affecting a yong Maiden, named Silvestra, was constrained (by the earnest importunity of his Mother) to take a journey to Paris. At his return home from thence againe, he found his love Silvestra married. By secret meanes, he got entrance into her house, and dyed upon the bed lying by her. Afterward, his body being carried to Church, to receive buriall, she likewise died there instantly upon his coarse. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0101&expand=day01">The Fourth Day, The Ninth Novell</a> (Filostrato)</p>
<p>Messer Guiglielmo of Rossiglione having slaine Messer Guiglielmo Guardastagno, whom hee imagined to love his wife, gave her his heart to eate. Which she knowing afterward, threw her selfe out of an high window to the ground; and being dead, was then buried with her friend. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0508&expand=day05">The Fifth Day, The Eight Novell</a> (Filomena)</p>
<p>Anastasio, a Gentleman of the Family of the Honesti, by loving the Daughter to Signior Paulo Traversario, lavishly wasted a great part of his substance, without receiving any love from her againe. By perswasion of some of his kindred and friends, he went to a Countrey dwelling of his, called Chiasso, where he saw a Knight desperately pursue a young Damosell; whom he slew, and afterward gave her to be devoured by his Hounds. Anastasio invited his friends, and hers also whom he so dearely loved, to take part of a dinner with him, who likewise saw the same Damosell so torne in peeces: which his unkind Love perceiving, and fearing least the like ill fortune should happen to her; she accepted Anastasio to be her Husband. </p>

