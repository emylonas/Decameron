<h2>Death in the <em>Decameron</em></h2>
<p>Death is of course on the minds and heart of the Brigata members, as they have escaped the city in fear of the <a href="../../../plague/">Plague</a>. Along with death, there is of course a lot of murder in the stories, which occurs mostly out of love and jealousy. Besides murder, the classic tragic story of lovers who cannot be together and therefore take their lives to escape misery appears often in the Decameron. Finally, there are dead characters that return to life, sometimes appearing to give a message or warning to the living or sometimes because they were not dead after all. Death is looked upon gravely when it causes lovers to part, and lightly when it brings lovers together. <a href="../../heart/dismemberment.php">Dead bodies</a> and body parts also make rather graphic appearances in the stories. An interesting aspect of death is that widows seem to be the most sought after women in the Decameron, presenting experienced lovers without marital taboo.</p>

