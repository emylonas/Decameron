<h2>Stories in which dead characters return</h2>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0307&expand=day03">The Third Day, The Seventh Novell</a> (Emilia)</p>
<p>Theobaldo Elisei, having received an unkinde repulse by his beloved, departed from Florence, and returning thither (a long while after) in the habite of a Pilgrime; he spake with her, and made his wrongs knowne unto her. He delivered her Father from the danger of death, because it was proved, that he had slaine Theobaldo: he made peace with his brethren, and in the end, wisely enjoyed his hearts desire. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0308&expand=day03">The Third Day, The Eight Novell</a> (Lauretta)</p>
<p>Ferando, by drinking a certaine kinde of powder, was buried dead. And by the Abbot, who was enamored of his Wife, was taken out of his Grave, and put into a darke prison, where they made him beleeve, that hee was in Purgatorie. Afterward, when time came that hee should be, raised to life againe; he was made to keepe a childe which the Abbot had got by his Wife. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0405&expand=day04">The Fourth Day, The Fifth Novell</a> (Filomena)</p>
<p>The three Brethren to Isabella, slew a Gentleman that secretly loved her. His ghost appeared to her in her sleepe, and shewed her in what place they had buried his body. She (in silent manner) brought away his head, aid putting it into a pot of earth, such as Flowers, Basile, or other sweete hearbes are usually set in; she watered it (a long while) with her teares. Wherefore her Brethren having intelligence; soone after she dyed, with meere conceite of sorrow. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0410&expand=day04">The Fourth Day, The Tenth Novell</a> (Dioneo)</p>
<p>A physitians wife laide a Lover of her Maides (supposing him to be dead) in a Chest, by reason that he had drunke Water, which usually was given to procure a sleepy entrancing. Two Lombard usurers, stealing the Chest, in hope of a rich booty, carryed it into their owne house, where afterward the man awaking, was apprehended for a Theefe. The Chamber-maide to the Physitians wife, going before the bench of Justice, accuseth her selfe for putting the imagined dead body into the Chest, by which meanes he escapeth hanging. And the theeves which stole away the Chest, were condemned to pay a great summe of money. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0508&expand=day05">The Fifth Day, The Eight Novell</a> (Filomena)</p>
<p>Anastasio, a Gentleman of the Family of the Honesti, by loving the Daughter to Signior Paulo Traversario, lavishly wasted a great part of his substance, without receiving any love from her againe. By perswasion of some of his kindred and friends, he went to a Countrey dwelling of his, called Chiasso, where he saw a Knight desperately pursue a young Damosell; whom he slew, and afterward gave her to be devoured by his Hounds. Anastasio invited his friends, and hers also whom he so dearely loved, to take part of a dinner with him, who likewise saw the same Damosell so torne in peeces: which his unkind Love perceiving, and fearing least the like ill fortune should happen to her; she accepted Anastasio to be her Husband. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0710&expand=day07">The Seventh Day, The Tenth Novell</a> (Dioneo)</p>
<p>Two Citizens of Siena, the one named Tingoccio Mini, and the other Meucio di Tura, affected both one woman, called Monna Mita, to whom the one of them was a Gossip. The Gossip dyed, and appeared afterward to his companion, according as he had formerly promised him to doe, and tolde him what strange wonders he had seene in the other world. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov1004&expand=day10">The Tenth Day, The Fourth Novell</a> (Lauretta)</p>
<p>Signior Gentile de Carisendi, being come from Modena, took a Gentlewoman, named Madam Catharina, forth of a grave, wherin she was buried for dead: which act he did, in regard of his former honest affection to the said Gentlewoman. Madame Catharina remaining there afterward, and delivered of a goodly Sonne: was (by Signior Gentile) delivered to her owne Husband, named Signior Nicoluccio Caccianimico, and the yong infant with her. </p>

