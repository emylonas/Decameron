<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0101&expand=day01">The Fourth Day, The Ninth Novell</a> (Filostrato)</p>
<p>Messer Guiglielmo of Rossiglione having slaine Messer Guiglielmo Guardastagno, whom hee imagined to love his wife, gave her his heart to eate. Which she knowing afterward, threw her selfe out of an high window to the ground; and being dead, was then buried with her friend. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0508&expand=day05">The Fifth Day, The Eight Novell</a> (Filomena)</p>
<p>Anastasio, a Gentleman of the Family of the Honesti, by loving the Daughter to Signior Paulo Traversario, lavishly wasted a great part of his substance, without receiving any love from her againe. By perswasion of some of his kindred and friends, he went to a Countrey dwelling of his, called Chiasso, where he saw a Knight desperately pursue a young Damosell; whom he slew, and afterward gave her to be devoured by his Hounds. Anastasio invited his friends, and hers also whom he so dearely loved, to take part of a dinner with him, who likewise saw the same Damosell so torne in peeces: which his unkind Love perceiving, and fearing least the like ill fortune should happen to her; she accepted Anastasio to be her Husband. </p>

