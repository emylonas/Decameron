<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0209&expand=day02">The Second Day, The Ninth Novell</a> (Filomena)</p>
<p>Bernardo, a Merchant of Geneway, being deceived by another Merchant, named Ambroginolo, lost a great part of his goods. And commanding his innocent Wife to be murthered, she escaped, and (in the habite of a man) became servant to the Soldane. The deceiver being found at last, shee compassed such meanes, that her Husband Bernardo came into Alexandria, and there, after due punnishment inflicted on the false deceiver, she resumed the garments againe of a woman, and returned home with her Husband to Geneway. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0506&expand=day05">The Fifth Day, The Sixth Novell</a> (Pampinea)</p>
<p>Guion di Procida, being found familiarly conversing with a young Damosell, which he loved; and had beene given (formerly) to Frederigo, King of Sicilie: was bound to a stake, to be consumed with fire. From which h dan ger (neverthelesse) he escaped, being knowne by Don Rogiero de Oria, Lord Admirall of Sicilie, and afterward married the Damosell. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0507&expand=day05">The Fifth Day, The Seventh Novell</a> (Lauretta)</p>
<p>Theodoro falling in love with Violenta, the Daughter to his Master, named Amarigo, and she conceiving with child by him; was condemned to be hanged. As they were leading him to the Gallowes, beating and misusing him all the way: he happened to be knowne of his owne Father, whereupon he was released, and afterward enjoyed Violenta in marriage. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov1003&expand=day10">The Tenth Day, The Third Novell</a> (Filostrato)</p>
<p>Mithridanes envying the life and liberality of Nathan, and travelling thither, with a setled resolution to kill him: chaunceth to conferre with Nathan unknowne. And being instructed by him, in what manner he might best performe the bloody deede, according as hee gave direction, hee meeteth with him in a small Thicket or Woode, where knowing him to be the same man, that taught him how to take away his life: Confounded with shame, hee acknowledgeth his horrible intention, and becommeth his loyall friend. </p>

