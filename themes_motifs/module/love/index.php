<h2>Love</h2>
<p>Since these Novellas are intended to teach women some lessons in life, <a href="../../">Amore</a> is one of the most important themes. On <a href="../../../texts/DecShowText.php?lang=eng&myID=day04&expand=day04">Day 4</a>, love stories are requested by Filostrato, who wants to hear stories between lovers that end tragically. But on <a href="../../../texts/DecShowText.php?lang=eng&myID=day05&expand=day05">Day 5</a>, Fiammetta insists on love stories with happy endings in which the characters are able to live their loves to their fullest desire. Love is found between all social tiers in the Decameron. It is often women married unhappilly to older men that seek love within the ranks of younger or socially lower men. The narrators distinguish acts of love from acts of lust by the language of the descriptions reflecting the feelings the lovers have towards another. True love is a deep, lasting and often tested phenomenon of beauty. Lust on the other hand is often quick and short-lived. Love at the time of Boccaccio was strong, intense and celebrated.</p>

