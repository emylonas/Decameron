<h2>Wit</h2>
<p>The use of wit, or clever remarks, is one of the main themes of the Decameron. Wit is the most common weapon used among the characters of the Novella to harm each other. This form of rhetorical jousting is sometimes used for sheer entertainment, in other words in order to play a practical joke, often on ousiders of <a href="../../../images/maps/decworld/florence.php">Florence</a>. Wit is also employed by many characters in retaliation, or to escape a potentially dangerous situation. Especially for those characters of lower rank, wit is sometimes the only way to redeem themselves or avoid trouble. Wit appears in three distinct forms. Sometimes, all it takes are a few clever words to win a situation by wit. Sometimes, the characters resort to lenghty stories to fool or outdo their rhetorical opponents. Finally, wit can also appear in the form of a clever action. Wit is employed between all members of society, men, women and the clergy to fool each other. </p>

