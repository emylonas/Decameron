<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0402&expand=day04">The Fourth Day, The Second Novell</a> (Pampinea)</p>
<p>Fryar Albert made a young Venetian Gentlewoman beleeve, that God Cupid was falne in love with her, and he resorted oftentimes unto her, in the disguise of the same God. Afterward, being frighted by the Gentlewomans kindred and friends, he cast himselfe out of her Chamber window, and was bidden in a poore mans House; on the day following, in the shape of a wilde or savage man, he was brought upon the Rialto of Saint Marke, and being there publikely knowne by the Brethren of his Order, he was committed to Prison.</p>
