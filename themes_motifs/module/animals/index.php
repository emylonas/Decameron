<h2>Animals in the <em>Decameron</em></h2>
<p>Animals are a rare motif in the Novella, thus when they do appear, they usually have a significant role in the plot of the stories. Some of the animals are part of humorous tricks, such as the stealing of <a href="../../../society/magic/folk_magic.php">Calandrino's</a> pig or the one-legged crane the cook presents his master. Other animals symbolize the actions or characteristics of certain figures, such as the noble falcon of Frederigo. The types of animals that appear are birds, such as falcons or nightingales, and mammals such as dogs or mules. The songs of the <a href="../../../literature/narratology/apologia.php">Nightingales</a> are often associated with love themes. There is not one way to characterize the significance of animals as a whole, because of their varied roles, they are simply props in the narrative tales the Brigata spin.</p>

