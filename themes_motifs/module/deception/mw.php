<h2>Men deceiving women</h2>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0302&expand=day03">The Third Day, The Second Novell</a> (Pampinea)</p>
<p>A querry of the Stable, belonging to Agilulffo, King of the Lombardes, found the meanes of accesse to the Queenes bed, without any knowledge or consent in her. This being secretly discovered by the King, and the party known, he gave him a marke, by shearing the haire of his head. Whereupon, he that was so shorne, sheared likewise the heads of all his fellowes in the lodging, and so escaped the punishment intended towards him. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0306&expand=day03">The Third Day, The Sixth Novell</a> (Fiammetta)</p>
<p>Ricciardo Minutolo fell in love with the wife of Philippello Fighinolfi, and knowing her to be very jealous of her Husband, gave her to understand, that hee was greatly enamoured of his Wife, and had appointed to meete her privately in a Bathing house, on the next day following: where shee hoping to take him tardie with his close compacted Mistresse, found her selfe to be deceived by the said Ricciardo. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0307&expand=day03">The Third Day, The Seventh Novell</a> (Emilia)</p>
<p>Theobaldo Elisei, having received an unkinde repulse by his beloved, departed from Florence, and returning thither (a long while after) in the habite of a Pilgrime; he spake with her, and made his wrongs knowne unto her. He delivered her Father from the danger of death, because it was proved, that he had slaine Theobaldo: he made peace with his brethren, and in the end, wisely enjoyed his hearts desire. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov0705&expand=day07">The Seventh Day, The Fifth Novell</a> (Fiammetta)</p>
<p>A jealous man, clouded with the habite of a Priest, became the Confessour to his owne Wife; who made him beleeve, that she was deepely in love with a Priest, which came every night, and lay with her. By meanes of which confession, while her jealous Husband watched the doore of his house; to surprize the Priest when he came: she that never meant to do amisse, had the company of a secret Friend, who came over the toppe of the house to visite her, while her foolish Husband kept the doore. </p>
<p><a href="../../../texts/DecShowText.php?lang=eng&myID=nov1010&expand=day10">The Tenth Day, The Tenth Novell</a> (Dioneo)</p>
<p>The Marquesse of Saluzzo, named Gualtiero, being constrained by the importunate solliciting of his Lords, and other inferiour people, to joyne himselfe in marriage; tooke a woman according to his owne liking, called Grizelda, she being the daughter of a poore Countriman, named Janiculo, by whom he had two children, which he pretended to be secretly murdered. Afterward, they being grown to yeres of more stature, and making shew of taking in marriage another wife, more worthy of his high degree and Calling: made a seeming publique liking of his owne daughter, expulsing his wife Grizelda poorely from him. But finding her incomparable patience; more dearely (then before) hee received her into favour againe, brought her home to his owne Pallace, where (with her children) hee caused her and them to be respectively honoured, in despight of all her adverse enemies. </p>

