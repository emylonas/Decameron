<h2>Plague</h2>
<p>The arrival of the catastrophic and enigmatic Plague forever changed the way in which fourteenth-century man understood his relationship to the world around him. There were those who saw the vast human destruction as a sort of Old Testament-like form of divine punishment and others who attributed it to the tyranny of chance - an epistemological hypothesis which held dangerous theological implications. Boccaccio's emphasis on a naturalistic portrayal of the plague's effects in Florence, thought by some to have been inspired by analogous accounts in Lucretius' <em>De rerum natura</em>, dismantled Bernard Silvester's notion of Nature as <em>Mater generationis</em>&nbsp;and sets the stage for an interpretation of health and stability as mere illusions. This perspective allows for the literary development of unaccustomed concepts regarding the relationship of man to his environment (as well as to his fellow man) and casts new light on the ramifications of the <em>brigata</em>'s attempts at diversion - attempts to create in an uncertain and malevolent world their own utopia.</p>

