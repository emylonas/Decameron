<p>The gruesome French tales above of love and revenge have scenarios very similar to some in the <em>Decameron</em>. The husband punishes both the lover and the wife, ironically giving her what she most desired in a form which destroys her and her lover. Forced by her husband to eat her lover's heart unknowingly, the wife becomes the victim of a secret, transgressive and violent act. What is more, the consumption of food can have inherently sensual overtones and, as a consequence, the motif of the eaten heart at times lends itself to a sexual interpretation. In this light, in could be said that the cuckolded husband reasserts his power over the wife through a perversion of intercourse; the ingestion of the lover's heart represents an extreme degree of degradation insofar as it reflects - though in a violent, deviant fashion - the wife's incorporation of her lover's body into her own.</p>
<p>Upon being discovered, however, this act of eating may take on an aspect of a funerary meal, and the ingestion of one into another's body becomes an occasion for grief and mourning. The woman's reaction changes the husband's intended meaning. Thus the symbolism may be variously interpreted according to the narrative function of the characters involved and, in effect, to the attitude of the narrator whom Boccaccio chooses for the telling of the tale.</p>

