<h2>The Motif of the Eaten Heart</h2>
<p>Even the thought of someone eating a human heart can provoke strong emotions. Naturally, when an author actively incorporates this theme as a literary leitmotiv, it can make quite an impression on the reader - and on other writers. Though the motif is an old one indeed, it is enlightening to take a look at the original adaptations of Boccaccio and of others who may have exercised an influence on our author.</p>
