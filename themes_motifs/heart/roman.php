<h2>"Le Roman du Ch&acirc;telain de Coucilet et la Dame de Fayel"</h2>
<p>This tale, written at the end of the thirteenth or beginning of the fourteenth century by a little known writer named Jakemon Sakesep, relates the story of Raoul de Couci who had been given by his lover, La Dame de Fayel, braids of her hair as a symbol of her devotion. When he leaves to go off to battle, he carries them with him in a jeweled box. In the heat of the fight he is struck by a poisoned arrow and so instructs a servant to cut out his heart, to put it in the box with the braids and to take it back to his lover with a letter explaining how his heart belongs to her. The lady's husband catches the servant and, upon discovering the heart, has it made into a meal for his wife. When she realizes that she has eaten her lover's heart, she refuses any food and dies soon afterward.</p>
<p>The story appears in numerous versions throughout the following centuries, passing into English for the first time in the old romance of &quot;The Knight of Curtesey and the Lady of Faguell.&quot; It is taken up again by Howell in 1634 in his &quot;Familiar Letters&quot; and dedicated to Ben Johnson.</p>
<p>(C. Sa.) Milad Doueihi. &quot;Cor ne Edito.&quot; <em>MLN</em>&nbsp;108.4 (1993): 696-709. For an exhaustive interpretation of the tale, see Gaston Paris. <em>Histoire litt&eacute;raire de la France</em>. vol. 8. Paris: V. Palme, 1875. pp. 343 <em>et seq</em>. </p>

