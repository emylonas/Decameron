<h2>Disguise and Gender-Role Reversal</h2>
<p>Given that trickery is a common theme in the <em>Decameron</em>, it is not surprising that there are many characters in the <em>novelle</em> in disguise. Cross-dressing certainly did not carry the same cultural meanings in the Middle Ages as it does today. Whenever there is an instance of change-of-gender disguise in a story, there is a shift, comic or serious, in status and role. Dressing in the attire of the opposite sex, in itself, was not thought of as being so daring or subversive as impersonating someone from a radically different class or vocation.</p>
<p>There is, however, a huge distinction between the types of disguises men and women use, and their reasons for doing so. The vast majority of people in disguise are men, and in the first half of the <em>Decameron</em>, they do not cross-dress. The women who disguise themselves, on the other hand, do tend to cross-dress. When men change roles, it is often for lust or comedy, whereas when women do, there is a serious situation, usually perpetuated by a man, that they must try to rectify. The only other apparent instances of gender role reversals of the time are shown as women taking on &quot;manly&quot; qualities. Masculinity is associated with the rational and the brain, as opposed to the emotions and irrationality of the feminine.</p>

