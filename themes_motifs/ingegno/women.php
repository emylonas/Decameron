<h2>Women in Disguise</h2>
<p>Whenever women take on other gender roles, they gain power, but it's usually a manifestation of the power that they already have. There is a lot of identity changing during the Second Day.</p>
<p>The women who take on men's roles are: </p>
<ul class="links">
	<li>The &quot;Abbot&quot; in the story about Alessandro (<a href="../../texts/DecShowText.php?lang=eng&myID=nov0203&expand=day02">Day Two, Tale Three</a>)</li>
	<li>Donna Zinevra, who dresses as a young man in order to escape her husband Bernab&ograve; (<a href="../../texts/DecShowText.php?lang=eng&myID=nov0209&expand=day02">Day Two, Tale Nine</a>)</li>
</ul>
<p>Clothing is key to identifying gender in these stories. It is interesting to consider whether this extreme importance of clothing, to the point of one not recognizing one's wife until she is dressed like a lady, is somewhat realistic or whether it is merely a narrative device in which we must suspend our disbelief. Indeed, Boccaccio's use of masculine pronouns in reference to these women can be construed as a way to put the listener/reader in the same place as the unaware characters in the story or as an actual dependency of language on the role one plays.</p>

