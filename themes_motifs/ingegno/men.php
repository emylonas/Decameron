<h2>Men in Disguise</h2>
<p>The goal of men who go into disguise is usually to get a lover (without her knowledge).</p>
<p>Examples are:</p>
<ul class="links">
	<li>The groom who schemes to sleep with King Agilulf's wife as if he were the King himself (<a href="../../texts/DecShowText.php?lang=eng&myID=nov0302&expand=day03">Day Three, Tale 2</a>)</li>
	<li>Ricciardo, who fools Catella into thinking he was her husband (<a href="../../texts/DecShowText.php?lang=eng&myID=nov0306&expand=day03">Day Three, Tale 6</a>)</li>
</ul>
<p>Another common element which adds ironic humor to this type of story is people pretending to be more pious than they are.</p>
<p>For example:</p>
<ul class="links">
	<li>The Abbot who makes Ferondo believe he is in Purgatory in order to sleep with Ferondo's wife (<a href="../../texts/DecShowText.php?lang=eng&myID=nov0308&expand=day03">Day Three, Tale Eight</a>)</li>
	<li>Friar Alberto who convinces Monna Lisetta that he is the Angel Gabriel (<a href="../../texts/DecShowText.php?lang=eng&myID=nov0402&expand=day04">Day Four, Tale Two</a>)</li>
</ul>

