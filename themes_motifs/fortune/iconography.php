<p>The ubiquitous and ominous wheel of Fortune was a legacy to the Middle Ages from the second book of Boethius' <em>Consolation of Philosophy</em>. It took on many styles, from the delicately drawn miniatures of fine manuscripts to the huge rose windows of the cathedrals at Amiens and Basel. <em>Fortuna</em> herself is often drawn larger than the human figures, in accordance with medieval convention, to underscore her greater importance. The wheel is frequently absurd in its mechanics: <em>Fortuna</em> may propel it with a graceful touch of a spoke or with an awkward-looking crank; each revolution may be swift or require a full life-time. The wheel characteristically bears on its rim four shelves of &quot;stages&quot; with four human figures. The figure rising on the left is usually labeled <em>regnabo</em> (I shall reign), the one at the top is marked <em>regno</em> (I reign) and is often crowned, that descending on the right is <em>regnavi</em> (I have reigned), and the writhing figure at the lowest point is <em>sum sine regno</em> (I have no kingdom). The victim is sometimes depicted as thrown from the wheel by gravity and centrifugal force, and sometimes as crushed under a wheel fit for a heavy cart.</p>
<p>Medieval representations of <em>Fortune</em> emphasize her duality and instability. She often displays two faces, either side-by-side, or back-to-back like Janus; with one black face and one white, or one smiling and one lowering. Sometimes one eye is beaming and one weeping. She may appear blindfolded, like Justice, but without the scale: she is, so to speak, impartial in injustice. Sometimes her head is bald behind but has a long forelock for seizing, like Opportunity. Her clothing may be of changeable colors or sharply divided into dark and bright or rich and beggarly. Occasionally her vivid clothing and bold bearing suggest the prostitute.</p>
<p>(A. K.) Richard Leighton Greene. s.v. &quot;Fortune.&quot; <em>Dictionary of the Middle Ages</em>, Vol.3, Joseph R Strayer, ed. New York: Scribner's, 1983. pp. 145-147.</p>

