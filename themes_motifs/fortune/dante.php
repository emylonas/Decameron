<h2><em>Fortuna</em> in Dante</a></h2>
<p>Fortune, an unpredictable and uncontrollable influence on human affairs, was often personified as a minor female deity as in the writings of the ancients. <em>Fortuna</em>, as the Romans perceived her, derived her &quot;personality&quot; from the Greek notion of <em>tyche</em>, meaning &quot;chance&quot;, &quot;luck&quot;, or &quot;what happens&quot;. She was seen as the prime mover of the Aristotelian <em>Causae per accidens</em> and all unexplained events were attributed to her actions. SS. Augustine (De Divers. <em>Quaest</em>. 83.24) and Thomas (<em>Contra Gent</em>. 3.74 and <em>Summa</em> 1.116.1) went to great pains to explain that no event was wholly driven by chance, however, and denied her existence.</p>
<p>In the seventh canto of Dante's <em>Inferno</em>, Vergil briefly explains the nature of Fortune to the poet. Dante merges the pagan and Christian beliefs in his scheme of the universe by elevating her to the &quot;rank&quot; of a Divine Intelligence, God's &quot;general minister and guide,&quot; who controls the empty wealth and splendor of the world of men. She is inscrutable, swift and powerful within her licensed limits. Though often slandered, she dwells above in bliss, untroubled.</p>
<blockquote>&quot;Your wisdom cannot withstand her: she foresees, judges, and pursues her reign, as theirs the other gods. Her changes know no truce. Necessity compels her to be swift, so fast do men come to their turns. This is she who is much reviled even by those who ought to praise her, but do wrongfully blame her and defame her. But she is blest and does not hear it. Happy with the other primal creatures she turns her sphere and rejoices in her bliss&quot; (<em>Inferno</em>, VII.85-96).
