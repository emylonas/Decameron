<h2>The Art of Courtly Love</h2>
<p>Andreas Capellanus wrote the three books of his treatise <em>The Art of Courtly</em> Love at the request of Countess Marie of Troyes, daughter of Eleanor of Aquitaine. It supposedly portrays life at the Queen's court from 1170 to 1174. In the book Capellanus discusses the concept of courtly love and prepares to explain its "rules" very clearly so as to provide a manual or handbook on Love:</p>

<ol>
	<li>Marriage is no real excuse for not loving.</li>
	<li>He who is jealous cannot love.</li>
	<li>No one can be bound by a double love.</li>
	<li>It is well known that love is always increasing or decreasing.</li>
	<li>That which a lover takes against the will of his beloved has no relish.</li>
	<li>Boys do not love until they reach the age of maturity.</li>
	<li>When one lover dies, a widowhood of two years is required of the survivor.</li>
	<li>No one should be deprived of love without the very best of reasons.</li>
	<li>No one can love unless he is propelled by the persuasion of love.</li>
	<li>Love is always a stranger in the home of avarice.</li>
	<li>It is not proper to love any woman whom one would be ashamed to seek to marry.</li>
	<li>A true lover does not desire to embrace in love anyone except his beloved.</li>
	<li>When made public love rarely endures.</li>
	<li>The easy attainment of love makes it of little value: difficulty of attainment makes it prized.</li>
	<li>Every lover regularly turns pale in the presence of his beloved.</li>
	<li>When a lover suddenly catches sight of his beloved his heart palpitates.</li>
	<li>A new love puts an old one to flight.</li>
	<li>Good character alone makes any man worthy of love.</li>
	<li>If love diminishes, it quickly fails and rarely revives.</li>
	<li>A man in love is always apprehensive.</li>
	<li>Real jealousy always increases the feeling of love.</li>
	<li>Jealousy increases when one suspects his beloved.</li>
	<li>He whom the thought of love vexes eats and sleeps very little.</li>
	<li>Every act of a lover ends in the thought of his beloved.</li>
	<li>A true lover considers nothing good except what he thinks will please his beloved.</li>
	<li>Love can deny nothing to love.</li>
	<li>A lover can never have enough of the solaces of his beloved.</li>
	<li>A slight presumption causes a lover to suspect his beloved.</li>
	<li>A man who is vexed by too much passion usually does not love.</li>
	<li>A true lover is constantly and without intermission possessed by the thought of his beloved.</li>
	<li>Nothing forbids one woman being loved by two men or one man by two women.</li>
</ol>
<p>(P. G.)</p>

