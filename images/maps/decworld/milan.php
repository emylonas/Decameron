									<p class="bodynoindent"><img src="../../page_elements/content_bullet.gif" width="5" height="5"> <b>Economic Aspects:</b> Milan, due to its location at the intersection of several Alpine passes in the upper <a href="detail_nitaly.php">Po Valley</a>, developed into one of the Roman Empire's largest and most powerful cities. Milan thrived (with only occasional periods of decline) during the Middle Ages and eventually became renowned for the production of high quality military arms.</p>
									<p class="bodynoindent"><img src="../../page_elements/content_bullet.gif" width="5" height="5"> <b>Contemporary Political Aspects:</b> During the early 14th century, the people of Milan turned to the Visconti, a powerful and landed Ghibelline family, for protection against invaders and encroachers. During the rule of Giovanni and Lucchino (sons of Matteo, the first Visconti ruler of Milan), Milan annexed Genoa (1353) and greatly consolidated its power in Lombardy. In 1354 Galeazzo II and Bernab&ograve; (nephews of Giovanni) took control of the city and remained in power until 1385 when Gian Galeazzo, son of Galeazzo II, had Bernab&ograve; put to death and assumed political control. Under his reign, though the citizens of Milan suffered notable restrictions on personal liberty, the city expanded its sphere of influence to include much of Northern and Central Italy. Gian Galeazzo ran a form of government closely resembling a modern bureaucracy, founded the Milan cathedral and was a patron of literature and art.</p>
