<h2>Filippo Argenti (Filippo Adimari): <em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov0908">IX.8</a></h2>
<p>What little is known of Filippo Argenti derives from Dante Alighieri's depiction of him in <em>Inf</em>. VIII.62ff and the commentary tradition. Filippo was of the Cavicciuoli branch of the Adimari family, probably aligned with the Black Guelphs. The Adimari family was rich and powerful, although their ancestry was not aristocratic. Filippo's personal wealth is suggested by the anecdote (appearing in Boccaccio's <em>Esposizioni</em>, and in the <em>Chiose Cassinesi</em>) that the appellation &quot;Argenti&quot; was given him because he shoed his horses with silver.</p>
<p>Dante assigns Filippo a place in Hell, among the wrathful in the river Styx, where he once again demonstrates the hot temper for which he was known. Both Sacchetti (<em>Trecentonovelle</em> (CXIV) and Boccaccio (<em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov0908&expand=day09">IX.8</a>) depict Filippo in conformity with that characterization.</p>
<p>In <em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov0908&expand=day09">IX.8</a>, Filippo is described as &quot;uom grande e nerboruto e forte, sdegnoso, iracundo e bizzarro&quot;. In the novella, his anger is intentionally provoked by Ciacco, as part of a practical joke on a third party. Boccaccio stretches the limits of Filippo's conventional image by placing it within a purely comic context.</p>
