<h2>Cino da Pistoia (c. 1270 - c. 1336): <em>Decameron</em> IV, <a href="../../texts/DecShowText.php?lang=eng&myID=day04">introduction</a></h2>
<p>Guittoncino dei Sigisbuldi, commonly known as Cino da Pistoia, was a poet and jurist living in Italy during Dante's time. Cino studied law in Pistoia and at the University of Bologna, where he received his doctorate in 1314. At the time of the Emperor Henry VII's entry into Italy, Cino, like Dante, warmly supported the emperor, and participated in many of Henry's diplomatic missions. Cino, who was well-known as a judge, professor of law, and as author of legal texts, also distinguished himself as a poet. He was honored by his contemporaries for both his legal and creative work, and died in 1336.</p>
<p>Cino was a friend to Dante, greatly admired and imitated by Petrarch, and perhaps Boccaccio's teacher during the latter's youthful years in <a href="../../images/maps/decworld/naples.php">Naples</a>. At the confluence of a number of literary currents, Cino's considerable poetic opus of over 150 poems has not yet received a definitive critical assessment. Cino takes up themes, techniques and meters of the stilnovistic tradition while, at the same time, recycling earlier Proven&ccedil;al, Sicilian and Guittonian models.</p>
<p>In the <a href="../../texts/DecShowText.php?lang=eng&myID=day04">introduction</a> to Day Four, Boccaccio, in response to the accusation of being too old to write for the pleasure of women, evokes Cino, together with Dante and <a href="guido_cavalcanti.php">Guido Cavalcanti</a> who, even in their old age, loved the beauty of women and sought to please them. Petrarch's name is conspicuously absent, perhaps because of the more moral character of his verse.</p>
