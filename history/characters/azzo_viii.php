<h2>Azzo VIII da Ferrara (1263 - c. 1308): <em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov0202&expand=day02">II.2</a></h2>
<p>Born into the house of Este, the ruling dynasty of Ferrara for over a century, Azzo VIII was the son of Obizzo II d'Este and Giacomina Fieschi. Azzo succeeded his father in the rule of Ferrara, Modena and Reggio in 1293.</p>
<p>Most of his reign was occupied with military conflicts with the surrounding cities of Parma, <a href="../../images/maps/decworld/bologna.php">Bologna</a> and Mantua. In 1300, during a period of relative peace, Azzo contracted a marriage between his sister Beatrice and Galeazzo Visconti, a Milanese lord. After the death of his first wife, Azzo remarried Beatrice, the daughter of <a href="charles_ii_anjou.php">Charles II of Anjou</a>, King of <a href="../../images/maps/decworld/naples.php">Naples</a>. Unfortunately, Azzo's second marriage resulted in disastrous political consequences, which led to feelings of discontent among his subject territories and his allies.</p>
<p>Azzo VIII is repeatedly mentioned in Dante's <i>Divina Commedia</i> in a negative light - guilty of patricide (<i>Inf</i>. XII.111f), a wicked seducer of women (<i>Inf</i>. XVIII.55ff), and responsible for the violent death of a young man (<i>Purg</i>. V.77f). Only his infamous lasciviousness has been substantiated with historical documentation. Boccaccio, who does not comment upon Azzo's character, sets the <a href="../../texts/DecShowText.php?lang=eng&myID=nov0202&expand=day02">second tale</a> of Day Two of the <i>Decameron</i> in Ferrara, &quot;al tempo del marchese Azzo da Ferrara.&quot;</p>
<p>(R.P./N.S.) Trevor, Dean, s.v. Este, Azzo d', Vol. 43 <i>Dizionario biografico degli italiani</i>. Roma: Istituto della Enciclopedia italiana, 1960. pp. 324-326.</p>

