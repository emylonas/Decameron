<h2>Cecco Angiolieri (1260 - c. 1312): <em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov0904">IX.4</a></h2>
<p>Descended from the illustrious Angiolieri banking family on his father's side, and the wealthy Salimbene family on his mother's side, Cecco Angiolieri was a poet of realistic, satirical verse in the goliardic tradition. Since Cecco's perception of himself as portrayed in his poetry is often fictionalized, many historians have turned to archival documents to glean more reliable biographical information. In such documents are records of his less than heroic participation in the Sienese army on two separate occasions (1281 and 1288) and of fines he received for breaking the evening curfew. Documents also mention that his estate was burdened by immense debts, so great that, in 1312, his heirs refused their claim to it.</p>
<p>Over 100 sonnets have been attributed to Cecco, including three bantering sonnets addressed to his contemporary, Dante. Dante's responses, unfortunately, have not survived. Cecco's comic, parodic, and sometimes grotesque treatment of themes - love, autobiography, local personages and happenings, as well as invectives against members of his own family - reveals an underlying technical acumen that makes him one of the foremost comic poets of the era.</p>
<p>Cecco Angiolieri is portrayed in <em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov0904&expand=day09">IX.4</a>, not as a poet, but as a young nobleman with insufficient means. Seeking to better his fortune, Cecco is humiliated by a gambling trickster in a scenario reminiscent of his verse.</p>
