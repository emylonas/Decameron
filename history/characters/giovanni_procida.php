<h2>Giovanni "Gianni" di Procida (1210 - 1298): <em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov0206">II.6</a>, <a href="../../texts/DecShowText.php?lang=eng&myID=nov0506">V.6</a></h2>
<p>Designated as Governor of Procida (an island off the coast of Sicily) by the Hohenstaufens, Gianni entered the service of <a href="frederick_iii.php">Frederick III</a> as a medical doctor, and later upheld the claims of <a href="manfred.php">Manfred</a> and Conradin to Sicily. After the defeat of Conradin at Tagliacozzo in 1268, Gianni da Procida joined the Aragonese court, where he played a significant role in the conflict between <a href="charles_i_anjou.php">Charles I</a> of Anjou and <a href="peter_iii_aragon.php">Peter III</a> for control of Italy. Popular opinion held that Gianni had been instrumental in instigating the revolt of the Sicilian Vespers. The Florentine chronicler Giovanni Villani portrays him as a &quot;savio e ingegnoso cavaliere e signore&quot; stating that &quot;Questi per suo senno e industria si pens&ograve; di recare la forza del re Carlo in basso stato, e in parte gli venne fatto&quot; (VIII.57).</p>
<p>Boccaccio includes Gianni da Procida in two tales (<a href="../../texts/DecShowText.php?lang=eng&myID=nov0206&expand=day02">II.6</a> and <a href="../../texts/DecShowText.php?lang=eng&myID=nov0506&expand=day05">V.6</a>); in both he is described in laudatory terms as honest, courageous, and loving. Boccaccio's positive view of Gianni, despite his Angevin sympathies, is also demonstrated in the <em>De Casibus</em> IX.19.</p>
<p>(R.P./N.S.) Villani, Giovanni. <em>Nuova Cronica</em>, 3 vols, ed. Giuseppe Porta. Parma: Guanda, 1990.</p>

