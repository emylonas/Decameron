<h2>Peter III of Aragon (c. 1236 - 1285): <em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov0206">II.6</a>, <a href="../../texts/DecShowText.php?lang=eng&myID=nov1007">X.7</a>
</h2>
<p>
Son of James I of Aragon, Peter III was King of Aragon from 1276-1285.
Having married Constance, daughter of <a href="manfred.php">Manfred</a>, he came forward as the
representative of the claims of the Hohenstaufen in Naples and Sicily
against <a href="charles_i_anjou.php">Charles</a>, Duke of Anjou.  During the rebellion of the Sicilian
Vespers (1282), he fought vigorously, reconquering the Sicilian kingdom
from the French, and was thereby proclaimed king in 1282. Angevin efforts
to regain the kingdom were futile, and Peter ruled Sicily until his death
in 1285, leaving the throne to his sons, James II and <a href="frederick_iii.php">Frederick III</a>.
<p>
Dante speaks highly of Peter, placing him in Ante-Purgatory, stating that "d'ogni valor port√ö cinta la corda" (<i>Purg</i>
VII.114). The Florentine chronicler Villani affirms that Peter was of
excellent character, courageous and wise, a great warrior, and feared by
both Christians and Saracens, "like no other king of his time" (<i>Nuova
cronica</i> VIII.103).  In <i>Decameron</i> <a href="../../texts/DecShowText.php?lang=eng&myID=nov0206">II.6</a>, Peter is depicted
positively from the Ghibelline point of view of <a href="currado_ii_malaspina.php">Currado II</a> during the
Sicilian Vespers; in <a href="../../texts/DecShowText.php?lang=eng&myID=nov1007">X.7</a>, Peter is portrayed as the subject of the passion
of a young patrician woman.
