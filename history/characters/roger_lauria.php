<h2>Ruggiero di Lauria (c. 1250-1305): <em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov0506">V.6</a></h2>
<p>Sicilian nobleman Ruggiero di Lauria entered the service of <a href="manfred.php">Manfred</a>'s daughter, Constance (wife of <a href="peter_iii_aragon.php">Peter III</a> of Aragon), after the defeat of Manfred in 1266. In the following years, Ruggiero was to play a decisive role in the war between France and Aragon for control of Sicily.</p>
<p>In 1283, Ruggiero was appointed as Grand Admiral by Peter III, who had recently been proclaimed King of Sicily by the Sicilians themselves in a revolt against <a href="charles_i_anjou.php">Charles I</a> of Anjou. Shortly afterward, Ruggiero led the Sicilian fleet to victory over the French in Malta (1283) and in the Bay of Naples (1284), capturing <a href="charles_ii_anjou.php">Charles II</a> of Anjou. In 1285, Ruggiero protected Catalonia from the attacks of the French King Phillip III and later conquered the island of Majorca.</p>
<p>When the successor to Peter III, James II, agreed to trade Sicily to Charles II of Anjou, the vexed Sicilians offered the crown to James' younger brother, <a href="frederick_iii.php">Frederick III</a>. This episode is commonly known as the Sicilian Vespers. Initially, Ruggiero sided with Frederick, helping him to defend his crown. However, taking into consideration his sizable holdings in Valencia, the admiral eventually shifted his allegiance to the side of James II and the Angevins, fighting against the Sicilians yet again in 1299 and 1300. After the Peace of Caltabelotta in 1302, Ruggiero retired and returned to Spain.</p>
<p>In the <a href="../../texts/DecShowText.php?lang=eng&myID=nov0506&expand=day05">sixth tale</a> of Day Five, Ruggiero, in the service of King Frederick III of Sicily, rescues Gianni da Procida from the unjust wrath of the king.</p>
<p>(R.P./N.S.) Quintana, Manuel Jos&eacute;. <em>El Cid, Guzm&aacute;n el Bueno, Roger de Lauria</em>. Madrid: Espasa-Calpe, 1959.</p>

