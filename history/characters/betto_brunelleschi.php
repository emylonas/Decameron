<h2>Betto Brunelleschi (Betto, short for Brunetto): <em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov0609&expand=day06">VI.9</a></h2>
<p>Betto Brunelleschi was a Florentine magnate, a Black Guelph, and part of the leadership elite between 1301 and 1311, during the ascendancy of his party in Florence. Originally a Ghibelline, Betto held a number of important political posts during the last decade of the 13th century. In the wake of <a href="charles_iii_valois.php">Charles of Valois</a>' arrival in Florence, his allegiance shifted exclusively to the Black faction who assumed complete control of the government.</p>
<p>Betto became one of the leaders of the Black party and distinguished himself as an able ambassador to the Papacy. In the years that followed, the Black faction began to lose its cohesion; infighting resulted in the alienation and eventual death of the bellicose Black leader <a href="corso_donati.php">Corso Donati</a>. Despite his suspected involvement in the murder of members of the Donati and Cavalcanti families, Betto tried to revive friendly relations with them. In 1311, he was killed in his own house by young men from the Donati clan.</p>
<p>Boccaccio's depiction of Betto in <a href="../../texts/DecShowText.php?lang=eng&myID=nov0609&expand=day06">VI.9</a> as a &quot;sottile e intendente cavaliere,&quot; able to grasp <a href="guido_cavalcanti.php">Guido Cavalcanti</a>'s subtle insult, is rather far removed from the image left to us by history. What rings true, however, is the violent animosity existing between the members of the different factions.</p>
<p>(R.P./N.S.) Cardini, Franco. s.v. Brunelleschi, Betto. Vol. 14.
<em>Dizionario biografico degli italiani</em>, Roma: Istituto della Enciclopedia italiana, 1960. pp. 532-534.

