<h2>Frederick I "Barbarossa" (1123 - 1190): <em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov1009">X.9</a></h2>
<p>Frederick I, nephew of Emperor Conrad III of the Hohenstaufen dynasty, was one of the greatest monarchs of medieval Germany - brave, intelligent and chivalrous. He was Holy Roman Emperor from 1152 to 1190, and, simultanously, King of both Germany and Italy.</p>
<p>As King of Germany, Frederick managed to negotiate a settlement with the opposing Welf family, thus consolidating his imperial domains in Germany, and establishing a well-structured feudalism. Shortly after becoming Emperor, Frederick set his sights on Northern  Italy, and initially gained control over significant territories. This led to his eventual clash with the Papacy, as Frederick claimed the right to appoint municipal authorities and collect taxes in conformity with Roman Law. His early successes were checked by the subsequent formation of a Lombard League in 1167 and his defeat at the battle of Legnano in 1176. The Emperor later abandoned his ambitions for domination of the Northern Italian cities, granting them freedom at the Peace of Constance in 1183.</p>
<p>In later years, Frederick returned his focus to the situation in Germany, and defeated the Welf party, which at this time was led by his cousin, Henry the Bold. By arranging the marriage of his son, Henry, to Constance of Altavilla, heiress to the throne of Sicily, Frederick secured Henry's later inheritance of the Sicilian kingdom. Frederick I dies in 1190 while participating in the Third Crusade.</p>
<p>The name of Frederick I in Decameron <a href="../../texts/DecShowText.php?lang=eng&myID=nov1009&expand=day10">X.9</a> helps to set the chronological scene for the narration, although the main character of the novella is the famed <a href="saladin.php">Saladin</a> of the same period.</p>
