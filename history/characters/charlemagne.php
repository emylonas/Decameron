<h2>Charlemagne "Charles the Great" (742 - 814AD): <em>Decameron</em>, conclusion</h2>
<p>The son of Pepin, King of the Franks, Charlemagne inherited the throne jointly with his brother Carloman in 768 AD, and, at Carloman's death in 771 AD, became the sole ruler of the Frankish kingdom. As King, Charlemagne came to the aid of Pope Adrian I against the Lombards, eventually conquering and absorbing their kingdom into his own. He also subdued the Saxon people and converted them forcefully to Christianity. Although Charlemagne's armies advanced into Spain, his campaign there was unsuccessful, and resulted in the retreat across the Pyrenees immortalized in the <em>Song of Roland</em>. Charlemagne absorbed Bavaria into his Empire and held a loose sovereignty over the neighboring Slavic peoples. His sovreignty eventually expanded to include nearly all the Christian lands of Western Europe.</p>
<p>Charlemagne established his court at Aachen (present-day Aix-la-Chapelle), where, in addition to his political and military achievements, he fostered a cultural revival known as the &quot;Carolingian Renaissance.&quot; His administration of the kingdom was personal and centralized; there was no great distinction between the secular and religious functions of government. By fashioning himself as a defender of the Church, Charlemagne was admired by succeeding generations as the ideal unifier of Church and State. Such an idealization inspired Florentine chronicler Giovanni Villani to include him as one of the legendary founders of Florence (<em>Nuova Cronica</em> IV.1).</p>
<p>Charlemagne's reputation as a leader of knights is recalled in the concluding chapter of the <em>Decameron</em>.</p>
<p>(R.P./N.S.) Favier, Jean. <em>Charlemagne</em>. Paris: Fayard, 1999.</p>

