<h2>Conrad II (Currado) Malaspina (died c. 1294): <em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov0206">II.6</a></h2>
<p>The Malaspina family of Northern Italy were wealthy nobles who supported the imperial line. Currado II, who was the son of Federigo I, marquis of Villafranca in Val di Magra, held a prominent place in the Malaspina clan. He was the grandson of Currado I (founder of the &quot;Spino secco&quot; branch of the family) and great-grandson of <a href="frederick_ii.php">Emperor Frederick II</a> whose natural daughter, Costanza, was Currado I's wife.</p>
<p>Currado II figures appears in Dante's <em>Commedia</em>; the poet places him in the Valley of Princes (<em>Purg</em>. VIII.65, 118). In a dialogue with the spirit of Currado II, Dante strongly praises Currado's family, asserting that it still retains the gallantry and civility for which it is famed throughout Europe. Currado responds by stating that Dante will personally witness the truth of that reputation before seven years have passed. In fact, the poet was warmly received in Lunigiana by Franceschino Malaspina, cousin of Currado, in 1306.</p>
<p>The portrayal of Currado and his &quot;valorosa e santa&quot; wife in the <a href="../../texts/DecShowText.php?lang=eng&myID=nov0206&expand=day02">sixth story</a> of Day Two of the <em>Decameron</em> is consistently positive. He is depicted as a generous and compassionate nobleman who helps Madonna Beritola and her the members of her family reunite after political upheaval in Sicily has separated them and thrown them into poverty and misfortune.</p>
<p>(R.P./N.S.) Piattoli, Renato. s.v. Malaspina, Currado. Vol. 3.
<em>Enciclopedia dantesca</em>, Roma: Istituto della Enciclopedia italiana, 1970-78. p. 779; Toynbee, P. <em>Dictionary of Proper Names and Notable Matters in the Works of Dante</em>. Oxford: Clarendon, 1968. pp. 353-354.</p>

