<h2>Alfonso VIII of Castile (1155-1214): <em>Decameron</em> <a href="../../texts/DecShowText.php?lang=eng&myID=nov1001&expand=day10">X.1</a></h2>
<p>In 1029, Castile, which had originally been a county within the domain of Len, was declared a separate kingdom by Sancho the Great of Navarre. In the course of the 11th century, Castile joined once again with Le&oacute;n, but broke off shortly thereafter. Alfonso VIII, son of Sancho III and Blanche of Navarre, succeeded to the throne of Castile at the age of three.</p>
<p>Throughout the greater part of Alfonso VIII's reign, he engaged in conflicts with the Moorish Almohad dynasty and neighboring Christian kingdoms. Alfonso made great strides in the consolidation of Castile's military and political power by defeating the Muslims at the Battle of Las Navas de Tolosa in 1212, and by overcoming a joint attack by Le&oacute;n and Navarre in 1295. Alfonso VIII's ability to secure Castile's independence from the nearby states served as the foundation for Castile's future hegemony in Spain. His triumphs over the Moors is considered a milestone in the Christian reconquest of the Iberian peninsula.</p>
<p>Scholars have associated Alfonso VIII with a reference to &quot;lo buono re di Castella&quot; in a list of great rulers famed for their liberality in Dante's <i>Convivio</i> (IV.xi.14). Alfonso was known for his generosity, particularly in the cultural sphere; indeed, it was he who founded the University of Valencia. In accordance with this reputation, Alfonso is depicted in the <i>Decameron</i> (<a href="../../texts/DecShowText.php?lang=eng&myID=nov1001&expand=day10">X.1</a>) as a valorous and magnanimous king, conferring gifts upon one of his faithful Italian courtiers.</p>
<p>(R.P./N.S.) Mart&iacute;nez D&iacute;ez, Gonzalo <i>Alfonso VIII, rey de Castilla y Toledo</i>. Burgos: Editorial la Olmeda, 1995.</p>

