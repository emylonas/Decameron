<h2>John Florio (1553?-1625), writer and translator</h2>
									
<p>John Florio's father, Michelangelo Florio, was a Florentine Protestant who fled from persecution to England shortly before Edward VI's reign. His son, John, was born in 1553 and apparently resided in Oxford for much of his youth where he became a &quot;teacher and instructor.&quot; He dedicated his English translation of Ramuzio to Edward Bray, high sheriff of Oxfordshire, and matriculated at Magdalen College in 1581. Florio was in London at the end of the century, where he dedicated his great Italian-English dictionary to Roger, earl of Rutland. In 1603, he became reader in Italian to Queen Anne, and was appointed by the King &quot;groom of the privy chamber.&quot; Florio died in August or September of 1625 in Fulham, after having contracted the plague.
</p>
<p>The translation of the Decameron attributed to him, presented here in electronic form, appears to be the first ever realized in the English language. It opens with these words:<br>
</p>

<blockquote>
<strong>To the Reader.</strong><br />Bookes (Courteous Reader) may rightly be compared to Gardens; wherein, let the painfull Gardiner expresse neuer so much care and diligent endeauour; yet among the very fairest, sweetest, and freshest Flowers, as also Plants of most precious Vertue; ill fauoring and stinking Weeds, fit for no vse but the fire or mucke-hill, will spring and sprout vp. So fareth it with Bookes of the very best quality, let the Author bee neuer so indulgent, and the Printer vigilant: yet both may misse their ayme, by the escape of Errors and Mistakes, either in sense or matter, the one fault ensuing by a ragged Written Copy; and the other through want of wary Correction. If then the best Bookes cannot be free from this common infirmity; blame not this then, of farre lighter argument, wherein thy courtesie may helpe vs both: His blame, in acknowledging his more sufficiency, then to write so grosse and absurdly: And mine, in pardoning vnwilling Errours committed, which thy iudgement finding, thy pen can as easily correct.</p>
<p><strong>Farewell.</strong></p>
</blockquote>
<p >(R.O.) 
<p>The Decameron, containing an hundred pleasant nouels. Wittily discoursed, betweene seauen honourable ladies and three noble gentlemen. London: I. Iaggard, 1620. (2 v. ill. 26 cm., owned by the John Hay Library, Brown U.); Dictionary of National Biography, vol. XII. London: Oxford University Press, 1937-38. pp. 336-339.</p>


		      
