<div class="day" id="day00"><h2>The Decameron | Table of Contents</h2>

<p>
The present English translation is that of <a href="florio-bio.php">John Florio</a>, published in 1620 by the London publisher Iaggard.
</p>


<h3><a href="day01.php">The First Day</a></h3>
<p class="rubric">Whereon, Under The Government Of Madame Pampinea, The Argument Of Each Severall Descourse, Is Not Limitted To Any One Peculiar Subject. 
</p>

<h3><a href="day02.php">The Second Day</a></h3>
<p class="rubric">Wherein, All The Discourses Are Under The Government Of Madam Philomena: Concerning Such Men Or Women, As (In Divers Accidents) Have Been Much Mollested By Fortune, And Yet Afterward (Contrary To Their Hope And Expectation) Have Had A Happy And Successefull Deliverance. 
</p>
							
<h3><a href="day03.php">The Third Day</a></h3>
<p class="rubric">Upon Which Day, All Matters To Be Discoursed On, Doe Passe Under The Regiment Of Madam Neiphila: Concerning Such Persons As (By Their Wit And Industry) Have Attained To Their Long Wished Desires, Or Recovered Something, Supposed To Be Lost. 
</p>

<h3><a href="day04.php">The Fourth Day</a></h3>
<p class="rubric">Wherein All The Severall Descourses, Are Under The Government Of Honourable Philstratus: And Concerning Such Persons, Whose Loves Have Had Successelesse Ending. 
</p>

<h3><a href="day05.php">The Fift Day</a></h3>
<p class="rubric">Whereon, All The Discourses Do Passe Under The Government Of The Most Noble Lady Fiammetta: Concerning Such Persons, As Have Bene Successefull In Their Love, After Many Hard And Perillous Misfortunes. 
</p>

<h3><a href="day06.php">The Sixt Day</a></h3>
<p class="rubric">Governed Under The Authority Of Madam Eliza, And The Argument Of The Discourses Or Novells There To Be Recounted, Doe Concerne Sudden, Persons; Who By Some Witty Words (When Any Have Checkt Or Retorting Them) Have Revenged Themselves, In A Sudden, Unexpected And Discreet Answere, Thereby Preventing Losse, Danger, Scorne And Disgrace, Retorting Them On The Busi-Headed Questioners. 
</p>

<h3><a href="day07.php">The Seventh Day</a></h3>
<p class="rubric">When The Assembly Being Met Together, And Under The Regiment Of Dioneus: The Discourses Are Directed, For The Discoverie Of Such Policies And Deceites, As Women Have Used For Beguiling Of Their Husbandes, Either In Respect Of Their Love, Or For The Prevention Of Some Blame Or Scandall, Escaping Without Sight, Knowledge, Or Otherwise. 
</p>

<h3><a href="day08.php">The Eight Day</a></h3>
<p class="rubric">Whereon All The Discourses, Passe Under The Rule And Government, Of The Honourable Ladie Lauretta. 
</p>
								
<h3><a href="day09.php">The Ninth Day</a></h3>
<p class="rubric">Whereon, Under The Government Of Madame Aimilia, The Argument Of Each Severall Descourse, Is Not Limitted To Any One Peculiar Subject: But Every One Remaineth At Liberty, To Speak Of Whatsoever Themselves Best Pleaseth. 
</p>
								
<h3><a href="day10.php">The Tenth Day</a></h3>
<p class="rubric">Whereon, Under The Government Of Pamphilus, The Severall Arguments Do Concerne Such Persons, As Either By Way Of Liberality, Or In Magnificent Manner, Performed Any Worthy Action, For Love, Favour, Friendship, Or Any Other Honourable Occasion. 
</p>

