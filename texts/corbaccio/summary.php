				<h2>The <i>Corbaccio</i>: Summary of the Text</h2>			
					
<h3>Part I: Proemio: Incipit</h3>

<p class="bodystyle">Like Boccaccio's other works, the text begins with a formal authorial Proemio, where the author thanks God (mediated through the Virgin Mary), for a special grace which has been granted him. He now hopes that the following work will benefit his readers (the famous Boccaccian <i>utilit&agrave; topos</i>).</p>

<h3>Part II: Cornice (Frame)</h3>

<p class="bodystyle">In this part of the text, the Narrator relates the situational frame story which sets the scene for the vision to come. Alone in his room, the Narrator is suffering the desperate pains of love; he is suicidal in his distress, and then fearful for his immortal soul. The solution to his problem is presented as a brisk monologue from his personified 'pensiero'; consoled, he is able to leave the house, and socializes with his intellectual friends. He returns home to fall into a deep sleep.</p>

<h3>Part III: Sogno: Inferno (Dream: Hell)</h3>

<p class="bodystyle">The Narrator finds himself proceeding down a path in a delightful wood. As he continues along the path, he finds himself advancing faster and faster, until the landscape suddenly becomes hostile and he is halted in a tangle of thorns and briars. Trapped in this Dantean forest, reminiscent of the 'selva oscura' of <i>Inferno</i> I and wood of the suicides of <i>Inferno</i> XIII, he sees a figure approaching him.</p>

<h3>Part IV: Sogno: Dialogo (Dream: Dialogue)</h3>

<p class="bodystyle">This central (and longest section) of the text contains the whole dialogue between the Narrator and the Guide. After an initial meeting which recalls the encounter between Dante-<i>personaggio</i> and Virgil, the characters converse at length. The Narrator tells his tale of his ill-fated love-affair; the Guide responds with the famous anti-feminist invective, made more entertaining and 'realistic' by the fact that he is revealed to be the Narrator's beloved's dead husband.</p>

<h3>Part V: Sogno: Purgatorio (Dream: Purgatory)</h3>

<p class="bodystyle">After the edifying dialogue, the Narrator and his Guide advance at some speed through a sunlit landscape, which recalls the slopes of Dante's Mount Purgatory.</p>

<h3>Part VI: Cornice (Frame)</h3>

<p class="bodystyle">The Guide disappears and the Narrator awakes, to find himself cured of his affliction.</p>

<h3>Part VII: Conclusione: Explicit</h3>

<p class="bodystyle">The work ends with the author's expression of hope that his little book will be of use to others (if not actually to women).</p>


<p class="bodystyle">(G.A.)</p>

