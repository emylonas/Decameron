<p class="bodystyle">The <i>Corbaccio</i> can fairly be described as Boccaccio's most problematic text. Not only are Boccaccio scholars divided on various critical issues regarding the text (such as the meaning of the title, the date of composition, and Boccaccio's motivation for writing), but its scabrous misogynistic content has caused considerable disquiet among readers. It was extremely popular from its first circulation until the early nineteenth century, as can be seen by the fact that at least 82 manuscripts are still extant; while more than 27 different print editions in Italian alone have been recorded, since its first edition of 1487. For most of the nineteenth and twentieth centuries, it was been considered something of an embarrassment, but is now undergoing a critical rehabilitation. In the past 50 years, there has been a resurgence of interest in this enigmatic little book, with two new critical editions (Nurmela, 1968; Padoan, 1994); its first translation into English (Cassell, 1975); and the first major monograph devoted exclusively to it (Hollander, 1988). To these works we add the first online edition of the text with accompanying critical material. We hope that this edition will serve students and scholars of Boccaccio alike as a convenient desktop supplement to the <a href="biblio.shtml#crited">print editions</a>.</p>

