				<div class="day" id="day00"><h2>Select <i>Corbaccio</i> bibliography</h2>	
							
<p class="bodystyle">The reader is advised to consult the bibliographies to be found in the various editions of the text, especially those of Natali (1992); Padoan (1994); Cassell (1975); and Hollander's book <i>Boccaccio's Last Fiction</i> (1988).  Brown University library call numbers are provided where available.</p>

<a name="crited"></a><h3>Critical editions</h3>

<p class="bodystyle"><i>Corbaccio</i>, ed. by Giorgio Padoan, in <i>Tutte le opere di Giovanni Boccaccio</i>, 12 vols, ed. by Vittore Branca (Milan: Mondadori, 1964-), V (ii) (1994), 413-614</p>

<p class="bodystyle" style="margin-left:20px">The most recent critical edition, with revised text and revised numeration of paragraphs; includes manuscript and edition history; textual variants; detailed introduction and notes, index of proper names; index of nouns, bibliography. [ROCK PQ4266.A1 1964 v. 5.2]</p>

<p class="bodystyle"><i>Il Corbaccio</i>, ed. by Tauno Nurmela, Annales Academi&aelig; Scientarum Fennic&aelig; Series B, 146 (Helsinki: Suomalainen Tiedeakatemia, 1968)</p>

<p class="bodystyle" style="margin-left:20px">The previous critical edition; includes manuscript and edition history; textual variants; introduction to the text; notes; index of names; index of works cited; plates. [ROCK AS262 F5 146]</p>

<a name="othed"></a><h3>Other editions</h3>

<p class="bodystyle"><i>L'Ameto - Lettere - Il Corbaccio</i>, ed. by Nicola Bruscoli, in Giovanni Boccaccio, <i>Opere</i>, vol. V (Bari: Laterza, 1940) [ROCK PQ4270.A2 1946]</p>

<p class="bodystyle"><i>Corbaccio</i>, ed. by Pier Giorgio Ricci (Milan and Naples: Ricciardi, 1977).</p>

<p class="bodystyle"><i>Il Corbaccio</i>, ed. by Mario Marti (Galatina: Congedo, 1982)</p>

<p class="bodystyle" style="margin-left:20px">Contains introduction, brief notes; no paragraph numeration. [ROCK PQ4270 C7x 1982]</p>

<p class="bodystyle"><i>Il Corbaccio</i>, ed. by Giulia Natali (Milan: Mursia, 1992)</p>

<p class="bodystyle" style="margin-left:20px">Excellent paperback edition based on Nurmela's text; contains introduction to text; short biography of Boccaccio; bibliography; detailed notes, especially strong on identifying literary allusions to Boccaccio's sources. Paragraph numeration reproduces Nurmela's critical edition.</p>

<a name="transl"></a><h3>Translations</h3>

<p class="bodystyle"><i>The Corbaccio</i>, ed. and trans. by Anthony K. Cassell (Urbana: University of Illinois Press, 1975), 1994.</p>

<p class="bodystyle" style="margin-left:20px">The first (and best!) English translation of the <i>Corbaccio</i>, written in a neutral language which nonetheless conveys the vigour of Boccaccio's Italian. Contains translator's introduction; introduction to the text; outstanding notes; general bibliography; bibliography of manuscripts and editions of the <i>Corbaccio</i>; bibliography of modern English translations of Boccaccio's works; index. Also contains an Appendix: Sumptuary Statutes of 1355 and 1356, a translation of  Florentine ordinances against female adornment and the fines to be paid for the infractions of this code.
 [PQ 4272 E5 C6 1975]</p>

<p class="bodystyle">Cartier, Normand R., <i>Boccaccio's Revenge. A Literary Transposition of the Corbaccio (The Old Crow)</i> (The Hague: Nijhoff, 1977).</p>

<p class="bodystyle" style="margin-left:20px">A self-proclaimed transposition of the text, intended for the general reader rather than the Boccaccio specialist. The language of the translation is more colloquial than that of Cassell, and the introduction and notes belie a sometimes idiosyncratic view of Boccaccio and his career. However, the notes are detailed, and sometimes provide good background information not found in other editions of the <i>Corbaccio</i>.  [PQ 4272 E5 C6 1977]</p>

<a name="monogr"></a><h3>Monographs</h3>

<p class="bodystyle">Hollander, Robert, <i>Boccaccio's Last Fiction: Il Corbaccio</i> (Philadelphia: University of Pennsylvania Press, 1988)</p>

<p class="bodystyle" style="margin-left:20px">The fundamental study of the <i>Corbaccio</i>, which contains excellent overviews of various critical problems such as the Boccaccian Narrator, the dating of the text, the meaning of the title, the influence of Ovid and Dante; and bibliography. Also contains four appendices: 1. 'Texts in the <i>Corbaccio</i> reflecting passages in Dante', which although incomplete, is the most detailed list available in print; 2. 'The Proem of the <i>Corbaccio</i>: Sources and analogues'; 3. 'A partial census of some critical views concerning various problems in the <i>Corbaccio'</i>; 4. 'The <i>Remedia amoris</i> and the <i>Proemio</i> of the <i>Decameron</i>'.  [PQ4270 C73 H65 1988]</p>

<p class="bodystyle">Levi, Attilio, <i>Il Corbaccio e la Divina Commedia</i> (Turin: Loescher, 1889)</p>

<p class="bodystyle" style="margin-left:20px">The first detailed study of the presence of Dante in the <i>Corbaccio</i>; contains list of textual allusions and discussion of the more general influence of Dante on Boccaccio.  [HAY DANTE YB AN85]</p>

<a name="articles"></a><h3>Articles</h3>

<p class="bodystyle" style="margin-left:20px">These topics are often discussed in the editorial introduction of the editions above; in addition, see the following articles:</p>

<a name="gen"></a><h4>General articles</h4>

<p class="bodystyle">Cartier, Normand R., 'Boccaccio's Old Crow', <i>Romania</i>, 98 (1977), 331-48</p>

<p class="bodystyle" style="margin-left:20px">A general overview of the main critical questions regarding the text, featuring extracts from the author's own translation. [ROCK PC2.R6]</p>

<p class="bodystyle">Psaki, Regina, 'The Play of Genre and Voicing in Boccaccio's <i>Corbaccio</i>', <i>Italiana</i>, 5 (1993), 41-54</p>

<p class="bodystyle" style="margin-left:20px">A good introductory article, which discusses the effect of the various voices in the <i>Corbaccio</i>: Author, Narrator, Dreamer, and Spirit.</p>

<a name="text-trad"></a><h4>Textual tradition</h4>

<p class="bodystyle">Nurmela, Tauno, 'Manuscripts et &eacute;ditions du <i>Corbaccio</i> de Boccace', <i>Neuphilologische Mitteilungen</i>, 54 (1953), 102-34</p>

<p class="bodystyle" style="margin-left:20px">Describes the manuscript and print editions of the Corbaccio, including textual variants. [ROCK PB10 .N7]</p>

<a name="title"></a><h4>The meaning of the title (in addition to Hollander)</h4>

<p class="bodystyle">Bourciez, J., 'Sur l'&eacute;nigme du Corbaccio', <i>Revue des Langues Romanes</i>, 72 (1958), 330-37</p>

<p class="bodystyle" style="margin-left:20px">Reviews the various theories pertaining to the meaning of the title, and suggests links to the Old French poem <i>Renart</i>, Ovid's <i>Metamorphoses</i>, and the <i>Proverbia que dicuntur super natura feminarum</i>. [ROCK PC2.R4]</p>

<p class="bodystyle">Cassell, Anthony K., 'The Crow of the Fable and the <i>Corbaccio</i>: A Suggestion for the Title', <i>Modern Language Notes</i>, 85 (1970), 83-91</p>

<p class="bodystyle" style="margin-left:20px">Reviews the main theories of the title, and suggests that the title refers to the widow, by alluding to the classical tale of the crow adorned as a peacock. [ROCK 1-SIZE PB1 .M6]</p>

<p class="bodystyle">Hauvette, Henri, 'Une confession de Boccace: &quot;Il Corbaccio&quot;', <i>Bulletin Italien</i>, I (1901), 3-21; reprinted in <i>Etudes sur Boccace</i> (1894-1916), ed. by C. Pellegrini (Turin: Bottega d'Erasmo, 1968).</p>

<p class="bodystyle" style="margin-left:20px">Dated but interesting study of the <i>Corbaccio</i>. Hauvette contends that the text represents Boccaccio's immediate reaction to a strongly felt emotion; the work is thus a foray into a rare genre for Boccaccio, the confession. Includes a highly imaginative account of Boccaccio's supposed humiliation at the hands of 'une noble et belle veuve'. [ROCK PQ4001 .B8]</p>

<p class="bodystyle">Jeffrey, Violet M., 'Boccaccio's Titles and the Meaning of &quot;Corbaccio&quot;', <i>Modern Language Review</i>, 28 (1933), 194-204</p>

<p class="bodystyle" style="margin-left:20px">An elegant article, which considers the various theories for the word 'corbaccio' in terms of Boccaccio's own propensity for naming his books and characters from supposedly Greek derivations. The title is thus a circumlocutory neologism, with the meaning 'labyrinth of love'. [ROCK PB1 .M66]</p>

<p class="bodystyle">Nobili, Claudia Sebastiana, 'Per il titolo <i>Corbaccio</i>', <i>Studi e problemi di critica testuale</i>, 48 (1994), 93-114.</p>

<p class="bodystyle" style="margin-left:20px">Suggests an association between the words 'corbaccio' and 'Boccaccio'; the crow of the title would therefore relate to the author rather than the widow. Also discusses the image of the crow in medieval literature and culture. [ROCK P47.S7]</p>

<p class="bodystyle">Rossi, Aldo, 'Proposta per un titolo del Boccaccio: Il Corbaccio', <i>Studi di filologia italiana</i>, 20 (1962), 383-90</p>

<p class="bodystyle" style="margin-left:20px">Presents the usual survey of theories, with particular emphasis on medieval bestiaries. [PROCK PC1001 .S8]</p>

<a name="date"></a><h4>The date of composition</h4>

<p class="bodystyle">Padoan, Giorgio, 'Sulla datazione del <i>Corbaccio</i>', <i>Lettere italiane</i>, 15 (1963), 1-27 [ROCK AP37 .L52]</p>

<p class="bodystyle" style="margin-left:20px">&ndash; <i>ibid</i>, in <i>Boccaccio, le Muse, il Parnaso e l'Arno</i> (Florence: Olschki, 1978). (Updated version of this article. For an overview of historical theories regarding the dating of the Corbaccio, see pp. 200-04.) [ROCK PQ4277 .Z5 P23]</p>

<a name="interp"></a><h4>Questions of interpretation</h4>

<p class="bodystyle">Barricelli, Gian Piero, 'Satire of Satire: Boccaccio's <i>Corbaccio</i>', <i>Italian Quarterly</i>, 18 (1975), 95-111</p>

<p class="bodystyle" style="margin-left:20px">Contends that, with the <i>Corbaccio</i>, Boccaccio is not satirizing women as much as the genre of antifeminism and the stupidity of men who take it seriously. This is achieved through the presentation of such a pair of clowns as the figures of the Narrator and his Guide. Fundamental (despite being printed in an annoyingly large font).  [ROCK 1-SIZE PQ4001 .I77]</p>

<p class="bodystyle">Cassell, Anthony K., 'An Abandoned Canvas: Structural and Moral Conflict in the <i>Corbaccio</i>', <i>Modern Language Notes</i>, 89 (1974), 60-70.</p>

<p class="bodystyle" style="margin-left:20px">Presents some of the problems associated with the <i>Corbaccio</i>. [ROCK 1-SIZE PB1 .M6]</p>

<p class="bodystyle">Cottino-Jones, Marga, 'The <i>Corbaccio</i>: Notes for a Mythical Perspective of Moral Alternatives', <i>Forum Italicum</i>, 4 (1970), 490-509</p>

<p class="bodystyle" style="margin-left:20px">Considers the hero's moral crisis, his enslavement to carnal love and his spiritual enlightenment, within the mythic cycle of birth, death, and rebirth. Draws heavily on Northrop Frye's model of narrative structure. [ROCK PC1001 .F65]</p>

<p class="bodystyle">Marti, Mario, 'Per una meta-lettura del <i>Corbaccio</i>', <i>Giornale storico della letteratura italiana</i>, 153 (1976), 60-86.</p>

<p class="bodystyle" style="margin-left:20px">Attempts to gauge the extent of <i>autobiografismo</i> in the <i>Corbaccio</i>; considers such factors as Boccaccio's previous autobiographical poses; the densely allusive  nature of the text; and possible factors in Boccaccio's personal life around the proposed time of composition.</p>

<p class="bodystyle">Padoan, Giorgio, 'Il <i>Corbaccio</i> tra spunti autobiografici e filtri letterari', <i>Revue des &Eacute;tudes Italiennes</i>, 37 (1991), 21-37.</p>

<p class="bodystyle" style="margin-left:20px">A detailed overview of some of the critical issues: the manuscript tradition, the religiosity of the text, and the possibility of a real-life romantic rejection as its inspiration. [ROCK PQ4001 .R5]</p>

<p class="bodystyle">Psaki, Regina, 'Boccaccio and Female Sexuality: Gendered and Eroticized Landscapes', in <i>The Flight of Ulysses: Studies in Memory of Emmanuel Hatzantonis</i>, ed. by Augustus A. Mastri (Chapel Hill, NC: Annali d'Italianistica, 1997), pp. 125-34.</p>

<p class="bodystyle" style="margin-left:20px">An excellent article, which considers how Boccaccio uses a variety of physical spaces to represent female sexuality in both the <i>Corbaccio</i> and the <i>Decameron</i>.</p>

<p class="bodystyle">Veglia, Marco, 'Sul nodo culturale del <i>Corbaccio</i>', <i>Studi e problemi di critica testuale</i>, 52 (1996), 79-100</p>

<p class="bodystyle" style="margin-left:20px">Considers the construction of love as <i>pestis</i> in the <i>Corbaccio</i> in relation to Boccaccio's other works (the <i>Decameron</i>, the <i>Buccolicum carmen</i>, his Latin correspondence); and the intellectual influence of Petrarch. [ROCK P47 .S7]</p>

<a name="lit-sourc"></a><h4>Literary sources for the <i>Corbaccio</i></h4>

<p class="bodystyle">Armstrong, Guyda, 'Dantean Framing Devices in Boccaccio's <i>Corbaccio</i>', <i>Reading Medieval Studies</i>, 27 (2001), 139-161</p>

<p class="bodystyle" style="margin-left:20px">Discusses the Dantean allusions deployed by Boccaccio in the various narrative frames of the text. [ROCK D111 .R25]</p>
 
<p class="bodystyle">Cassell, Anthony K., '<i>Il Corbaccio</i> and the Secundus Tradition', <i>Comparative Literature</i>, 25 (1973), 352-60.</p>

<p class="bodystyle" style="margin-left:20px">Considers the text in relation to the aphorisms and biography of Secundus, which became staples of the medieval 'quid est mulier' <i>topoi</i>. Also contains good bibliography for the classical and medieval misogynist traditions. [ROCK PN851 .C6]</p>

<p class="bodystyle">Hollander, Robert, 'Texts in the <i>Corbaccio</i> reflecting passages in Dante', in <i>Boccaccio's Last Fiction: Il Corbaccio</i> (Philadelphia: University of Pennsylvania Press, 1988).</p>

<p class="bodystyle" style="margin-left:20px">Thorough table of references, also indicating which allusions have been noted by previous scholars. Incomplete but the best currently available in print.</p>

<p class="bodystyle">Levi, Attilio, <i>Il Corbaccio e la Divina Commedia</i> (Turin: Loescher, 1889)</p>

<p class="bodystyle" style="margin-left:20px">Fundamental early study of the textual echoes of Dante in the <i>Corbaccio</i>; contains (incomplete) list of allusions.   [HAY DANTE YB AN85]</p>

<p class="bodystyle">Mercuri, Roberto, 'Ritrattazione in limitare di vita e ripresa di motivi danteschi nel <i>Corbaccio</i>', in <i>Letteratura italiana: Storia e geografia</i>, ed. by Alberto Asor Rosa, 3 vols (Turin: Einaudi, 1987), I, <i>L'et&agrave; medievale</i>, 229-455, 436-44.</p>

<p class="bodystyle" style="margin-left:20px">Excellent treatment of Boccaccio's exploitation of Dantean language and imagery in the <i>Corbaccio</i>. [ROCK PQ4037 .L457 1982 v.7.1]</p>

