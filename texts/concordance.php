<h2>The Online Concordance to the <i>Decameron</i></h2>
<p>
This thoroughgoing concordance is intended to provide scholars, teachers and students with a reliable and easily accessible lexical index of Boccaccio's masterpiece. Until now, this sort of inventory was available only in the edition compiled by Alfredo Barbina under the direction of Umberto Bosco (<i>Concordanze del "Decameron"</i>, 2 vol., Firenze, Giunti, 1969). Since this work has for many years been out of print and is currently held by relatively few North American libraries, however, the editors of the <i>Decameron</i> Web decided to produce a new version, based on Branca's updated edition, for the benefit of readers around the world. In addition to its immediate availability, this concordance differs from Barbina's in a couple of other ways as well. We have chosen the KWIC format (key words in context) for display so that users may more easily find combinations that interest them, e.g. <i>s&iacute; come</i>, <i>s&iacute; fatto</i> and so on. All words are listed in alphabetical order rather than being grouped with infinitives or masculine singular forms. In this way, users can quickly locate specific verbs and adjectives by number and gender. Those interested in all forms of a specific verb or adjective should consult the lexicon (in <a href="concordance/lexicon.pdf">PDF</a> or <a href="concordance/lexicon.zip">ZIP</a> format) where they may browse all the words that appear in the <i>Decameron</i>. Many of the unusual forms that may be missed, as well as variant spellings, have been cross-referenced for convenience. As a general rule, if an uncommon form does not appear at a reasonable distance from its relative, an indication will be provided. Note, however, that we have taken it for granted that readers of the <i>Decameron</i> are already familiar with frequent medieval spellings and complete verb conjugations. Some of the most frequent words have been eliminated. These include most subject pronouns, possessive adjectives, articles, prepositions, some selected adverbs such as <i>non</i>, object pronouns including <i>ci</i> and <i>ne</i>, and a small selection of very common conjunctions such as <i>ma</i>, <i>e</i> and <i>o</i>. When in doubt, the user should consult the lexicon. Note that names are not separated unless, as in the case of Can Grande, they may be mistaken for another word.

<p>
We have attempted to distinguish homographs as clearly as possible. Abbreviations commonly used include <i>adj.</i> (adjective), <i>adv.</i> (adverb), <i>interj.</i> (interjection), <i>n.</i> (noun), <i>p.p.</i> (past participle), <i>pr.</i> (pronoun), <i>num.</i> (number), <i>v.</i> (verb form), <i>&lt;</i> (derives from) and <i>cf.</i> (<i>confer</i> or compare).

