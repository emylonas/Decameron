<h2>Seconda Giornata - Novella Seconda</h2>
<p><span class="framing">[Voice: filostrato]</span><br>
  [001]
Rinaldo d'Asti, rubato, capita a 
            Castel Guiglielmo e è albergato da una 
            donna vedova; e, de' suo' danni ristorato, sano e salvo si torna a casa sua.</p>
<div><p><span class="framing">[Voice: author]</span><br>
  [002]
Degli accidenti di 
            Martellino da 
            <span class="highlight">Neifile</span> raccontati senza modo risero le donne, e massimamente tra' giovani 
            Filostrato; al quale, per ciò che appresso di 
            <span class="highlight">Neifile</span> sedea, comandò la reina che novellando la seguitasse. Il quale senza indugio alcuno incominciò:</p></div>
<div><p><span class="framing">[Voice: filostrato]</span><br>
  [003]
Belle donne, a raccontarsi mi tira una novella di cose catoliche e di sciagure e d'amore in parte mescolata, la quale per avventura non fia altro che utile avere udita; e spezialmente a coloro li quali per li dubbiosi paesi d'amore sono caminanti, ne' quali chi non ha detto il paternostro di san Giuliano spesse volte, ancora che abbia buon letto, alberga male.</p></div>
<p><span class="framing">[Voice: filostrato]</span><br>
  [004]
Era adunque, al tempo del 
          marchese Azzo da Ferrara, un mercatante chiamato 
          Rinaldo d'Asti per sue bisogne venuto a 
          Bologna; le quali avendo fornite e a casa tornandosi, avvenne che, uscito di 
          Ferrara e cavalcando verso 
          Verona, s'abbatté in alcuni li quali mercatanti parevano, e erano masnadieri e uomini di malvagia vita e condizione, con li quali ragionando incautamente s'accompagnò. 
          
  [005]
Costoro, veggendol mercatante e stimando lui dovere portar denari, seco diliberarono che, come prima tempo si vedessero, di rubarlo: e per ciò, acciò che egli niuna suspeccion prendesse, come uomini modesti e di buona condizione pure d'oneste cose e di lealtà andavano con lui favellando, rendendosi in ciò che potevano e sapevano umili e benigni verso di lui: per che egli gli avergli trovati si reputava in gran ventura, per ciò che solo era con un suo fante a cavallo.</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [006]
E cosí camminando, d'una cosa in altra, come ne' ragionamenti addivien, trapassando, caddero in sul ragionare delle orazioni che gli uomini fanno a Dio; e l'uno de' masnadieri, che eran tre, disse verso 
          Rinaldo: 
          "E voi, gentile uomo, che orazione usate di dir camminando?"</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [007]
Al quale 
          Rinaldo rispose: 
          "Nel vero io sono uomo di queste cose materiale e rozzo, e poche orazioni ho per le mani, sí come colui che mi vivo all'antica e lascio correr due soldi per ventiquatro denari; ma nondimeno ho sempre avuto in costume, camminando, di dir la mattina, quando esco dell'albergo, un paternostro e una avemaria per l'anima del padre e della madre di san Giuliano, dopo il quale io priego Idio e lui che la seguente notte mi deano buono albergo. 
          
  [008]
E assai volte già de' miei dí sono stato, camminando, in gran pericoli, de' quali tutti scampato pur sono la notte poi stato in buon luogo e bene albergato: per che io porto ferma credenza che san Giuliano, a cui onore io il dico, m'abbia questa grazia impetrata da Dio; né mi parrebbe il dí bene potere andare né dovere la notte vegnente bene arrivare, che io non l'avessi la mattina detto".</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [009]
A cui colui, che domandato l'avea, disse: 
          "E istamane dicestel voi?"</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [010]
A cui 
          Rinaldo rispose: 
          "Sí bene".</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [011]
Allora quegli, che già sapeva come andar doveva il fatto, disse seco medesimo: 
          "Al bisogno ti fia venuto, ché, se fallito non ci viene, per mio avviso tu albergherai pur male"; 
          
  [012]
e poi gli disse: 
          "Io similmente ho già molto camminato e mai nol dissi, quantunque io l'abbia a molti molto udito già commendare, né giammai non m'avenne che io per ciò altro che bene albergassi; e questa sera per avventura ve ne potrete avvedere chi meglio albergherà, o voi che detto l'avete o io che non l'ho detto. Bene è il vero che io uso in luogo di quello il Dirupisti o la 'Ntemerata o il Deprofundi, che sono, secondo che una mia avola mi solea dire, di grandissima virtú".</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [013]
E cosí di varie cose parlando e al lor cammin procedendo e aspettando luogo e tempo al lor malvagio proponimento, avvenne che, essendo già tardi, di là dal 
          Castel Guiglielmo, al valicar d'un 
          fiume questi tre, veggendo l'ora tarda e il luogo solitario e chiuso, assalitolo il rubarono, e, lui a piè e in camiscia lasciato, partendosi dissero: 
          "Va e sappi se il tuo san Giuliano questa notte ti darà buono albergo, ché il nostro il darà bene a noi"; e valicato il fiume andaron via.</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [014]
Il fante di 
          Rinaldo veggendolo assalire, come cattivo, niuna cosa al suo aiuto adoperò, ma volto il cavallo sopra il quale era non si ritenne di correre sí fu a 
          Castel Guiglielmo, e in quello, essendo già sera, entrato, senza darsi altro impaccio albergò.</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [015]
Rinaldo, rimaso in camiscia e scalzo, essendo il freddo grande e nevicando tuttavia forte, non sappiendo che farsi, veggendo già sopravenuta la notte, tremando e battendo i denti, cominciò a riguardare se da torno alcuno ricetto si vedesse dove la notte potesse stare, che non si morisse di freddo; ma niun veggendone, per ciò che poco davanti essendo stata guerra nella contrada v'era ogni cosa arsa, sospinto dalla freddura, trottando si dirizzò verso 
          Castel Guiglielmo, non sappiendo perciò che il suo fante là o altrove si fosse fuggito, pensando, se dentro entrar vi potesse, qualche soccorso gli manderebbe Idio. 
          
  [016]
Ma la notte obscura il sopraprese di lungi dal 
          castello presso a un miglio: per la qual cosa sí tardi vi giunse, che, essendo le porti serrate e i ponti levati, entrar non vi poté dentro. 
          
  [017]
Laonde, dolente e isconsolato piagnendo, guardava dintorno dove porre si potesse, che almeno addosso non gli nevicasse: e per avventura vide una casa sopra le mura del 
          castello sportata alquanto in fuori, sotto il quale sporto diliberò d'andarsi a stare infino al giorno. E là andatosene e sotto quello sporto trovato uno uscio, come che serrato fosse, a piè di quello ragunato alquanto di pagliericcio che vicin v'era, tristo e dolente si pose a stare, spesse volte dolendosi a san Giuliano, dicendo questo non essere della fede che aveva in lui. 
          
  [018]
Ma san Giuliano, avendo a lui riguardo, senza troppo indugio gli apparecchiò buono albergo.</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [019]
Egli era in questo 
          castello una 
          donna vedova, del corpo bellissima quanto alcuna altra, la quale il 
          marchese Azzo amava quanto la vita sua e quivi a instanzia di sé la facea stare: e dimorava la predetta 
          donna in quella casa, sotto lo sporto della quale 
          Rinaldo s'era andato a dimorare. 
          
  [020]
E era il dí dinanzi per avventura il marchese quivi venuto per doversi la notte giacere con essolei, e in casa di lei medesima tacitamente aveva fatto fare un bagno e nobilmente da cena. E essendo ogni cosa presta (e niuna altra cosa che la venuta del marchese era da lei aspettata) avvenne che un fante giunse alla porta, il quale recò novelle al marchese per le quali a lui subitamente cavalcar convenne: per la qual cosa, mandato a dire alla 
          donna che non l'attendesse, prestamente andò via. 
          
  [021]
Onde la 
          donna, un poco sconsolata, non sappiendo che farsi, diliberò d'entrare nel bagno fatto per lo marchese e poi cenare e andarsi a letto; e cosí nel bagno se n'entrò.</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [022]
Era questo bagno vicino all'uscio dove il meschino 
          Rinaldo s'era accostato fuori della terra; per che, stando la 
          donna nel bagno, sentí il pianto e 'l triemito che 
          Rinaldo faceva, il quale pareva diventato una cicogna: laonde, chiamata la sua 
          fante, le disse: 
          "Va sú e guarda fuori del muro a piè di questo uscio chi v'è e chi egli è e quel ch'el vi fa". 
          
  [023]
La 
          fante andò e aiutandola la chiarità dell'aere vide costui in camiscia e scalzo quivi sedersi, come detto è, tremando forte; per che ella il domandò chi el fosse. 
          
  [024]
E 
          Rinaldo, sí forte tremando che appena poteva le parole formare, chi el fosse e come e perché quivi quanto piú brieve poté le disse: e poi pietosamente la cominciò a pregare che, se esser potesse, quivi non lo lasciasse di freddo la notte morire. 
          
  [025]
La 
          fante, divenutane pietosa, tornò alla 
          donna e ogni cosa le disse. La qual similmente pietà avendone, ricordatasi che di quello uscio aveva la chiave, il quale alcuna volta serviva alle occulte entrate del marchese, disse: 
          "Va e pianamente gli apri; qui è questa cena e non saria chi mangiarla, e da poterlo albergar ci è assai".</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [026]
La 
          fante, di questa umanità avendo molto commendata la 
          donna, andò e sí gli aperse; e dentro messolo, quasi assiderato veggendolo, gli disse la 
          donna: 
          "Tosto, buono uomo, entra in quel bagno, il quale ancora è caldo".</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [027]
E egli questo, senza piú inviti aspettare, di voglia fece: e tutto dalla caldezza di quello riconfortato da morte a vita gli parve esser tornato. La 
          donna gli fece apprestare panni stati del marito di lei poco tempo davanti morto, li quali, come vestiti s'ebbe, a suo dosso fatti parevano; e aspettando quello che la 
          donna gli comandasse incominciò a ringraziare Idio e san Giuliano che di sí malvagia notte, come egli aspettava, l'avevano liberato e a buono albergo, per quello che gli pareva, condotto. 
          
  [028]
Appresso questo la 
          donna, alquanto riposatasi, avendo fatto fare un grandissimo fuoco in una sua camminata, in quella se ne venne e del buono uomo domandò che ne fosse.</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [029]
A cui la 
          fante rispose: 
          "Madonna, egli s'è rivestito e è un bello uomo e pare persona molto da bene e costumato".</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [030]
"Va dunque,"disse la 
          donna"e chiamalo e digli che qua se ne venga: al fuoco e si cenerà, ché so che cenato non ha".</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [031]
Rinaldo nella camminata entrato, e veggendo la 
          donna e da molto parendogli, reverentemente la salutò e quelle grazie le quali seppe maggiori del beneficio fattogli le rendé. 
          
  [032]
La 
          donna, vedutolo e uditolo e parendole quello che la 
          fante dicea, lietamente il ricevette e seco al fuoco familiarmente il fé sedere e dello accidente che quivi condotto l'avea il domandò: alla quale 
          Rinaldo per ordine ogni cosa narrò. 
          
  [033]
Aveva la 
          donna, nel venire del fante di 
          Rinaldo nel 
          castello, di questo alcuna cosa sentita, per che ella ciò che da lui era detta interamente credette, e sí gli disse ciò che del suo fante sapea e come leggiermente la mattina appresso ritrovare il potrebbe. 
          
  [034]
Ma poi che la tavola fu messa, come la 
          donna volle, 
          Rinaldo con lei insieme, le mani lavatesi, si pose a cenare. 
          
  [035]
Egli era grande della persona e bello e piacevole nel viso e di maniere assai laudevoli e graziose e giovane di mezza età; al quale la 
          donna avendo piú volte posto l'occhio addosso e molto commendatolo, e già, per lo marchese che con lei doveva venire a giacersi, il concupiscibile appetito avendo desto nella mente ricevuto l'avea. Dopo la cena, da tavola levatasi, con la sua fante si consigliò se ben fatto le paresse che ella, poi che il marchese beffata l'avea, usasse quel bene che innanzi l'aveva la fortuna mandato.</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [036]
La 
          fante, conoscendo il disiderio della sua 
          donna, quanto poté e seppe a seguirlo la confortò; per che la 
          donna, al fuoco tornatasi dove 
          Rinaldo solo lasciato aveva, cominciatolo amorosamente a guardare, gli disse: 
          
  [037]
"Deh, 
          Rinaldo, perché state voi cosí pensoso? non credete voi potere essere ristorato d'un cavallo e d'alquanti panni che voi abbiate perduti? Confortatevi, state lietamente, voi siete in casa vostra. Anzi vi voglio dir piú avanti: che, veggendovi cotesti panni indosso, li quali del mio morto marito furono, parendomi voi pur desso, m'è venuta stasera forse cento volte voglia d'abracciarvi e di basciarvi: e, s'io non avessi temuto che dispiaciuto vi fosse, per certo io l'avrei fatto".</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [038]
Rinaldo, queste parole udendo e il lampeggiar degli occhi della 
          donna veggendo, come colui che mentacatto non era, fattolesi incontro con le braccia aperte, disse: 
          "Madonna, pensando che io per voi possa omai sempre dire che io sia vivo, a quello guardando donde torre mi faceste, gran villania sarebbe la mia se io ogni cosa che a grado vi fosse non m'ingegnassi di fare; e però contentate il piacer vostro d'abracciarmi e di basciarmi, ché io abraccerò e bascerò voi vie piú che volentieri".</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [039]
Oltre a queste non bisognar piú parole. La 
          donna, che tutta d'amoroso disio ardeva, prestamente gli si gittò nelle braccia; e poi che mille volte, disiderosamente strignendolo, basciato l'ebbe e altrettante da lui fu basciata, levatisi di quindi nella camera se ne andarono, e senza niuno indugio coricatisi pienamente e molte volte, anzi che il giorno venisse, i loro disii adempierono. 
          
  [040]
Ma poi che a apparir cominciò l'aurora, sí come alla 
          donna piacque levatisi, acciò che questa cosa non si potesse presummere per alcuno, datigli alcuni panni assai cattivi e empiutagli la borsa di denari, pregandolo che questo tenesse celato, avendogli prima mostrato che via tener dovesse a venir dentro a ritrovare il fante suo, per quel usciolo onde era entrato il mise fuori.</p>
<p><span class="framing">[Voice: filostrato]</span><br>
  [041]
Egli, fatto dí chiaro, mostrando di venire di piú lontano, aperte le porte, entrò nel 
          castello e ritrovò il suo fante; per che, rivestitosi de' panni suoi che nella valigia erano e volendo montare in sul cavallo del fante, quasi per divino miracolo addivenne che li tre masnadieri che la sera davanti rubato l'aveano, per altro maleficio da lor fatto poco poi appresso presi, furono in quello 
          castel menati; e per confessione da loro medesimi fatta, gli fu restituito il suo cavallo, i panni e i denari, né ne perdé altro che un paio di cintolini de' quali non sapevano i masnadieri che fatto se n'avessero. 
          
  [042]
Per la qual cosa 
          Rinaldo, Idio e san Giulian ringraziando, montò a cavallo e sano e salvo ritornò a casa sua; e i tre masnadieri il dí seguente andaro a dare de' calci a rovaio.</p>
 
