								<p class="bodystyle"><b>To celebrate the 700th anniversary of Giovanni Boccaccio’s birth and the year of Italian Culture in the United States the Decameron Web @ Brown University in collaboration with the Italian Consulate General in Boston, Massachusetts and the Ente Nazionale Giovanni Boccaccio of Certaldo, Italy announces...</b></p>
								<p class="bodystyle"><b><i>New extended deadline for submissions: October 31, 2013</i></b></p>
								<p class="bodystyle"><b>The Boccaccio AfterLife Prize for Best Translation and Adaptation of a Decameron novella into any Media</b> (Text, HyperText, Theatre, YouTube movie, Videogame, Twitter, or any other social media, animation or visualization on the digital platform).</p>
								<p class="bodystyle">The adaptation of literary works into other media is a cultural phenomenon which long predates our contemporary transition from an analog to a digital environment. However, ‘adaptation’ has acquired a new meaning within our contemporary context, a context increasingly characterized by what we call ‘trans-mediality,’ or cultural production across multiple media platforms. (Harry Potter - in his incarnations as literary character, film star, videogame hero, internet fanfiction persona, etc., comes immediately to mind.)</p>
								<p class="bodystyle">Boccaccio belongs to that elite group of authors whose legacy widely transcends the written word.  Adaptations of the <i>Decameron</i> surface in the cinema, digital projects, even computer games - perhaps only Dante’s <i>Comedy</i>, among Italian literary classics, has known similar popularity outside the limits of academe.</p>
								<p class="bodystyle">With this competition, we want to harness the creativity of students and readers of Boccaccio, inviting them to translate a novella of their choice into their own, contemporary language, social context and preferred media. </p>
								<p class="bodystyle"><b>Prize:</b> Winners will be selected in three categories: best translation-adaptation into contemporary English, best adaptation for the stage, and best adaptation for other media (Youtube, social networks, animation, videogames, and similar) The winners will receive $500 and publication of the project on the Decameron Web site. Finalists will be invited to Providence for a celebratory event organized in collaboration with the Italian Consulate General in Boston and the Casa del Boccaccio Foundation of Certaldo, Italy.  The ceremony will be introduced by a roundtable discussion on adaptations of Boccaccio’s works throughout the centuries (announcement to follow), and a theater group from Boccaccio’s birth town of Certaldo (Florence) will perform two short plays adapted from the Decameron. <b>The competition is open to participants from any country.</b></p>
								<p class="bodystyle"><b>Deadline:</b>  All submissions, in whatever language or media, should reach us no later than <b>October 31, 2013.</b></p>
								<p class="bodystyle">Address all correspondence to: The Boccaccio AfterLife Prize, c/o Department of Italian Studies, Box 1942, Brown University, Providence, RI 02912, USA; or email: vhl@brown.edu</p>
