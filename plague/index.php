<h2>The Plague</h2>
<p>There is an enormous amount of  scholarship on the Plague and the discussion about its causes, origins and effects on 14th-century culture and society is still raging. Two classic studies on this topic are: David Herlihy, <i>The Black Death and the Transformation of the West</i>, Cambridge: Harvard University Press, 1997;  and Philip Ziegler, <i>The Black Death</i>, London, Collins, 1979. Among more recent contributions, in English: John Aberth, <em>The Black Death:  The Great Mortality of 1348-1350, </em>Bedford Saint Martin, 2005; Shona Kelly Wray, &quot;Boccaccio and the doctors: medicine and compassion in the face of the plague,&quot; <em>Journal of Medieval History</em>; Sept. 2004, Vol. 30 Issue 3, p.301-322, and  Samuel Kline Cohn, Jr., &ldquo;The Black Death: The End of a Paradigm,&rdquo; <em>The American Historical Review</em> 107.3 (June 2002):  p.703 (36). <!-- End -->
<!-- Bottom links --></p>
