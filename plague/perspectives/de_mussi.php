<h2>Gabriele de' Mussi on the Plague</h2>
<p>Gabriele de' Mussi, a notary from Piacenza, gave a vivid account of the plague in <a href="../origins/spread.php">Kaffa</a> and Sicily (<em>Istoria de morbo sive mortalitate que fuit de 1348</em>):</p>
<p><cite>&quot;Tell, O Sicily, and ye, the many islands of the sea, the judgements of God. Confess, O Genoa, what thou hast done, since we of Genoa and Venice are compelled to make God's chastisement manifest. Alas! our ships enter the port, but of a thousand sailors hardly ten are spared. We reach our homes; our kindred and our neighbours come from all parts to visit us. Woe to us for we cast at them the darts of death! Whilst we spoke to them, whilst they embraced us and kissed us, we scattered the poison from our lips. Going back to their homes, they in turn soon infected their whole families, who in three days succumbed, and were buried in one common grave. Priests and doctors visiting the sick returned from their duties ill, and soon were numbered with the dead. O death! cruel, bitter, impious death! which thus breaks the bonds of affection and divides father and mother, brother and sister, son and wife. Lamenting our misery, we feared to fly, yet we dared not remain.&quot;</cite></p>
<p>Gabriele de' Mussi's account continues to record the terror in Piacenza. Until quite recently it had been believed that de' Mussi was himself a passenger on one of the plague-ships from Kaffa; but it is fairly certain that he was, in fact, in Piacenza during 1347.</p>
<p>(M.R., ed: D.S.) Adapted from: George Deaux, <em>The Black Death 1347</em>. New York: Weybright and Talley, 1969. Chapter IV, pp. 75ff.</p>

