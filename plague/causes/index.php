<h2>Causes of the Plague</h2>
<p>What was known as the Black Plague began in the East, in countries such as India, Tartar, Syria, and Armenia. It included at least four variants and was caused by the bacillus <em>Yersina pestis</em>. This bacillus thrives in the stomach of fleas which in turn are typically parasites of black rats. Due to circumstances not yet understood, the bacilli may multiply to such an extent that the flea's digestive tract is blocked and the flea regurgitates numerous bacilli into the bloodstream of the rodent host. The death of the rodent will cause the relocation of the flea, and if its next host is a human, then a contagion will begin.</p>
<p>The most common form of the disease was the bubonic plague, which caused hemorrhages or buboes of varying sizes. This is the least deadly, and is strictly insect borne; it cannot be transmitted from human to human. The pneumonic plague occurs when the bacillus moves into the pulmonary system, and generally occurred only when the disease was contracted in winter. The septicemic and enteric plague attack the blood and digestive systems, respectively, and are 100% fatal. These both are more rare than the other two types, perhaps because the hosts are killed so rapidly that the bacilli are not given much of a chance to thrive and be transmitted.</p>
