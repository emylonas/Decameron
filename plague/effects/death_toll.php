<h2>The Death Toll</h2>
<p>Plague-ridden ships from <a href="../perspectives/de_mussi.php">Kaffa</a> brought the disease to Italy. As early as January 1348, Genoese ships had carried the infection to their home port and to Venice, and from there the Black Death spread quickly throughout the Italian peninsula. The terrible devastation is recounted in numerous contemporary reports, such as the account of Matteo Villani and Boccaccio's famous introduction to the <em>Decameron</em>.</p>
<p>Some cities lost almost all their inhabitants: in Venice at least three-quarters died. In Pisa seven-tenths of the inhabitants died, and many families were completely destroyed. In Siena the plague raged from April until October and, according to the <em>Cronica Senese</em>&nbsp;of Agnolo di Tura, 80,000 people died in those seven months. Di Tura reported: &quot;And I, Agnolo di Tura, carried with my own hands my five little sons to the pit; and what I did many others did likewise.&quot; The expanding economy of the city was checked and the deaths of many painters, among them the brothers Pietro and Ambrogio Lorenzetti, ended the development of the first Sienese school. Florence was so devastated that for a long time the disease itself was known as &quot;the plague of Florence.&quot; Estimates of the dead vary greatly: Villani says three out of every five died; Antoninus, the Archbishop, estimates the toll at 60,000; Boccaccio says 100,000. Throughout Italy at least half the population died.</p>
<p>(Ed: D.S.) adapted from: Deaux, George. <em>The Black Death 1347</em>. New York: Weybright and Talley, 1969. pp. 85ff. </p>
