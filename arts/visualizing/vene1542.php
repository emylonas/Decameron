<h2>Illustrations in a 1542 Venetian Edition of the <em>Decameron</em></h2>
<p>This Italian edition of the <em>Decameron</em>&nbsp;has as its official title <em>Il Decamerone</em>, and was edited by a certain Gabriel Iolito di Ferrarii in Venice in the year 1542.</p>
<p>Printed in Roman moveable type, the single volume text contains ten woodcut illustrations inserted into the text, one at the beginning of each day. The first and second days appear to illustrate the members of the <em>brigata</em>&nbsp;themselves, sitting, playing music, dancing and singing. The subsequent eight days commence with illustrations depicting scenes from the first story of each respective day.</p>
<p>The prints are approximately 2 by 2 &frac12; inches in area, and they are printed on the same paper as the text, with no extra plates or cover sheets to protect them (as colored or hand-tipped prints sometimes have). The paper is fairly thin, and text from the <em>verso</em>&nbsp;of the paper is visible through the illustrations. There is not even the slightest attempt at cross-hatching in the prints, indicating either a lack of time or expertise on the part of the cutter - or perhaps a lack of significant value assigned to the prints themselves, as compared to the text and its relative value or prestige. A certain amount of value can be assumed regarding the volume as a whole, though, because the edges of the pages are gilded.</p>
<p>The woodcuts themselves are relatively simple in composition. Most stories are illustrated with a single scene, with the exception of the story of Ruggieri (<a href="../../texts/DecShowText.php?lang=eng&myID=nov1001&expand=day10">X.1</a>), which is shown in two scenes within the usual size frame: on the left is shown Ruggieri on his mule as it relieves itself in the river, and on the right are Ruggieri and King Alfonso before two chests, one of which is opened to reveal a pile of earth.</p>
<p>(E. L.) Located in the Star Collection at the John Hay Library, Brown University.</p>

