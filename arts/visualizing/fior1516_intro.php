<h2><em>Il decamerone</em>. Florence, Filippo Giunta, 1516.</h2>
<p>Boccaccio, Giovanni, 1313-1375. <em>Il decamerone</em>. 4â€  in 8's. Florence, Filippo Giunta, July 29, 1516. Typ 525.16.223, Department of Printing and Graphic Arts, Houghton Library, Harvard College Library.</p>
<p>20.5 x 13.5 cm. Eighteenth-century red morocco, gilt borders, ornaments on spine, green morocco label, gilt inside borders, with Quaritch's Sunderland library plate, Sunderland shelf marks, and Rahir label. (Nash fund.) </p>
<h3 class="break">Bibliographical description of edition</h3>
<p>Title-page without ornamentation. Woodcut at the head of the "proemio," 74 x 93 mm., showing Boccaccio's seven women and three men seated in a garden for the telling of the tales. The figures are named on the block. This is a reduced, reversed copy of a block designed for the Venice edition of 1492 [...]. Giunta repeats this block at the head of days 2, 6-10. ninety-six small woodcuts (including one repetition), 43 x 70 mm., also reduced, reverse free copies of the 1492 illustrations. The first is a cut of the author at work. There are a few points at which the illustration varies from that in the 1504 edition, and may depend on the fifteenth-century edition. Apparently someone at Giunta's press began to censor the blocks. On leaves e2v and l6v, parts of the blocks have been cut off or masked; similar scenes later in the volume are left intact. Initials are three-line roman capitals. Giunta's fleur-de-lis device [...] on the verso of leaf P10, recto blank. Italic letter.</p>
<p>First Giunta edition. On the verso of the title is an address, "Messer Giovanni Bocchaccio al lectore," in which "Boccaccio" writes about the art of printing and the superiority of the Giunta edition. ((<em>Italian 16th century books</em>, v. 1, pp. 96-97)</p>

