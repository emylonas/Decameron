<h2><em>Decamerone ouer Cento nouelle</em>. Venice, Bartolomeo de' Zanni, 1504.</h2>
<p>Boccaccio, Giovanni, 1313-1375. <em>Decamerone ouer Cento nouelle</em>. f†. Venice, Bartolomeo de' Zanni, July 5, 1504. *IC.B6308D. 1552, Department of Rare Books, Houghton Library, Harvard College Library.</p>
<p>28.5 x 19.5 cm. Diced calf, blind-stamped, gilt on spine, from the library of conte Gaetano Melzi. (Gift of Curt H. Reisinger.)</p>
<h3 class="break">Bibliographical description of edition</h3>
<p>Title in two lines only, without ornamentation. Woodcut, 105 x 121 mm., at the head of the "prima giornata" showing Boccaccio's company of seven women and three men                seated in a garden for the telling of the tales. The figures are labelled on the block. Another woodcut of the same company, 104 x 157 mm., is placed at the head of the text for the second day and repeated for each subsequent day. This cut is in two compartments. On the left, the center figure holds a lute. On the right, the center                figure wears a wreath and is seated before a fountain. Ninety-seven small woodcuts (including two repetitions), approximately 57 x 73 mm. Thirteen of these blocks are signed "b". One of the repetitions is the block of the author writing, which appears at the beginning of the volume with the "Vita" of Boccaccio and at the end before the "Excusatione delo auctore" (leaves aa4v, xx4v). [...] [B]oth large blocks and all of the small blocks are from the set designed for the Venice, Giovanni and Gregorio de' Gregori, edition of 1492, the first illustrated edition printed in Italy. The small blocks are specific scenes from the tales, many with figures labelled on the block. [...] The large block repeated at the head of each day is a copy of the two-compartment block; the garden scene is omitted. [...] Small initials, white on black. Zanni's orb and cross device, white on black [...] on leaf xx5v. Roman letter, two columns.</p>
<p>First Zanni edition. The "Vita de Giouan Bocchaccio da Certaldo" on leaves aa4v-aa5r is by Girolamo Squarciafico.</p>
<p>(<em>Italian 16th century books</em>, v. 1, pp. 95-96)</p>

