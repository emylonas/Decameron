<h2>Visualizing the Text</h2>
<p>The task of collecting images and mounting them on the web is a potentially limitless project that we have only just begun. Hindered by copyright and other access problems, what you see here is a fraction of what really exists in libraries and elsewhere. Where illustrations have been found but for whatever reason we are unable to show in image form, the source has been cited as clearly as possible so that in addition to forming a collection of images, we are also able to provide resources for further research if you are so inclined.</p>
<p>The images we have been able to collect so far are grouped below according to medium and content. The various media include early manuscript illuminations, woodcut prints from early printed book editions, photogravures in a more recent edition, and paintings. The section organized by content groups images according to the stories they illustrate. Another section deals with the artist or painter as character and motif in the stories. The last section consists of information on forthcoming and recent publications on visualizations of the <em>Decameron</em>&nbsp;as well as of Dante's <em>Divine Comedy</em>, another Italian text that has inspired many visual renderings since its conception, and other resources of possible value or interest for further research.</p>

