<h2>Medieval Music</h2>
<p>Music plays a small but critical role in the <em>Decameron</em> in both organizational and narrative terms. Each of the ten days of storytelling closes with a song, ostensibly composed and sung by one of the ten narrators. These <em>ballate</em> (ballads) serve an important purpose in the structural architecture of the text, signalling the transitional moment between the end of one prose unit (the Day of storytelling), and the beginning of the next. The inclusion of a song at the end of each day's novellas allows Boccaccio to experiment in a popular poetic genre within the confines of prose. It is thus not too much of a stretch to describe the <em>Decameron</em> as a <em>prosimetrum</em>, that is to say, a work consisting of <em>prosa</em> (prose) and <em>metricus</em> (verse) just as was his earlier work, the <em>Comedia delle ninfe fiorentine</em>.</p>

<p>The <em>ballate</em> also serve a purpose within the frame narrative of the text. By delineating the close of the storytelling activities for the day with a song sung by each of the narrators, Boccaccio is also able to differentiate between the ten characters of the <em>brigata</em>. The <em>brigata</em> members often seem like mere ciphers, inserted only to animate the structural architecture; the content of each <em>ballata</em>, and the reaction it produces from the other characters, is often the only clue to their various personalities.</p>

<p>Finally, music sometimes plays a key role in the individual novellas. Often, Boccaccio uses a song to provide a crucial plot device (e.g., in the tale of Gianni Lotteringhi (VII, 1), whose wife chants an exorcism to warn her lover away), and music appears throughout the book as the expression of a profoundly oral literary culture.</p>

