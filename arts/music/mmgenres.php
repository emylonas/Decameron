<h2>Genres of music in the Trecento</h2>
<p>Some popular genres of secular music in the Trecento were the <em>ballata</em> (ballad), the <em>caccia</em> (hunting song), and the madrigal.</p>

<p>The <em>ballata</em>'s name is derived from the Italian verb <em>ballare</em> (to dance), and they are so called because they often accompanied a dance. It seems likely that the <em>ballata</em> was essentially monophonic during the thirteenth century, but began to appear in polyphonic settings, usually for three voices, during the Trecento (Yudkin, p. 297). It would become the most prominent secular musical form of the late fourteenth century, as can be seen by the great number of <em>ballate</em> which have been preserved in contemporary manuscripts. Of Landini's 154 extant compositions, 141 are <em>ballate</em>. In fact, of approximately five hundred Trecento pieces which remain extant, more than 400 songs are of the <em>ballata</em> genre. The <em>ballata</em> is composed of two sections, solo verses alternating with a choral refrain. The first section (deemed 'A' for modern nomenclature purposes) has a <em>ripresa</em> (refrain) and the third and fourth lines of a strophe (<em>volta</em>, turning). Section B contains the first and second lines of the strophe (<em>piedi</em>, feet) sung twice with a first ending (aperto)  and second ending, (chiuso). The form follows the pattern ABBAA.</p>

<p>The madrigal poem was another very popular musical genre in Trecento Italy, although its origins remain obscure. There were many variants during this period, but the standard form consisted of two or three stanzas, each of three lines, followed by a final two-line stanza known as the <em>ritornello</em>. Typically the opening stanzas were sung to the same music, with a variation for the <em>ritornello</em>. The subject matter was usually pastoral or amatory, sung as a two or three voice polyphony (Yudkin, p. 525).</p>

<p>The <em>caccia</em> was another popular musical genre in Trecento Italy. Similar to the French <em>chace</em>, the <em>caccia</em> is a three part composition which plays on the dual meaning of the name: the three parts 'chase' each other in a canon, which describes an exciting event such as a hunt. Most <em>cacce</em> had two upper parts in a vocal canon with a third tenor part (sometimes instrumental) providing a noncanonic free counterpoint.</p>

<p>Most <em>cacce</em> described hunting expeditions, or similar dramatic events in an outdoor setting. The verse is freely written in one long stanza, and often interspersed with realistic shouts and interjections.</p>

