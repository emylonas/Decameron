<h2>The ballate and the carnivalesque: Bahktin's theories of the body popular</h2>
<p>The <em>ballate</em> achieve this insight and closeness through the espousal of certain techniques. Firstly, these songs, viewed in the context of medieval visual and lyrical art, clearly belong to a non-sacred realm of culture. As part of the <em>Decameron</em>, they are embedded in a context that communicates through jest, performance, and concerns of the flesh, rather than divinity. This playful manner, when viewed through Bakhtin's understanding of the medieval world, is a certain example of the carnivalesque. Thus, the overriding spirit of the <em>Decameron</em> is one of laughter and bawdy wit. However, the <em>ballate</em>, though they gain their energy from this context, are more serious, more elevated in style, and this difference in tone gives them an insightful, emotional authenticity.</p>
<p>Medieval music, unlike painting produced at the time, depended less on the established funding, and therefore was able to proliferate in both vernacular and liturgical segments of society. This persistent distinction between sacred and secular, Latin and vernacular, religious and sacrilegious, is very much in keeping with the Bakhtinian understanding of medieval culture. In his essay &quot;Carnival Ambivalence,&quot; Bakhtin asserts that &quot;a boundless world of humorous forms and manifestations opposed the serious tone of medieval ecclesiastical and feudal culture.&quot;  (<a href="bibliog.php#bakhtin">Bakhtin</a> 196)</p>

