<h2>All My Heart</h2>
<p>I gave her all my heart<br />
And here it waits for her,<br />
Paining and pounding in my chest.<br /></p>
<p>I curse Cupid, wretched imp,<br />
Who sprung the arrow from his quiver,<br />
Gave my steady heart a limp,<br />
Such sorrow the Lord cannot deliver.<br />
This heart is hers to take,<br />
Though she turns away,<br />
It pounds and pulses for her sake.<br /></p>
<p>O,  how can I blame the message boy,<br />
Who thought not of her indifference<br />
But threw his pointed poison toy,<br />
That has come to be my sufferance?<br />
It's she who makes my heart so ill,<br />
And I who fail to cure it,<br />
Alas, it's true, I love her still. (O how shall I endure it!)<br />
<p>Her gentle countenance and grace<br />
Hide within a mind so sly<br />
That had she not such lovely face<br />
I would wish at once to die.<br />
My heart she spurns,<br />
Alone it smolders,<br />
And in earthly conflagration burns.</p>

