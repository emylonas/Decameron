	<img src="../images/costa.jpg" alt="Lorenzo Costa, Concert, 1485-1495" width="238" height="305" />
</div>

<ul class="links">
	<li><strong>Musica Mundana</strong> [Lauda: Medieval Christmas track 5] - &quot;the harmony that presides over the movement of heavenly bodies,&quot;</li>
	<li><strong>Musica Humana</strong> [O rosa bella: Il Solazzo track 10] &quot;which concerns the balance of body and soul...man and the world,&quot;</li>
	<li><strong>Musica Instrumentalis</strong> [Principio de virtu: Il Solazzo track 9] &quot;the imitation of nature by means of art and its instruments.&quot; (<a href="bibliog.php#bosseur">Bosseur</a> p.24)</li>
</ul>

<p>It is evident that the <em>ballate</em> belong neither to the first category, Musica Mundana, nor the third, Musica Instrumentalis, though elements of both these are present. (God is frequently called upon, and instruments often accompany the song.)  Rather, the <em>ballate</em>, neither carnivalesque nor sacred, but honestly, intensely emotional, belong to the category of Musica Humana.</p>

