<h2>The ballate as Musica Humana: Boethius's theory of music</h2>
<p>Boccaccio's ballate are less parodic than their carnivalesque context, the Decameron. They, rather, are most akin to what Bakhtin describes as &quot;Renaissance literature&quot;&ndash;artwork informed by a &quot;thousand year old [tradition of] laughter&quot; and enriched by &quot;humanist scholarship.&quot; (<a href="bibliog.php#bakhtin">Bakhtin</a> 208)  As such, they can be classified according to Boethius' categories of music. A sixth-century philosopher who influenced the medieval understanding of organization, Boethius divided into three categories:</p> 
 
