<p>Likewise, the music produced used distinct rhythms and rhyme schemes to aid the singer in memorization. It also included image cues&ndash;the description of a visual picture&ndash;to make the song accessible and memorable. Thus, it was the responsibility of composer, or <em>trobador</em>, to be &quot;a pathfinder [who]...worked out in the open imagination and charted...an actual experience of word and tone.&quot; (<a href="bibliog.php#duffin">Duffin</a> 265)  To complete this artistry the performer of medieval music had to (and still has to) internalize and imagine the song so that the quality of &quot;<em>ruminatio</em> (ruminating: inner chewing of text and music)&quot; (<a href="bibliog.php#duffin">Duffin</a> 269) would be achieved and expressed to the audience. Through the collaborative process of composition and presentation, the <em>ballate</em> achieve emotional, and therefore active communication. Their &quot;exuberance, spontaneity, and...penchant for striking gestures&quot; (Duffin 208) strongly affect the listener. They function as performative speech that produces emotion in the listener, therefore acting as catalyst to action, becoming practically an action in itself.</p>

<p>(R.W.)</p>

