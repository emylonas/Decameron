<h2>The Song</h2>
<p>The <em>ballata</em>, traditionally a single voice song, [Non al suo amante: Il Solazzo track 5] is intended to &quot;move the listener [perhaps even more than]... challenge and charm the performer.&quot; (<a href="bibliog.php#duffin">Duffin</a> 215) They achieve their presentation through an emotional coordination between composer and performer. This partnership evolved out of necessity; as medieval music, as an oral rather than manuscript tradition, had to be memorized. For this reason, it relied on &quot;a [fine] mesh created between text and music,&quot; (<a href="bibliog.php#duffin">Duffin</a> 275) and was learned by &quot;listening and interiorizing,&quot; (<a href="bibliog.php#duffin">Duffin</a> 264) a technique that required both composer and performer to act as poet.</p>

