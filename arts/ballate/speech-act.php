<h2>The Speech Act</h2>
<p>As songs the <em>ballate</em> differ from the <em>novelle</em>, because they are performative speech acts. Though they include narrative, they also rely on instances that are distinctly, even grammatically performative. When classified according to Austin's analysis of the speech act, their complex mix of narrative and performative speech becomes evident. More generally, these songs, as performances, produce emotions in their listeners through a variety of motifs and metaphors. Through these emotions, they further the possibility of active response, and thus function as catalysts to action, or actual speech acts.</p>

<div class="image_left">
	<img align="left" src="../images/unknown.jpg" alt="Unknown - information to follow" width="302" height="201" />
</div>

<p>As speech acts, the <em>ballate</em> are interesting because they function on two different levels. From the macroscopic perspective, each <em>ballata</em> in its entirety is a performance act, while from the microscopic perspective, each one contains multiple examples of grammatically classified speech acts. In his <em>Philosophical Papers</em>, J.L. Austin stipulates that performative speech often takes the &quot;first person singular present indicative active [form],&quot; (<a href="bibliog.php#austin">Austin</a> 242) in other words, the form which, because it is in the first person singular and in the present tense, reports on actions or emotions from such closeness that it essentially merges with the actual activities of the speaker. As an example, Austin discusses the phrase &quot;I promise...&quot; which, provided it is said with honest intention, is &quot;an operative clause and...a performative utterance&quot; (<a href="bibliog.php#austin">Austin</a> 236) because it is in complete accordance with the speaker's present intentions. Boccaccio's <em>ballate</em>, while not as legalistic as Austin's simple examples, contain many instances of classifiably active speech.</p>

