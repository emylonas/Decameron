<h2>Neifile's ballata</h2>
<p></p>

<p>The ballata of ninth day exemplifies this preview quality. It follows the announcement that the stories of the tenth day will be &quot;<em>on the subject of those who have performed liberal or munificent deeds, whether in the cause of love or otherwise.</em>&quot; The song, presented by Neifile, one of the most modest and respectful members of the <a href="../../brigata/">brigata</a>, is very pure, kind, and good. In fact, its nature is strikingly virtuous in comparison with the burlesque stories, supposedly unorganized by theme, which came before. Far from the intrigue and trickery of Bruno and Buffalmacco, it presents the voice of a young damsel, who takes pleasure in wildflowers and says of them,</p>

<blockquote>And in all these his face I see<br />
Who has so taken hold of me<br />
His wish is mine eternally.</blockquote>

<p>The dedication and bliss expressed in this song clearly belong to the coming day's stories&ndash;of good doers, honest kings, respectful friends, and faithful lovers. In fact, this <em>ballata</em> links directly to the tenth <em>novella</em>; it could easily read as the inner monologue of Griselda, the peasant-queen. Wildflowers certainly are Griselda's familiar domain, and, as an enamored subject of her king, a song of unending love seems her appropriate expression. However, the happy love of the <em>ballata</em> would be, in Griselda's case, far too simple considering the cruelties she endures at the whim of her beloved husband. Nevertheless, a rare, perhaps foolish, purity links these two narrations.</p>

