<h2>The Power of Speech</h2>
<p>This demonstration of influence is Boccaccio's display of the power of speech. While we can only speculate as to why he included the <em>ballate</em> in his collection of stories, it is evident through Panfilo's announcement: The stories will &quot;'assuredly fill you [<em>brigata</em> members] with a burning desire...to comport yourselves valorously...'&quot; that Boccaccio values the power of speech to promote action. Though primarily a writer of wit, jest, and Bakhtinian carnivalesque, he does not preclude the possibly for serious, non-sacred communication, but rather upholds this Musica Humana, in his way. Through layers of invented <em>trobadore</em>, singers, tale-tellers, listeners, and readers, Boccaccio, the source of the entire interaction, carries the influence of the <em>ballate</em> beyond the single day, set of stories, frame story, or book to which they belong. Because he honors them, they become windows from the material world into the internal, universal emotional world. With these <em>ballate</em>, poem-songs, he affirms his fundamental belief in the active power of words.</p>

<p>(R.W.)</p>

