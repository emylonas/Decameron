<h2>Boccaccio and the medieval comic tradition</h2>
<p>Indeed, Boccaccio's <em>Decameron</em> belongs to that sector of medieval culture which preferences laughter over solemnity and revels in carnivalesque humor and <em>parodia sacra</em>. In this comic tradition there were certainly songs, like those by the Tuscan poet, Simone Prudenzani, that played with themes of &quot;sin and viciousness&quot; (Il Solazzo). In fact, one of Prudenzani's <em>ballate</em>, <em>La Badessa</em>, about an abbess who mistakenly dons her lover's trouser in place of her habit, also appears as a story in the <em>Decameron</em>. These stories belong to a genre of what Erasmus called, &quot;amorous and lascivious melodies&quot; (A Medieval Christmas) and they provide complete contrast to the contemplative liturgical style of the time.</p>

