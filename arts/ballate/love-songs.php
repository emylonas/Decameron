<h2>The ballate as love songs</h2>
<p>The <em>ballate</em>, as lovers' songs, are laden with complex emotions. As a group, they include such a variety of feelings that it seems as if Boccaccio intentionally created a piece about each different kind of love&ndash;narcissistic, fulfilled, bereaved, desperate, anticipatory, longing, hidden, fresh, and jealous. Each one of these expressive poems uses approximately eight emotion words (like delight, content, bereave, desolate, woe) that work as clusters to describe the intense emotional state of the first person narrator.</p>

<p>While the <em>ballate</em> sometimes reflect the stories just told, as in the case of Filostrato's day four <em>ballata</em> which explicates his own unhappy love in relation to the tragic stories told, they more often prepare for the <em>novelle</em> of the coming day.  Requested after the crowning of the new king or queen, they directly follow the announcement of the next day's story theme. Thus, they are as much preludes to the day approaching as evening songs of the day concluded. Thus, the <em>ballate</em> function as previews, presentations which set the mood and even the voice of the stories that follow. Their influence on the storytellers is significant, as their stories seem to grow from the songs that precede them. All the <em>ballate</em> expose the pleasure and tortures of love, and the stories follow their leading, ruminating on this ever fascinating phenomenon.</p>

<p>(R.W.)</p>

