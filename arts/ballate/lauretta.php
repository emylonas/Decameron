<h2>Lauretta's ballata</h2>
<p>One example of this type of speech can be found in Lauretta's <em>ballata</em> from the third day. This song, a woman's lament, includes both narrative and distinctly active speech patterns.  It begins:</p>

<blockquote>None has need for lamentation<br />
	More than have I<br />
	Who, alas, all sick for love<br />
	In vain do sigh.</blockquote>

<p>With this sentence, she describes a situation in the third-person present tense and then focuses in on the first person, saying &quot;I...in vain do sigh.&quot; This report, because of it's closeness to the speaker, functions as an actual sigh, and is therefore a classifiable speech act in accordance with Austin's analysis. As the song continues, describing a lover lost, an impending wedding, an old yearning, more instances of action occur, and she makes exclamations (Alas for me! Gone all my mirth! Would God had let me die!) which read like sobs and sighs. However, it is important to recognize that much of this poem-song is narrative, and it is the mosaic combination of performative and descriptive speech which makes the <em>ballata</em> complete.</p>

