<h2>Introduction: Emotion and Performance</h2>
<p>The <em>Decameron</em> as a whole is both an ordered grid of tale-tellers and a complex layering of tale-telling. Amidst all these stories, there is one element which cuts through and rings true beyond the crafted narrative. This element is the <em>ballate</em>.</p>

<div class="image_center">
	<img src="../images/dfrancesca.jpg" alt="Piero della Francesca, The Nativity, Concert of Angels (detail) 1470" width="474" height="360" />
</div>

<p>The <em>ballate</em> appear at the end of each <em>giornata</em>, sung by one member of the <em>brigata</em> at the request of the nominated king or queen. Like the <em>novelle</em>, they describe situations and personal relationships. Yet they are more than the eleventh narrational episode of each day, because, unlike the <em>novelle</em>, they belong to the frame story and can never break free in the way each <em>novella</em> does. Rather, the <em>ballate</em>, presented as musical accompaniments to dance, provide a clear space, an emotional insight embedded in the opaque account of the <em>brigata</em>'s daily activities.  As love poems&ndash;laments and glorifications&ndash;they are both personal to the singer and emotionally relevant to all listeners. From within the <em>Decameron</em>, they provide an atemporal, human, emotional familiarity.</p>


<p>(R.W.)</p>

