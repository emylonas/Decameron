<h2>Filostrato's ballata</h2>
<p>Another example of effective communication is evident with respect to Filostrato's <em>ballata</em> at the end of the fourth day. In this song, his uses the voice of a lover whose darling has chosen another man. The narrative, as it progresses becomes more and more gloomy and the accompanying exclamations more and more desperate. The final stanza reads as follows:</p>

<blockquote>My song, if none should learn to sing<br />
Thee over, I take little care;<br />
No one can sing the as I can.<br />
Only, to Love one message bear:<br />
Beg him, since life was all<br />
Loathsome to me, and vile,<br />
To safer haven take<br />
Me for his honour's sake.</blockquote>

<p>This message of desired death, delivered by a character who admittedly suffers from love is a perfect, if unhappy, unity of poetry and performer. Of its effect on the <em>brigata</em>, Boccaccio writes, &quot;Filostrato's mood and the reason, were made abundantly clear...perhaps...[a] blush spread across [the]...cheeks [of one of the ladies] as he was singing.&quot; This physical response makes certain that an act is inspired by the song, and though it is initially subtle, a blush, it may lead to larger, more significant actions. In the analysis of the speech, it makes clear that the song as a whole, with its multiple artists and its combination of speech styles, functions as an impetus to action, and is therefore a true speech act. Thus the <em>ballata</em>, in achieving an emotional response, has reached beyond the realm of thought.

