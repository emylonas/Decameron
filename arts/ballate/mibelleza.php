<h2>Mi belleza</h2>
<p>They call it a blessing, <em>mi belleza</em><br />
But I would sooner say a curse.<br />
So many fellows bent on my undressing,<br />
And loveliness will make the ending worse.<br />
So it is to be a woman,<br />
A curse unfading and God-given.</p>

<p>I had once a lover truly gallant, <em>onesto</em><br />
Time was heaven in his arms.<br />
Strong and brave to any challenge,<br />
He was my soul's repose, a charm.<br />
O, I loved him as a woman<br />
Who has her conquering hero chosen!</p>

<p>No earthly bloom can change the heavenscript, <em>stella e cielo</em><br />
But must bend at wind and God's decree.<br />
So when he went to sea, my salty-lipped,<br />
He left me lonely, unprotected, woe to me!<br />
A woman in sorrow I remain,<br />
Mourning the God-taken, the forsaken.</p>

<p>Again I curse this blessing, <em>mi belleza</em><br />
Which even my grief will not despoil<br />
For the men in jostling and jesting<br />
Now aim their better's girl to soil.<br />
So it is for me to be a woman,<br />
Preparing to suffer the unwelcome.</p>

<p>I know they'll force on me a wedding, <em>dolorosa festa</em>,<br />
Though I tell them I prefer the widow's wait,<br />
Each one vies to win my bedding,<br />
And I lament, so merciless a fate!<br />
You see, I am now a woman,<br />
Accepting that honor's end has come.</p>

<p>O Lord, what good a rose turned sad, <em>tristeza</em><br />
Why do you make a melancholy bride?<br />
Beauty wed to happiness would make so many glad,<br />
Instead you send me dreams of suicide.<br />
So, o woe, I am a woman<br />
<em>Mi belleza</em> inescapable, God-given.</p>

