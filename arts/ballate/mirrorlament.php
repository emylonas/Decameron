<h2>Mirror Lament <em>with lyrics from Billie Holiday's "Fine and Mellow"</em></h2>
<p>My man don't love me, treats me awful mean.<br />
He offers candy sugars to keep me sweetly fed.<br />
<em>He's the meanest man I've ever seen.</em></p>

<p>He calls me Your Highness, says I am his queen,<br />
Then puts the yellow butter on my bread,<br />
<em>But my man don't love me, treats me mean.</em></p>

<p>He gives me beads and combs so I will preen,<br />
And in the glass I smooth the curls across my head,<br />
<em>All for him, the meanest man I've ever seen.</em></p>

<p>Like a jewel, he has me shined to golden sheen,<br />
Then keeps me boxed between the mirror and the bed,<br />
<em>No, he don't love me, treats me awful mean.</em></p>

<p>I know because his eye is cool and keen,<br />
And when he kisses me, I feel that I have bled,<br />
<em>Yes, he's the meanest man I've ever seen.</em></p>

<p>Beside him in reflection, I am pale and lean,<br />
In the corner, so precious, nearly dead.<br />
<em>My man don't love me, treats me awful mean.<br />
He's the meanest man I've ever seen.</em></p>

