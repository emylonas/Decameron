<h2>Advice from an Inland Girl</h2>
<p>When you go seaward,<br />
my darling, my one,<br />
do not forget how you came.</p>
<p>Remember the sweet wind<br />
come before thunder,<br />
taste the green mist of the rain.</p>
<p>Recall how the moon<br />
sets into the birch,<br />
sun rises over the pines.</p>
<p>Cradle their needle,<br />
their ever-green spines<br />
in the tang-scented bent of your palm.</p>
<p>When they ask for your name,<br />
the sea-faring men,<br />
say larkspur, yarrow, red thorn.</p>
<p>And tell them the wide earth,<br />
misted with morning<br />
is the land in which you were born.</p>
<p>Carry a meadow,<br />my love, in your soul,<br />offer it to strangers as you go.</p>

