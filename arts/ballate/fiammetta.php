<h2>Fiammetta's ballata</h2>
<p>A ballata which exemplifies the complex construction of emotional expression is the last one in the book, sung by Fiammetta in the voice of a jealous lady lover. This song begins, &quot;If love could come unmixed with jealousy...&quot; and describes an emotional state in which the lady lover could be blissfully content. However, this state of happiness relies on the hypothetical absence of jealousy, and thus hints at the speaker's revulsion from and involvement with this emotion. As the poem continues, she states her view of actual reality:</p>

<blockquote>But other women are as wise as I,<br />
I fear the worst, and tremble with dismay<br />
Seeing those others seek to steal away<br />
He who has stolen mine own soul...</blockquote>

<p>Her resulting &quot;misery...woe...jealousy...pain...heartsick[ness]&quot; become overtly clear with her description. Yet her underlying emotion precedes its mention, and her statement of misery really tells of enormous insecurity. Though she never utters the word fear, its presence, both in relation to other women and to the man whose &quot;faith [may not be]...equal to his worth,&quot; is abundantly evident. The ballata ends with an oath &quot;in God's name,&quot; promising vengeance on any woman who attempts to seduce her lover. Her threat is so potent that the brigata take it seriously; fearing her agitated state, plannning to act accordingly. Thus the ballata goes through layers of emotion, spoken and unspoken, which understood together are thick and intense.</p>

<p>This particular ballata comes at the end of the last day, and its effect travels outward beyond the stories of the day and even beyond the frame story. Yet it seems strange, to conclude such a long work with the worries of a jealous woman. Yet this lack of resolution is Boccaccio's intentional effort to dissuade the reader from drawing simple conclusions. He concludes with the inconclusive statement, &quot;such the ways of men/ And such the wiles of women who allure...&quot; challenging the reader to actually makes sense of the complicated human interactions of the Decameron.</p>

