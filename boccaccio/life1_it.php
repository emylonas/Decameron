<h2>Vita e opere di Boccaccio</h2>
<h3>1313</h3>
<p>Boccaccio nasce (nel mese di giugno o luglio) a Certaldo o a Firenze da una donna di identit&agrave; non accertata e da Boccaccino di Chellino, un agiato mercante che lo riconosce senza esitazione.</p>
<h3>1319-20</h3>
<p>Boccaccino sposa la nobildonna Margherita de' Mardoli. Nasce Francesco, fratellastro di Boccaccio.</p>
<h3>1327</h3>
<p>Boccaccio compie un viaggio a Napoli assieme al padre, agente del Banco dei Bardi.</p>
<h3>1330</h3>
<p>Frequenta forse le lezioni di Cino da Pistoia, giurista, poeta e amico di Dante e Petrarca, e intraprende gli studi di diritto canonico.</p>
<h3>1332</h3>
<p>Boccaccino si trasferisce a Parigi. Giovanni si dedica con pi&ugrave; grande libert&agrave; ai suoi interessi letterari, come &egrave; testimoniato dalle sue prime opere latine (la <em>Elegia di Costanza</em> e la <em>Allegoria mitologica</em>, entrambe sicuramente composte prima del 1334) e dalle sue prime prove in vernacolo.</p>
<h3>1333-34</h3>
<p>Primo contatto di Boccaccio con la poesia di Petrarca.</p>
<h3>1334-37</h3>
<p>Composizione della <em>Caccia di Diana</em>.</p>
<h3>1336-39</h3>
<p>Boccaccio termina il <em>Filocolo</em>. Nello stesso periodo termina i suoi studi.</p>
<h3>1339</h3>
<p>Giovanni compone le seguenti epistole latine: <em>Crepor celsitudinis</em>, dedicata a Carlo, duca di Durazzo; <em>Mavortis milex</em>, dedicata a Petrarca; <em>Nereus amphitribus</em> e <em>Sacre famis</em>, per amici ignoti.</p>
<h3>1339-40</h3>
<p>Composizione del <em>Teseida</em>.</p>
<h3>1340</h3>
<p>Porta a termine, fra l'autunno e l'inverno, il <em>Filostrato</em> (alcuni studiosi anticipano la data al 1335 circa).</p>
<h3>1340-41</h3>
<p>Boccaccio ritorna a Firenze.</p>
<h3>1341-42</h3>
<p>Composizione della <em>Comedia Ninfe</em> (nota anche come <em>Commedia delle ninfe fiorentine</em> e pi&ugrave; tardi con il controverso titolo di <em>Ninfale d'Ameto</em>) dedicato a Niccol&ograve; di Bartolo Del Buono. Primo abbozzo del <em>De vita et moribus domini Francisci Petracchi</em>.</p>
<h3>1342-43</h3>
<p>Prima stesura dell'<em>Amorosa visione</em>.</p>
<h3>1343-44</h3>
<p>Composizione dell'<em>Elegia di Madonna Fiammetta</em>.</p>
<h3>1344-45</h3>
<p>Composizione del <em>Ninfale fiesolano</em>.</p>
<h3>1347-48</h3>
<p>Si reca a Forl&igrave; presso la corte di Francesco Ordelaffi. Boccaccio e il grammatico Checco di Meletto Rossi si scambiano sonetti e <em>carmina</em>. In questo periodo viene a conoscenza delle vicende che caratterizzano gli ultimi anni di Dante. Compone le prime egloghe successivamente riunite sotto il titolo di <em>Buccolicum carmen</em>.</p>
<h3>1348</h3>
<p>A Firenze si avvertono i primi effetti della peste nera, che causer&agrave; la morte del padre, della matrigna e di numerosi amici di Boccaccio.</p>
<h3>1349-51</h3>
<p>Composizione del <em>Decameron</em>&nbsp; ...</p>

