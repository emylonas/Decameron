<h3>1350</h3>
<p>Primo incontro con Petrarca a Firenze. Comincia a lavorare alla <i>Genealogia deorum gentilium</i>, opera che non terminer&agrave; fino al 1374.</p>
<h3>1351</h3>
<p>Boccaccio si traferisce a Padova dove incontra nuovamente Petrarca. Rappresenta, con la carica di ambasciatore, il comune di Firenze presso la corte di Luigi di Brandeburgo. Conclude il primo abbozzo del <i>Trattatello in laude di Dante</i>.</p>
<h3>1355</h3>
<p>Boccaccio ritorna a Napoli. Quest'anno rappresenta il termine <i>post quem</i> della seconda redazione della <i>Amorosa visione</i> che sar&agrave; definitivamente completata nel 1360. Comincia a lavorare al <i>De casibus virorum illustrium</i> e al <i>De montibus, silvis, fontibus et de nominibus maris liber</i> conclusi rispettivamente nel 1363 e nel 1364.</p>
<h3>1357</h3>
<p>Boccaccio &egrave; a Ravenna, dove probabilmente riceve da Petrarca le <i>Invective contra medicum</i>.</p>
<h3>1359</h3>
<p>Terzo incontro con Petrarca a Milano. Boccaccio viene nominato ambasciatore in Lombardia, forse alla corte di Bernab&ograve; Visconti.</p>
<h3>1360</h3>
<p>Completa la prima stesura del <i>De casibus</i> e il primo compendio del <i>Trattatello</i>. Papa Innocenzo VI gli attribuisce la facolt&agrave; di ottenere ufficio, prebenda o cura d'anime. Dopo un tentativo di colpo di stato, a seguito del quale molti suoi amici (tra cui Niccol&ograve; di Bartolo Del Buono) sono condannati a morte, Boccaccio non riceve per i quattro anni successivi alcun incarico ufficiale.</p>
<h3>1361</h3>
<p>Boccaccio si ritira a Certaldo. Comincia a lavorare al <i>De mulieribus claris</i>.</p>
<h3>1361-62</h3>
<p>Ritorna, per ragioni non chiare, a Ravenna. Qui, per conto di Petrarca, che sta lavorando al <i>De vita solitaria</i>, raccoglie informazioni su San Pier Damiani.</p>
<h3>1362</h3>
<p>Versione definitiva del <i>De mulieribus</i>. Compone la <i>Vita sanctissimi patris Petri Damiani</i>.</p>
<h3>1363</h3>
<p>In seguito a una profonda crisi religiosa, Boccaccio si dedica esclusivamente ad attivit&agrave; spirituali. Si reca nuovamente a Napoli, ma vi si trattiene brevemente a causa della freddezza con cui viene accolto e ritorna a Firenze. Va a Padova da Petrarca che incontra per&ograve; a Venezia, dove nel frattempo questi si &egrave; trasferito. A luglio Boccaccio torna a Certaldo e porta a compimento le <i>Genealogie</i>.</p>
<h3>1364-65</h3>
<p>Boccaccio intrattiene un impegnativo dibattito epistolare con Petrarca sulle composizioni in volgare.</p>
<h3>1365</h3>
<p>Si trasferisce alla corte papale di Urbano V ad Avignone come ambasciatore di Firenze. Compone il <i>Corbaccio</i>. Si dedica al secondo compendio del <i>Trattatello</i>.</p>
<h3>1367</h3>
<p>Dopo un viaggio a Firenze si reca a Venezia dove incontra la figlia e il genero di Petrarca. Assume l'incarico di ambasciatore a Roma.</p>
<h3>1368</h3>
<p>A Padova incontra Petrarca che attira attorno a s&eacute; numerosi letterati.</p>
<h3>1369-70</h3>
<p>Boccaccio presiede alla pubblicazione del <i>Buccolicum carmen</i>.</p>
<h3>1370-71</h3>
<p>Dopo un ultimo viaggio a Napoli, si rititra a Certaldo.</p>
<h3>1372</h3>
<p>Boccaccio &egrave; sempre pi&ugrave; afflitto dall'obesit&agrave; e da una forma di idropisia che gli rende difficoltosi i movimenti; &egrave; anche tormentato dalla scabbia e da attacchi di febbre.</p>
<h3>1373</h3>
<p>Dedica la versione definitiva del <i>De casibus</i> a Mainardo Cavalcanti. Prosegue la revisione delle <i>Genealogie</i>. Boccaccio riceve dalla citt&agrave; di Firenze l'incarico di fare letture pubbliche della <i>Divina Commedia</i>.</p>
<h3>1374</h3>
<p>In serie difficolt&agrave; economiche e di salute, ritorna a Certaldo dove viene a conoscenza della morte di Petrarca. Il triste evento gli ispira l'ultimo sonetto delle sue rime senili. Continua a lavorare alle <i>Genealogie</i>.</p>
<h3>1375</h3>
<p>Boccaccio muore il 21 dicembre nella sua casa di Certaldo.</p>
<p>(G.M., M.P.) La presente cronologia &egrave; tratta, con riadattamenti, da C. Muscetta, <i>Giovanni Boccaccio</i>, in AA. VV., <i>Letteratura italiana Laterza</i>, vol. VIII, Bari, Laterza, 1989 e da G. Ferroni, <i>Storia della letteratura italiana</i>, vol. I, <i>Dalle origini al Quattrocento</i>, Torino, Einaudi, 1991.</p>

