<h2>Fiammetta</h2>
<p>Several scholars of Boccaccio like to believe that the Fiammetta of the <em>brigata</em>&nbsp;was based upon a real woman, Maria d'Aquino, with whom Boccaccio fell in love. &quot;Fiammetta&quot; is a recurring character in a number of Boccaccio's works (e.g., The <em>Filocolo</em>&nbsp;and <em>L'elegia di Madonna Fiammetta</em> ) and is generally described in consistent terms: &quot;[her hair] is so blonde that the world holds nothing like it; it shades a white forehead of noble width, beneath which are the curves of two black and most slender eyebrows ... and under these two roguish eyes ... cheeks of no other colour than milk.&quot;</p>

<p>In the <em>Decameron</em>, Fiammetta is one of the most assertive women. She often engages Filostrato and others in competitive storytelling about the nature of love. She is a clever, independent and resourceful woman who admires these same qualities in others. Fiammetta often tells stories about tricksters and delights in tales about strong female characters. Her tone is largely positive, with the exception of her stories of the Fourth and Fifth Days.</p>

<p>(C.S.)</p>

