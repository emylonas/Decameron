<h3>Stories</h3>

<p>Pampinea, the eldest of the seven lady narrators at twenty-seven years of age, is the natural leader among them. In Italian her name means &quot;la rigogliosa&quot; which could be translated as &quot;the flourishing one.&quot; It was her suggestion for the group to move to the countryside and her initiative that brought the three men into their plans. In return, she was made Queen of the first day.</p>

	<ul>
		<li><strong>Under the rule of Pampinea.</strong> Master Alberto of Bologna neatly turns the tables on a lady who was intent upon making him blush for being in love with her (<a href="http://www.stg.brown.edu/projects/decameron/engDecShowText.php?myID=nov0110&expand=day01">I.10</a>).
		<li><strong>Under the rule of Filomena.</strong> Three young men squander their fortunes, reducing themselves to penury. A nephew of theirs, left penniless, is on his way home when he falls in with an abbot, whom he discovers to be the daughter of the King of England. She later marries him and makes good all the losses suffered by his uncles, restoring them to positions of honor (<a href="http://www.stg.brown.edu/projects/decameron/engDecShowText.php?myID=nov0203&expand=day02">II.3</a>).
		<li><strong>Under the rule of Neifile.</strong> A groom makes love to King Agilulf's wife. Agilulf finds out, keeps quiet about it, tracks down the culprit, and shears his hair. The shorn man shears all the others, thus avoiding an unpleasant fate (<a href="http://www.stg.brown.edu/projects/decameron/engDecShowText.php?myID=nov0302&expand=day03">III.2</a>).
		<li><strong>Under the rule of Filostrato.</strong> Friar Alberto, having given a lady to understand that the Angel Gabriel is in love with her, assumes the Angel's form and goes regularly to bed with her, until, in terror of her kinsfolk, he leaps out of the window and takes shelter in the house of a pauper; the latter disguises him as a savage and takes him on the following day to the city square, where he is recognized and seized by his fellow friars, and placed under permanent lock and key (<a href="http://www.stg.brown.edu/projects/decameron/engDecShowText.php?myID=nov0402&expand=day04">IV.2</a>).
		<li><strong>Under the rule of Fiammetta.</strong> Gianni of Procida is found with the girl he loves, who had been handed over to King Frederick. He and the girl are tied to a stake, and are about to be burnt when he is recognized by Ruggieri de Loria. He is then set free, and afterwards they are married (<a href="http://www.stg.brown.edu/projects/decameron/engDecShowText.php?myID=nov0506&expand=day05">V.6</a>).
		<li><strong>Under the rule of Elissa.</strong> By means of a single phrase, Cisti the baker shows Messer Geri Spina that he is being unreasonable (<a href="http://www.stg.brown.edu/projects/decameron/engDecShowText.php?myID=nov0602&expand=day06">VI.2</a>).
		<li><strong>Under the rule of Dioneo.</strong> Whilst she is entertaining Leonetto, Madonna Isabella is visited by Messer Lambertuccio, who has fallen in love with her. Her husband returning unexpectedly, she sends Messer Lambertuccio running forth from the house with a dagger in his hand, and Leonetto is taken home a little later on by her husband (<a href="http://www.stg.brown.edu/projects/decameron/engDecShowText.php?myID=nov0706&expand=day07">VII.6</a>).
		<li><strong>Under the rule of Lauretta.</strong> A scholar falls in love with a widow, who, being in love with someone else, causes him to spend a winter's night waiting for her in the snow. But on a later occasion, as a result of following his advice, she is forced to spend a whole day, in mid July, at the top of a tower, where, being completely naked, she is exposed to the flies and the gadflies and the rays of the sun (<a href="http://www.stg.brown.edu/projects/decameron/engDecShowText.php?myID=nov0807&expand=day08">VIII.7</a>).
		<li><strong>Under the rule of Emilia.</strong> Talano d'Imolese dreams that his wife is savaged all about the throat and the face by a wolf, and tells her to take care; but she ignores his warning, and the dream comes true (<a href="http://www.stg.brown.edu/projects/decameron/engDecShowText.php?myID=nov0907&expand=day09">IX.7</a>).
		<li><strong>Under the rule of Panfilo.</strong> On hearing that a young woman called Lisa has fallen ill on account of her fervent love for him, King Peter goes to comfort her, and later on he marries her to a young nobleman; and having kissed her on the brow, he thenceforth always calls himself her knight (<a href="http://www.stg.brown.edu/projects/decameron/engDecShowText.php?myID=nov1007&expand=day10">X.7</a>).
	</ul>

<p>It is interesting to note that the final three stories of Pampinea (from day eight through ten) are placed seventh. Three is a holy number reflecting the trinity, and seven had its own particular connotation of being auspicious (see <a href="../literature/narratology/numerology.shtml">numerology</a>). Seven are the women on the <i>Decameron</i>, thus perhaps also re-emphasizing Pampinea's prominent role as their leader.</p>
<p>(A. K.)  </p>

