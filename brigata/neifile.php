<h2>Neifile</h2>
<h3 class="break">Appearance: <span class="unbold">young, beautiful, charming, elegant, cheerful.</span></h3>
<p>&quot;Neifile whose manners were no less striking than her beauty, replied with a smile...&quot; (<a href="../texts/DecShowText.php?lang=eng&myID=nov0102&expand=day01">I.2</a>).</p>
<h3 class="break">Her modest character:</h3>
<ul class="links">
	<li>In <a href="../texts/DecShowText.php?lang=eng&myID=nov0309&expand=day03">III.9</a> and in <a href="../texts/DecShowText.php?lang=eng&myID=nov0708&expand=day07">VII.8</a>, she starts out her stories by praising her predecessors and saying that she cannot possibly match their stories. Actually, however, she tells her best, most interesting stories when she says this and her stories are much appreciated by the others.
	<li>&quot;And with all her considerable charm she addressed her companions as follows: How is anyone to tell a better story than the one we have just heard from Lauretta? It was certainly fortunate for us that hers was not the first, for otherwise we would have derived little pleasure from the ones that followed, which is what I fear will happen with the last two stories of today&quot; (<a href="../texts/DecShowText.php?lang=eng&myID=nov0309&expand=day03">III.9</a>).
	<li>&quot;Fair ladies, if I am to entertain you with a story as excellent as the ones with which you have been regaled by my predecessors, my task will indeed be difficult...&quot; (<a href="../texts/DecShowText.php?lang=eng&myID=nov0708&expand=day07">VII.8</a>).
</ul>
<h3 class="break">Her assertive nature:</h3>
<ul class="links">
	<li>Neifile also shows that she is not as passive and submissive as she seems. When Filostrato says that the wolves (men) have taught the sheep (women) well, she answers him: &quot;If you men had tried to teach us anything of the sort, you might have learned some sense from us, as Masetto did from the nuns, and retrieved the use of your tongues when your bones were rattling from exhaustion&quot; (<a href="../texts/DecShowText.php?lang=eng&myID=d03conclu&expand=day03">Day 3 Conclusion</a>).
</ul>
<h3 class="break">Ideas:</h3>
<p>Neifile seems to represent a certain Ghibelline sensibility within the <i>brigata</i>, regularly favoring authority figures (fathers, guardians, masters, <i>podest&agrave;</i>, kings and so on). This attitude is reflected as well in her perspective on the duty to respect one's ruler or superior (sons, daughters, servants, wives, knights, et al.) in particular and to revere the ordered and established societal institutions and traditions (hierarchy of power in family, clan and country) in general.  </p>
	
