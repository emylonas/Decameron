<h2>Communication</h2>

<p>Communication in the thirteenth and fourteenth centuries was mostly oral. Only gradually did the ability to read and write grow, its growth being most notable among people in the cities. In certain genres, the <strong><em>volgare</em></strong> (Italian vernacular) was used as much as Latin. Gradually, more books were produced and more people were able to read them. Of course, there was a distinction among the literate; while religious texts, didactic novellas and chronicles were written for everyone, only educated people were able to read more complex works, such as treatises on theology and rhetoric.</p>
<p>Orality remained an important component even of written texts as they often had a simultaneous oral diffusion (e.g. Dante's <em>Comedy</em>&nbsp;and the <em>Decameron</em>). In fact, many works, both in poetry and prose, were composed specifically for oral performances. Among these can be counted the various <strong><em>exempla</em></strong> which were to be found commonly in sermons and which eventually contributed to the development of the <a href="../literature/narratology/novella.php"><em>novella</em> genre</a>.</p>
<p>(G.S. G.P.) </p>

