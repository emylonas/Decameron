<h2>Schooling</h2>
<p>During the Middle Ages the school system was not homogeneous and attendance was not mandatory. Wealthy families generally hired a tutor, even though in the fourteenth century public schools (in a wide sense) became more and more common.</p>
<p>There were two types of medieval curricula: 1) <strong>Grammar and Rhetoric School</strong>, after which students were admitted to the university. The subjects taught in this first school were then divided into two branches according to a division established in Late Antiquity: the <em>Trivium</em>&nbsp;(grammar, rhetoric and dialectics) and the <em>Quadrivium</em>&nbsp;(arithmetic, geometry, music and astronomy); 2) <strong>Calculus School</strong>, created for future businessmen. There was a third, unofficial kind of school as well. It was the workshop itself, where children, through apprenticeship, could learn everything they needed in order to be good craftsmen. Merchants' sons generally had both experiences: they would go to school for a few years before joining the family business.</p>
<p>The highest level of learning was acquired at the university. Universities developed as alternatives to the cultural monopoly of the church. &quot;University&quot; meant an association of famous scholars which was able, by virtue of their reputations, to attract a number of students from all of Europe. Paris, Bologna and Oxford are the oldest in Europe. By the end of the thirteenth century, there were approximately twenty universities and by the end of the fifteenth, there were about a hundred.</p>
<p>In the countryside, the church continued to control the educational system, mainly through the didactic functions of priests.</p>
<p>(G.S. &amp; G.P.) Adapted from Ceserani-De Federicis, <em>Manuale di letteratura</em>, pp. 67-70.</p>

