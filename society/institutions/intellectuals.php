<h2>Intellectuals</h2>
<p>The word <em>laicus</em>&nbsp;(layman), once used to define those unable to use Latin, regardless of their social status. In the later Middle Ages the term also carried the meaning of &quot;intellectual not connected to the church.&quot;</p>
<p>During the fourteenth century many intellectuals decided to join the church, mainly for economic and social reasons, yet they maintained a strong interest in secular culture (e.g. Boccaccio). Thus, the two cultures should not be conceived of as mutually exclusive. Indeed, secular urban culture tried to integrate its own values with religious beliefs and noble ideals. Several characters and situations found in the <em>Decameron</em>&nbsp;can be correctly understood only keeping in mind such a diversified reality.</p>
<p>In the Middle Ages intellectuals earned their wages working as teachers. Writing works of literature was not considered a profession in itself. In fact, most &quot;intellectual&quot; functions were fulfilled by the clergy. In Sicily, where Italian poetry first flourished (in the <em>scuola siciliana</em>), men of letters worked for King Frederick II and played an influential role in his court. Eventually, as more laymen replaced the clergy in carrying out the tasks once considered appropriate only for men of the cloth, they also became politically involved in the life of the town. They wrote in the vernacular and their audience became the entire society at large. When the political system changed from <em>comuni</em> (city-states) to <em>signorie</em> (seigniories), intellectuals continued to play a part in local politics, although now writing only for the presiding courts of the various <em>signorie</em>. They wrote in Latin and were often clergymen (e.g. Petrarch under the Visconti of Milan).</p>
<p>Petrarch and Boccaccio represent the first &quot;men of letters&quot; in the current sense of the word. Through them, for the first time, literature was considered a dignified profession in itself. Literature was the means through which they sought to fulfill their own personal ambitions.  </p>
<p>(G. S. &amp; G. P.) Adapted from G.Ferroni, <em>Storia della letteratura Italiana</em>.</p>

