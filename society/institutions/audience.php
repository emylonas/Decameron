<h2>The Audience</h2>
<p>In the Middle Ages, being able to write did not necessarily mean being able to read and vice versa. Moreover, schooling was not that common. During the thirteenth century, however, together with the expansion of marketing and banking, these two skills became more and more necessary. Since the ideal place for business was the city, the number of schools increased significantly there, while in the countryside culture was either oral or in the hands of the church.</p>
<p>Readers as a group also were not as homogeneous as they used to be. Along with professional readers, there were more and more people reading just for pleasure, mostly merchants, and sometimes owners of small personal libraries. Libraries, which were once common only in monasteries, were now found in universities as well as in the homes of not only nobles and intellectuals (e.g. Petrarch) but also in those of merchants and bankers. In such collections one could find school texts, but also French novellas, religious books and translations of contemporary and ancient works. It is not surprising that the <em>Decameron</em>'s most affectionate readers were found among this newly literate social class.</p>
<p>(G.S. &amp; G.P.) Adapted from Ceserani-De Federicis, <em>Manuale di letteratura</em>, pp. 80-82.</p>

