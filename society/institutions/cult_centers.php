<h2>Cultural Centers</h2>
<h3>Northwestern Italian Courts</h3>
<p>There were many Proven&ccedil;al troubadours in Monferrato, Lunigiana e Savoy. In Genoa Proven&ccedil;al poetry flourished.</p>
<h3>Venice and Veneto</h3>
<p>Florentine influence was very strong in this area. Padua and Verona were well known for their tradition of classical studies.</p>
<h3>Florence and Tuscany</h3>
<p>Florence was without a doubt the cultural capital of this period due in large measure to Dante and his works, to its libraries and studios. The large production of manuscripts, throughout the region, established the Florentine dialect as the literary language par excellence. Other Tuscan towns were also of great cultural importance such as Arezzo and Pistoia, for their reputation as centers of legal studies, and Siena and Pisa for <a href="book_prod.php">book production</a>.</p>
<h3>Perugia, Assisi and Umbria</h3>
<p>This area saw the development of religious literature connected to Franciscanism.</p>
<h3>Rome</h3>
<p>Rome was home to the Papacy and served as a both a cultural and religious center. Many theologicians and philosophers lived and worked in monasteries in Rome and in the Latium region (e.g. Thomas Aquinas).</p>
<h3>Palermo</h3>
<p>During the thirteenth century, this city was the most important place for vernacular poetry due to the influence of court of Frederick II. This literary production provided the seeds for the foundation of the <strong><em>dolce stil novo</em></strong>, a poetic movement which took shape later in Florence.</p>
<h3>Avignon</h3>
<p>This French town was very important for Italian culture because of its claim to the Papacy. Several Italian writers spent part of their lives there (e.g. Petrarch).</p>
