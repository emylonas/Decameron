<h2>Book Production</h2>
<p>During the Middle Ages books were quite different from those of today: since printing had not yet been invented, books were manuscripts, volumes made out of sheep skin and decorated with ornate illuminations. Books made of paper came into existence only during the thirteenth century. Paper was more perishable but less expensive, and it was easier to produce and to write on it. For these reasons books became more and more common. It is interesting to point out that books were generally very large (it was Petrarch who first conceived of the idea of a &quot;pocket- sized book&quot;). Also not many people could actually purchase books, even paper ones, as they were quite expensive. During the early Middle Ages priesthood had a monopoly on reading and writing. In fact <b><em>clericus</em></b> (cleric) was synonymous with &quot;Intellectual.&quot; This remained the situation while Latin was the only written language, an entirely different language from any of the vernacular ones. In urban areas the vernacular dialects became more and more important, expecially for practical purposes. Eventually they became sophisticated enough to be used in a written format. Finally, culture could be produced in cities, and no longer only in monasteries and courtrooms (where the first documented evidence of a successful coexistence between vernacular and Latin appears). While monasteries mainly produced religious texts in Latin, other literary production was strongly connected to the mercantile and banking environment and was therefore secular in nature.</p>
<p>(G.S. G.P) Adapted from G.Ferroni, <em>Storia della letteratura italiana</em>, vol. I <em>&quot;Dalle origini al Quattrocento&quot;</em>&nbsp;(Turin: Einaudi, 1991). pp. 88-100.</p>

