<h2>Magic in the <em>Decameron</em></h2>
<p>Magic and a strong belief in the supernatural appear on every day of the <em>Decameron</em>'s storytelling and in over one-fifth of all the novellas. Though Boccaccio seems to poke fun at and ridicule those who show a naive belief in magic (and who are consequently duped by those wiser or more cunning in general), he also includes elements which cannot be explained by reason alone, such as apparitions, dreams, and fantastic or implausible situations. To the medieval imagination, supernatural events were fascinating and often frightening phenomena that could be inspired either by God's will (a miracle) or by artificial means (magic). St. Thomas believed that the intellectual powers called upon to produce magical occurrences were not necessarily evil, but any attempt to thwart the will of God was, by definition, wrong (<em>Summa contra gentiles</em> CIV-CVII). Magical events, if in accordance with Divine Providence, were therefore wholly legitimate. At the level of popular culture, magic was a mysterious area of natural and supernatural activity that comprised not only paranormal activity but also common superstitions and the most fundamental questions of faith. What is more, the French romance tradition included an abundance of motifs which depended upon magical items such as rings and talismans. Boccaccio was intimately acquainted with these popular and literary traditions (as well as magic motifs present in the works of classical authors) and drew upon them in original and often extremely entertaining fashion.</p>
