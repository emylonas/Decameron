<h2>Social Critique</h2>
<p>Boccaccio employs magical elements to achieve a number of effects, many of them frequently satirical in nature. The majority of fourteenth-century society believed, in one way or another, in the power of magic spells, incantations, rituals, and other such lore. Among the members of the merchant classes, however, these beliefs were probably not as diffused owing to an ever greater tendency toward ways of thinking that depended more heavily on measured calculation and rationality. Boccaccio's social criticism, as regards the uses of magic, sometimes becomes clear in the roles of <em>beffatore</em> and <em>beffato</em>. Often those who trust in magic's efficacy are the butt of jokes revolving sex, money or revenge. This criticism is based not on one's social class; instead, it seems to be directed simply at those susceptible to harmful superstition at large or at foolish people in general, whether men or women, who lack intelligence and easily believe whatever tantalizingly disguised rubbish someone directs their way. In any study of magic in Boccaccio's work, however, we must strive to distinguish between those tales in which magic appears as a vehicle of the <em>beffa</em> and those in which it figures as a manifestation of the literary debt owed to the medieval romance tradtition.</p>
<p>(A.B. &amp; M.P.)</p>

