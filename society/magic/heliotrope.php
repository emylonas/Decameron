<h2>Heliotrope</h2>
<p>The heliotrope is the stone that Calandrino believes renders him invisible after he collects it along the banks of the Mugnone. It is a green non-crystalline quartz spotted with red jasper, also known as bloodstone. The name &quot;heliotrope&quot; comes from a combination of the Greek words for &quot;sun&quot; and &quot;direction.&quot; When placed in water in direct sunlight, the whole of it appears red. </p>
<p>One legend says that it got its red spots from Christ's blood falling on the stones beneath His feet. Another account says the blood is Phaeton's who lost control of the Chariot of the Sun and crashed to earth. In any case, the color's likeness to blood made it a suitable material in the early church for carved and engraved sacred objects. Kraos and Holden report that medieval people made figures in which the spots represented blood stains. This material was known as &quot;St. Stephen's Stone.&quot; It is also found in signet rings in India, Siberia and the Hebrides.</p>
<p>Pliny says that it is &quot;most blatant effrontery&quot; that heliotrope, when combined with a plant of the same name, can confer invisibility (Best and Brightman 35). By having Calandrino believe the stone really works, Boccaccio highlights Calandrino's simplicity.</p>
<p>(A.B.) Kraus, Edward Henry and Edward Fuller Holden. <em>Gems and Gem Materials</em>. New York: McGraw-Hill, 1925; St. Albert the Great. <em>The Book of Secrets of Albertus Magnus</em>. Michael R. Best and Frank H. Brightman, eds. Oxford: Clarendon Press, 1973.</p>

