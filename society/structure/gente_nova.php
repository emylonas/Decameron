<h2>The <em>Gente Nova</em></h2>
<p>In addition to bringing into the city great numbers of people from the surrounding towns and countryside, the <a href="../../plague/">Black Death</a> affected the character of Florentine society in still another way.</p>
<p>Through irregular inheritance and other exceptional circumstances, a class of <em>nouveaux riches</em>&nbsp;arose in the town and also in decimated Siena. Their wealth was accentuated by the impoverishment of many of the older families, such as the Bardi and the Peruzzi, who had lost their fortunes in the financial collapse. In both cities, too, many tradesmen and artisans were enriched to a degree unusual for the <em>popolo minuto</em>. One of the major conflicts of the time [was] the struggle between the old families and this <em>gente nuova</em>. Outcries against both foreigners and the newly rich, never lacking in the two cities, increased in volume and violence... For these <em>homines novi</em>, the immigrants, the newly rich and the newly powerful, had not on the whole been closely identified with the growth of the new culture in the first half of the 14th century. They adhered to more traditional patterns of thought and feeling, and it may well be that their ideal of a religious art was still the art of the later 13th century -- an art that was still visible almost everywhere in the cities, and even more in the churches of the <em>contado</em>. ...Writing in 1361 in his will about a Madonna by the &quot;outstanding&quot; painter Giotto, Petrarch says that though its beauty is a source of wonder to the masters of the art, the ignorant do not understand it. Boccaccio wrote that, as a poet and a student of classical antiquity, he was regarded as a sorcerer in the town of Certaldo...</p>

