<h2>Medieval Merchants and Trade</h2>
<p>In the emerging bourgeois economy of the 13th and 14th centuries, the potential success of merchants grew ever greater. More and more people began to have the means to purchase more and more goods, and so the demand for such goods increased. Foreign spices, silks, and <a href="../../plague/origins/spread.php">other less desired products</a> were brought from around the world. The main players in the trade game were the Venetians and the Genoese. In an ongoing battle to control trade routes, the competition between these two cities was fierce, at times escalating into violent confrontations.</p>

