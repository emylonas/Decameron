<h2>Authorship</h2>
<p>Before the fourteenth century books were mostly written in Latin and the word <em>auctor</em>&nbsp;(author) was used only to define writers who composed their works in ancient languages (ancient Greek texts were generally translated into Latin). Later in the same century texts written in the vernacular became common and the word <em>auctor</em>&nbsp;was used to denote contemporary writers.</p>
<p> However, the author was not the same person as the person who physically wrote down the text: this was the function of the <em>copista</em>&nbsp;(copyist), who was in charge of writing the text down under the author's dictation; he also made other copies from the manuscript.</p>
<p>While in the early Middle Ages only monks performed the duties of the <em>copisti</em>, later on the task was undertaken by laymen who worked in the <em>botteghe</em>&nbsp;(shops) on the orders of the author. Also sometimes readers particularly fond of certain books would copy them independently (e.g. Boccaccio and Petrarch).</p>
<p>The production and distribution of books was increased by the diffusion of schools and universities: students' books were now made by <em>copisti</em>&nbsp;able to follow the book's production from its writing to its binding. Such texts were written mainly in Latin. Latin texts, law books, translations from Greek and Arabic (mainly philosophical and scientific texts), classical authors, were produced generally by universities and monasteries. Vernacular production flourished particularly in Tuscany.</p>
