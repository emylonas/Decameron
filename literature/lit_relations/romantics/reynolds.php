<h2>From "The Ladye of Provence" by John Hamilton Reynolds</h2>
<p>&quot;...through the night<br/>
Strange phantoms trampled o'er his heart, and died<br/>
Fiercely before his eyes. --His menials heard<br/>
Pitiful screams at midnight in his room,<br/>
But never might they break his solitude.<br/>
At last, grief-madden'd,--from Provence he fled,--<br/>
No one knew whither: He return'd no more!&quot;</p>

<p>1821, <em>The Garden of Florence : and other poems</em>, Wright p.174.</p>
<p>The above passage depicts the extravagant remorse of Virgillisi, the disgruntled husband (&quot;Roussillon&quot; in Boccaccio [<a href="../../../texts/DecShowText.php?lang=eng&myID=nov0409&expand=day04"><em>Decameron</em> IV.9</a>]) who, like the killer of <a href="wilmot.php">William Wilmot's &quot;The Tale of Gismunda and Guiscardo&quot;</a>, exhibits a profound display of conscience and grief at the death of the lover of the man he killed, who in this case is his wife. Interestingly, Wilmot demonstrates little concrete reason for this show of guilt; the act which inspired it after all &quot;was fully justified, and only the means employed to wreak his vengeance could be condemned&quot; (Wright 425). Perhaps this passage can be viewed as an agreement with Boccaccio's &quot;belief in the invisible power of love, regardless of law or moral code and regardless of consequence&quot; (ibid.), implicit in the source tale. Despite the disrespect the lady showed for her marriage, her acts of true love were profound and cannot therefore be completely condemned.</p>

<h2>From &quot;The Garden of Florence&quot; by John Hamilton Reynolds</h2>
<p>&quot;They met all innocence--and hope--and youth;<br/>
And all their words were thoughts--their thoughts, pure truth.&quot;</p>
<p>&quot;Their eyes in married lustre could not part,<br/>
But, lighted by the radiance of the heart,<br/>
Shone on each other.&quot;</p>

<p>1821, <em>The Garden of Florence: and other poems</em>, st. vi and xii. In Wright.</p>
<p>These two short passages (based on <a href="../../../texts/DecShowText.php?lang=eng&myID=nov0407&expand=day04"><em>Decameron</em> IV.7</a>) are further indications of the above notion of Reynolds as a poet concerned with true love, its trappings of devotion and passion. In themselves they show a shared concern of this poet with Boccaccio, whose stories provide constant illustrations of his preoccupation with profound human love. Moreover, in demonstrating this shared concern they support the above reading of &quot;The Ladye of Provence.&quot;</p>
<p>(S. K.)</p>

