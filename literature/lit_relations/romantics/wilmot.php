<h2>From "The Tale of Gismunda and Guiscardo" by William Wilmot</h2>
<p>&quot;He felt the tempest, that within had brewed,<br/>
Burst forth, and with it life itself renewed :<br/>
Roared out his woe, and frantic with despair,<br/>
Plucked at his beard, and tore his hoary hair,<br/>
Upon her lifeless corpse his body flung,<br/>
Embraced it, kissed it, hugged it, to it clung,<br/>
Implored them both in mercy to forgive,<br/>
Howled out for death-and yet was doomed to live!&quot;</p>

<p>1819, <em>The Tale of Gismunda and Guiscardo</em>, Wright p. 70</p>
<p>The passage above describes the extreme and passionate grief of Tancredi (&quot;Tancred&quot; in Wilmot's poem) following the death of his daughter Ghismonda (&quot;Gismunda&quot;) (<em>Decameron</em> <a href="../../../texts/DecShowText.php?lang=eng&myID=nov0401&expand=day04">IV.1</a>). It is a portion of a lengthy description of this profound grief that ends with &quot;a mood of sentimental forgiveness ...as if remorse could atone so soon for Tancred's savage crime&quot; (Wright 411). This sentimentality is absent in Boccaccio's tale, which in fact suggests that &quot;the time for pity was past&quot; (<em>Decameron</em>, trans., McWilliam, p. 341). Wilmot's version on the whole demonstrates a propensity for descriptions of Nature, as indicated by the figurative reference to a &quot;tempest&quot; in the above passage. It can be argued that this preoccupation with the natural world would explain the poet's willingness to forgive Tancred, whose cruelty, though excessive, derived from the natural inclinations of a father.</p>
<p>(S. K.) </p>

