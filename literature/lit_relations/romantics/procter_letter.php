<h2>From "The Letter of Boccaccio" by B. W. Procter</h2>
<p>&quot;Then Love came-Love!--How like a star it streamed<br/>
In infancy upon me,--till I dreamed,<br/>
And 'twas as pure and almost cold a light,<br/>
And led me to the sense of such delight<br/>
As children know not; so, at last I grew<br/>
Enamoured of beauty and soft pain,<br/>
And felt mysterious pleasure wander through<br/>
My heart, and animate my childish brain;<br/>
And thus I rose (for patient still was I<br/>
And a true worshipper)-to poetry.&quot;</p>

<p>1823. <em>The Flood of Thessaly</em>, New York: Garland, 1978</p>
<p>This passage of Procter's poem indicates his notion of the spirit of Boccaccio as somewhat childlike in its light-heartedness and simplicity. It declares the love of Boccaccio for Fiammetta an inspiration for his poetry and thus connects the spirit of this love with the spirit of his poetry (and alludes naturally to the Dante-Beatrice relationship). Elsewhere in the poem, Procter's Boccaccio recalls Fiammetta against a background of natural beauty, making a subsequent connection between Nature and love which is evident also in &quot;A Sicilian Story&quot; and in the <em>Decameron</em>.</p>
<p>(S. K.) </p>

