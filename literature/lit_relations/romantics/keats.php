<h2>From "Isabella" by John Keats</h2>
<p>&quot;50<br/>
With duller steel than the Persean sword<br/>
They cut away no formless monster's head,<br/>
But one, whose gentleness did well accord<br/>
With death, as life. The ancient harps have said,<br/>
Love never dies, but lives, immortal Lord :<br/>
If Love impersonate was ever dead,<br/>
Pale Isabella kiss'd it, and low moan'd.<br/>
'Twas love; cold,--dead indeed, but not dethroned.</p>
<p>51<br/>
In anxious secrecy they took it home,<br/>
And then the prize was all for Isabel:<br/>
She calm'd its wild hair with a golden comb,<br/>
And all around each eye's sepulchral cell<br/>
Pointed each fringed lash; the smeared loam<br/>
With tears, as chilly as a dripping well,<br/>
She drench'd away:--and still she comb'd, and kept<br/>
Sighing all day--and still she kiss'd, and wept.</p>
<p>52<br/>
Then in a silken scarf,--sweet with the dews<br/>
Of precious flowers pluck'd in Araby,<br/>
And divine liquids come with odorous ooze<br/>
Through the cold serpent-pipe refreshfully,--<br/>
She wrapp'd it up; and for its tomb did choose<br/>
A garden-pot, wherein she laid it by,<br/>
And cover'd it with mould, and o'er it set<br/>
Sweet basil, which her tears kept ever wet.</p>
<p>53<br/>
And she forgot the stars, the moon, and sun,<br/>
And she forgot the blue above the trees,<br/>
And she forgot the dells where waters run,<br/>
And she forgot the chilly autumn breeze;<br/>
She had no knowledge when the day was done,<br/>
And the new morn she saw not: but in peace<br/>
Hung over her sweet basil evermore,<br/>
And moisten'd it with tears unto the core.&quot; </p>

<p>1818. John Keats, <i>Collected Poems</i>, Cambridge: Harvard University Press, 1982.</p>
<p>Perhaps the most renowned of the Romantic retellings of Boccaccio's tales, Keats' &quot;Isabella&quot; demonstrates both a masterful sense of the original story (<a href="../../../texts/DecShowText.php?lang=eng&myID=nov0405&expand=day04"><i>Decameron</i> IV.5</a>) as well as a refined, exquisite quality of verse. However, due to certain liberties taken by the translator who composed the version of the tale Keats happened to read, the tale tends in his adaptation to be somewhat idealized, his intention being &quot;to portray an ideal passion&quot; (Wright 400). This inclination can explain the lack of gruesome detail of the disinterment of Lorenzo's head as it occurs in the <i>Decameron</i>, particularly in the translation of 1620, which mentions a razor that the distraught lover brings with her to the burial sight. As is evident in stanza 53 of this passage, the depiction of the forest does not inspire gloom or terror as it might due to the nature of the situation; rather, Keats presents an idyllic countryside. Nature, like the love of Lorenzo and Isabella, is idealized in Keats' adaptation.</p>
