<h2>From an Adaptation of the Tale of Masetto (<a href="../../../texts/DecShowText.php?lang=eng&myID=nov0301&expand=day03">III.1</a>) by John Cam Hobhouse</h2>
<p>&quot;And thus since then the deaf and dumb<br/>
In crowds before the convent come;<br/>
In pious hopes to gain their shares<br/>
Of comfort by the vestals prayers :<br/>
And like the blest Massetto prove<br/>
The mighty power of heavenly love.&quot;</p>
<p>Hobhouse, <em>Imitations and Translations from the Ancient and Modern Classics</em>, London: Longman, Hurst, Rees and Orme, 1809. p. 128.</p>
<p>In his adaptation, John Cam Hobhouse, a Protestant, clearly emphasizes the parody of the Catholic Church that Boccaccio presents with the tale of Masetto. The pilgrimage of the faithful to the convent that Hobhouse describes does not occur in the <em>Decameron</em>; it is an addition that in the irony of its tone takes on a strong satirical intention. Perhaps the most ironic moment of this stanza occurs with the phrase &quot;heavenly love,&quot; which in actuality refers to the most earthly and, indeed, the most sinful form of love.</p>
<p>(S. K.)</p>

