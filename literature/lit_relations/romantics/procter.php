<h2>From "A Sicilian Story" by B. W. Procter</h2>
<p>&quot;Nature there in wildest guise<br/>
Stands undebased and nearer to the skies;<br/>
And 'midst her giant trees and waters wide<br/>
The bones of things forgotten, buried deep,<br/>
Give glimpses of an elder world.&quot;</p>

<p>1820, <em>A Sicilian Story</em>, st. ix. In Wright.</p>
<p>B. W. Procter's retelling of the tale of Lorenzo and Lisabetta (<a href="../../../texts/DecShowText.php?lang=eng&myID=nov0405&expand=day04"><em>Decameron</em> IV.5</a>), who are Guido and Isabel in his poem, strays from the original less than some other Romantic efforts at adaptation. Implicit in the poem is &quot;an intimate bond between man and Nature&quot; (Wright 415) which is less significant in Boccaccio's story yet which clearly exists in a number of other tales of the <em>Decameron</em>. The above passage depicts Guido's account of America, where &quot;man, untouched by civilisation, [is] in harmony with the primaeval forest&quot; (ibid.). This intuitive sense of man's connection with Nature can explain Isabel's vision, which in Procter's poem juxtaposes the spirit of Guido with the basil-tree under which his head is buried. </p>

