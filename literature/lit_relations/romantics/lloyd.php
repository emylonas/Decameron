<h2>From "Titus and Gisippus" by Charles Lloyd</h2>
<p>&quot;Towards winter autumn then was verging : --then,<br/>
For the first season, with unmuffled face,<br/>
Had winter dar'd to stalk thro' ev-ry scene,<br/>
Of tints, of flowers, of leaves, which seem'd to lean,<br/>
With a meek trust, in the prolong'd embrace<br/>
Of nature : for the first time, then arose<br/>
The distant mountains clad with morning snows.</p>
<p>Upon the half-stripp'd branches, which did bend<br/>
To the wild blast, here droop'd a yellow leaf,<br/>
And there a brown one. With day's light did blend<br/>
A sombre shade which spoke of nature's grief.--<br/>
To the eager air the season seem'd to lend<br/>
A piercing shrewdness; and if still a sheaf<br/>
Broke the long furrows' level, soddening rains<br/>
Had smear'd its golden hue with dingy stains.&quot;</p>

<p>1821. <em>Titus and Gisippus, with other poems</em>, st. 107-08. In Wright.</p>
<p>The above passages from Lloyd's &quot;Titus and Gisippus&quot; demonstrate what Herbert Wright calls an &quot;imbalance&quot; in the poem, an abundance of lengthy descriptions that dominate portions of the story, such as Titus' dramatic and powerful speech, which are significant in Boccaccio's original tale (<a href="../../../texts/DecShowText.php?lang=eng&myID=nov1008&expand=day10"><em>Decameron</em> X.8</a>). However, they are intriguing in that they use natural description to provide a sense of ill omen and to create an elaborate mood. The poet is allowed the possibility of this type of description by the structure of verse rather than the more limiting descriptive prose genre that Boccaccio employed. Lloyd's poem is thus an interesting example of the alterations that verse retellings of the <em>Decameron</em>, both by the Romantics and by other poets, have been able to elaborate.</p>
<p>(S. K.) </p>

