<h2>From an Adaptation of the Tale of Lydia and Nicostratus (<a href="../../../texts/DecShowText.php?lang=eng&myID=nov0709&expand=day07">VII.9</a>) by Thomas Moore</h2>
<p>"She soon to Nicostratus said,<br/>
'A truth to tell I'm half afraid,<br/>
You cannot surely but observe,<br/>
Your pages' conduct while they serve.'<br/>
'Yes, that I have most certainly,<br/>
And often thought to ask them why.'<br/>
'My love, the youths would deem it treason,<br/>
To let you know the real reason;<br/>
But my regard surmounts all fear,<br/>
So plainly I must tell you, dear,<br/>
What ever lately has befell,<br/>
Your breath exhales a carious smell;<br/>
A grievous circumstance to me,<br/>
As well as to your company.'<br/>
Exclaim'd he, 'What!  my breath impure,<br/>
No tooth have I unsound I'm sure.'<br/>
'Perhaps you may, love, open wide,<br/>
Your mouth and let me look inside.'<br/>
She then examin'd every part,<br/>
And soon exclaim'd, 'My precious heart!<br/>
So long how could you with it bear,<br/>
Here's one as rotten as [a] pear;<br/>
If its extraction you delay,<br/>
'Twill all your sounder ones decay.<br/>
So I advise, beyond a doubt,<br/>
Immediately you get it out.</p>

<p>1812, <em>Spirit of Boccaccio's Decameron</em> (published anonymously), vol. 3, pp. 188-89.</p>
<p>Thomas Moore's animated description of the scene from <em>Decameron</em> VII.9 wherein Lydia manipulates her husband into allowing her to remove his tooth successfully preserves the humorous nature of the tale. In fact, Boccaccio's fluent yet less lively prose conveys a certain violent nature of the situation that does not come through in Moore's adaptation. This poem, and perhaps the entirety of Moore's <em>Spirit of Boccaccio's Decameron</em>, can be viewed as a more light-hearted version of Boccaccio's tale.</p>
<p>(S. K.) </p>

