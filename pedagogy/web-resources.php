									<p class="headerstyle"><a name="copyright"></a>Copyright and Fair Use</p>
								</div>
								<div align="left">
									<p class="bodynoindent">Digital materials makes copyright increasingly complex. The Digital Millennium Copyright Law has begun to clarify how electronic materials comply to copyright law. You need to become aware that linking to other sites and uploading images, sound, and/or video to your site may have copyright infringements.</p>

									<p class="bodynoindent"><a href="http://copyright.com">&quot;Academic Permissions Service&quot;</a><br>
										Copyright Clearance Center is where you can clear reproduction rights and permissions online.</p>
									<p class="bodynoindent"><a href="http://fairuse.stanford.edu/">&quot;Copyright &amp; Fair Use&quot;</a><br>
										Stanford University Libraries</p>
									<p class="bodynoindent">Provides links organized into following categories:</p>

									<ul>
										<li class="bodynoindent">Primary Materials
										<li class="bodynoindent">Current Legislation, Cases and Issues
										<li class="bodynoindent">Resources on the Internet
										<li class="bodynoindent">Overview of Copyright Law
									</ul>
									<p class="bodynoindent"><a href="http://www.uoregon.edu/~tep/technology/onlinelearning/fairuse.html">&quot;Guide to Copyright and Fair Use Issues&quot;</a><br>
										Teaching Effectiveness Program, University of Oregon</p>
									<p class="bodynoindent">Provides a brief summary of the issue and links to other sites.</p>

									<p class="bodynoindent"><a href="http://www.libraries.psu.edu/mtss/fairuse/guidelinedoc.html">&quot;Fair Use Guidelines for Educational Multimedia&quot;</a><br>
										Prepared by the Educational Multimedia Fair Use Guidelines Development Committee July 17, 1996</p>
									<p class="bodynoindent">Cites the proposed guidelines from 1996. An interesting presentation of all of the issues relevant to educational use.</p>
								</div>
