									<p class="bodynoindent"><a href="#top">top</a></p>
									<p class="headerstyle"><a name="development"></a>Development Resources</p>

								</div>
								<div align="left">
									<p class="bodynoindent">The following sites are resources for helping you develop a course web site.</p>
									<p class="bodynoindent"><a href="http://www.wiu.edu/users/mfbhl/wiu/tech.htm">Bruce's Technology Center</a><br>
										&quot;Basic HTML&quot; is a good place to start if you are just beginning to learn how to create a web page from scratch.</p>
									<p class="bodynoindent"><a href="http://www.htmlhelp.com/faq/html/all.html">Web Authoring FAQ</a><br>

										Provides basics of web authoring with many links to helpful sites.</p>
									<p class="bodynoindent"><a href="http://depts.washington.edu/engl/cic/sgonline/glossary.php">Catalyst: Glossary</a><br>
										A useful list of definitions for many terms commonly used in connection to the Internet.</p>
									<p class="bodynoindent"><a href="http://www.uoregon.edu/~tep/technology/onlinelearning/anatomy.html">&quot;Anatomy of a Course Web site&quot;</a><br>
										Teaching Effectiveness Program, University of Oregon</p>
									<p class="bodynoindent"><a href="http://usableweb.com/">&quot;Usuable Web: Guide to Web Usability Resources&quot;</a><br>

										A collection of TONS of links and accompanying information about human factors, user interface issues, and usable design specific to the World Wide Web. Usable Web adds value to the links by providing descriptions, multiple organizational schemes (by date, site, topic, popularity) and search engine queries to even more resources.</p>
									<p class="bodynoindent"><a href="http://www.info.med.yale.edu/caim/manual/index.html">Web Style Guide</a><br>
										This site is a supplement to a very good book which the Sheridan Center has in its library for your use.</p>
								</div>
								<div align="center">
									<p class="bodynoindent"><a href="#top">top</a></p>
									<p class="headerstyle"><a name="eval-content"></a>Evaluating the&nbsp;Content of Web Sites</p>

								</div>
								<div align="left">
									<p class="bodynoindent">As students become more accustomed to surfing the web rather than using the library, the problem of discerning what is good and bad on the web increasingly becomes an issue. How do you teach your students what is good or bad content on a web site? Are your students developing the skills to assess their sources? The following sites may be good places to learn those skills with regard to electronic resources. In other words, you may want to pass these sites to your students as a way for them to begin to learn how to use the Web as a meaningful research tool.</p>
									<p class="bodynoindent"><a href="http://lib.nmsu.edu//staff/susabeck/eval.html">&quot;The Good, The Bad, and The Ugly&quot;</a><br>
										New Mexico State University Library</p>
									<p class="bodynoindent">Links organized into following categories:</p>
									<ul>

										<li class="bodynoindent">Examples (of inaccurate websites)
										<li class="bodynoindent">Criteria (for evaluating information in a web site)
										<li class="bodynoindent">Suggestions (for successful assignments)
										<li class="bodynoindent">Bibliography (with links)
									</ul>
									<p class="bodynoindent"><a href="http://www2.widener.edu/Wolfgram-Memorial-Library/webeval.htm">&quot;Evaluating Web Resources&quot;</a><br>
										Wolfgang Library, Widener University</p>
									<p class="bodynoindent">Contains PowerPoint presentations for teaching the subject to a group and checklists for different kinds of web pages. Uses actual pages as examples. Includes a bibliography.</p>
									<ul>

										<li class="bodynoindent">Personal
										<li class="bodynoindent">Business/Marketing
										<li class="bodynoindent">Advocacy
										<li class="bodynoindent">Informational
										<li class="bodynoindent">News
									</ul>
									<p class="bodynoindent"><a href="http://www.ithaca.edu/library/Training/hott.html">&quot;Guide to Critical Thinking about What You See on the Web&quot;</a><br>
										Ithaca College</p>
									<p class="bodynoindent">Well organized site with links to other sites. It also includes a very interesting homework assignment for students. If you use it, the authors ask that you forward the results from your students so they can add the data to that which they generate from their own classes. Example of a distracting background.</p>
									<p class="bodynoindent">Links to sites that instruct students in developing productive research/search strategies and skills for the evaluation of information on web sites.</p>

									<p class="bodynoindent"><a href="http://www.mtsu.edu/~cdharris/term-paper-guide/jge_v47_1999.htm ">&quot;Using the Internet to Prepare a Term Paper for a Geology Course&quot;</a><br>
										Harris and McCartney, 1999</p>
									<p class="bodynoindent"><i>Journal of Geoscience Education</i>, 47, 17-22.</p>
									<p class="bodynoindent"><b>WAIT, DON'T BE HASTY!</b> No matter the slant of this site, it could be very useful for you. The authors of this site and a related one explicitly give permission for you, the instructor, to download the text , to alter it to increase its relevance for your class, and to distribute it to your students.</p>
									<p class="bodynoindent"><a href="http://www.wiu.edu/users/mfbhl/evaluate.htm">&quot;Evaluating Web Sites: A Guide for Writers&quot;</a> and <a href="http://www.wiu.edu/users/mfbhl/websource.htm">&quot;Research on the Web: How Reliable is it?&quot;</a></p>

									<p class="bodynoindent">Two short papers describing the &quot;how to's&quot; of evaluating and researching on the Web.</p>
