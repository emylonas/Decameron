									<p class="headerstyle"><a name="prev-code-viol"></a>Preventing Academic Code Violations With Regard to Writing Papers</p>
								</div>
								<div align="left">
									<p class="bodynoindent">Not only do our students need to learn how to do reseach on the Web, they also need to learn what is acceptable use of web materials. Part of the problem is education with regard to copyright, but another part of the problem may exist in the assignments we as teachers assign. The following sites provide strategies and tips regarding the creation of assignments that will help avoid violations in the academic.</p>
									<p class="bodynoindent"><a href="http://www.uhh.hawaii.edu/uhh/teaching/PreventingPlagiarism.php">&quot;Preventing Plagiarism&quot;</a></p>
								</div>

								<div align="center">
									<p class="bodynoindent"><a href="#top">top</a></p>
									<p class="headerstyle"><a name="plagiarism"></a>Plagiarism</p>
								</div>
								<div align="left">
									<p class="bodynoindent">While you try to create assignments that will prevent academic code violations, nothing can be foolproof. An increasing number of resources created for students and educators alike can help you determine if a student's work is plagiarized from another web site.</p>
									<p class="bodynoindent"><a href="http://www.wiu.edu/users/mfbhl/wiu/plagiarism.htm">Plagiarism and the Web</a></p>

									<p class="bodynoindent">This is a short essay on the topic; more importantly, there is a short list of places where you and your student can get free papers and/or research assistance. This topic has recently been in the New York Times and The Chronicle of Higher Education. Some of the sites they mention may be found in this list.</p>
									<p class="bodynoindent"><a href="http://www.plagiarism.org">Plagiarism.org</a></p>
									<p class="bodynoindent">This site uses a database of papers and a series of mathematical formulas to determine the likelihood that a paper was plagiarized.</p>
									<p class="bodynoindent"><a href="http://www.coastal.edu/library/presentations/plagiarz.html">&quot;Detecting Plagiarized Papers&quot;</a></p>
									<p class="bodynoindent">Short list of things to look for.</p>
