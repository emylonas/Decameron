<h2>Activities Related To the Brigata</h2>
<h3 class="break">1. Brigata Seating Pattern</h3>
<p>For each day of the Decameron you read, create a chart to make a note of the seating pattern of the storytellers. Keep in mind the following questions as you gradually fill in the chart:</p>
<ul class="links">
	<li>Who is seated close together or far apart and what is their relationship?</li>
	<li>What friends or &quot;couples&quot; can we identify in the seating arrangement?</li>
	<li>Are the storytellers juxtaposed or grouped in any way thematically?</li>
</ul>
<h3 class="break">2. Portrait Of a Storyteller</h3>
<p>Create a new literary portrait of your assigned brigata character. Using the information contained in The Brigata section of the Decameron Web, the online bibliography, and secondary books/articles in the library, thoroughly research your character and write a concise description of him/her. Each &quot;literary portrait&quot; should adhere to the following structure. Make sure to include the following elements in your essay:</p>
<ul class="links">
	<li>General Character Traits
		<ul>
			<li>Physical Attributes (with an illustration, when possible)</li>
			<li>Psychological Attributes</li>
			<li>Historical Background (if any)</li>
			<li>Symbolic Value</li>
		</ul>
	</li>
	<li>Storyteller Profile
		<ul>
			<li>List of tales narrated by your storyteller</li>
			<li>Recurrent themes in the stories recounted by your characters</li>
			<li>Your storyteller's reaction to the stories of others</li>
		</ul>
	</li>
	<li>Appearance in Other Boccaccio Works (include citations). Search the following works by Boccaccio using the OVI site for additional references to your brigata character:
		<ul>
			<li>La Caccia di Diana</li>
			<li>Il Filocolo</li>
			<li>La Teseida delle nozze d'Emilia</li>
			<li>Il Filostrato</li>
			<li>La Comedia delle Ninfe Fiorentine</li>
			<li>L'Amorosa Visione</li>
			<li>Il Ninfale Fiesolano</li>
			<li>Il Corbaccio</li>
		</ul>
	</li>
	<li>Bibliography
</ul>
<p>Include a list of all primary and secondary sources used in your research (formatted in accordance with existing Decameron Web article citations).</p>

<h3 class="break">3. Creating a Deeper Narrative Identity</h3>
<p>In the previous assignment, you explored the general characteristics of your brigata member. As a natural extension of this project, examine your character's &quot;narrative identity&quot; in greater depth by carefully examining the tales that he/she recounts in the first three days. What are the common themes, narrative structures, and rhetorical strategies that unite the three novelle? Do the stories seem to accurately reflect the personality of the storyteller? And what is the reaction of the other brigata members to your character's stories</p>

