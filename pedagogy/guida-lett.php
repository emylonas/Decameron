<h2>Una breve guida linguistica per chi legge l&rsquo;italiano antico per la prima volta</h2>
<div>



                                <p>Nonostante l&rsquo;opinione di alcuni che la prosodia di Boccaccio sia difficile da leggere, la maggior parte delle difficolt&agrave; incontrate dal lettore moderno si deve semplicemente al fatto che parecchie regole linguistiche si sono trasformate nel corso degli ultimi 700 anni. In altre parole, non scriviamo pi&ugrave; come una volta. Non appena ci si sar&agrave; abituati alle norme della grammatica trecentesca, molti impedimenti alla comprensione spariranno. Si presenta qui sotto uno schema, diviso in quattro campi linguistici, delle modifiche storiche che possono rendere una prima lettura di questi testi a volte malagevole. In breve, si risponde alla domanda &laquo;Quali sono i cambiamenti accaduti nel corso dei secoli che possono presentare problemi di comprensione&raquo;?</p>
                              <div align="center"><p class="headerstyle">MORFOLOGIA</p></div>

<ol class="bodynoindent">

<li>Si &egrave; diminuita la diffusione di allotropia verbale, cio&egrave; si sono regolarizzate alcune terminazioni verbali. Questo processo si &egrave; verificato soprattutto nella prima persona singolare (e.g. <em>era</em>/<em>ero</em>; <em>amava</em>/<em>amavo</em>; <em>seggo</em>/<em>siedo</em>; <em>veggo</em>/<em>veggio</em>/<em>vedo</em>; <em>vegno/vengo</em>;<em> cheggio/chero/chiedo</em>) e nella terza persona singolare e plurale (e.g. <em>bee</em>/<em>beve</em>; <em>dee/deve</em>; [cfr. <em>dei/devi</em>;] <em>veggono</em>/<em>veggiono</em>/<em>vedono</em>; <em>face/fa</em>; [cfr. <em>dicere/dire</em>;]<em> ven/viene</em>;<em> sieno</em>/<em>siano</em>; <em>dolve/dolse</em>;<em> puote/pu&ograve;</em>;<em> f&eacute;</em>/<em>fe&rsquo;</em>/<em>fece</em>; <em>enno/sono</em>). N.B. ogni tanto, forme verbali che finiscono con una vocale accentata prendono, per epentesi, un&rsquo;altra sillaba (e.g. <em>finio/fin&igrave;</em>; <em>sentio/sent&igrave;</em>; <em>feo/f&eacute;/fece</em>).</li>

<li>Il condizionale e l&rsquo;imperfetto di certi verbi poteva formarsi con le terminazioni in &ndash;<em>ia</em> (come succede ancora in spagnolo e in portoghese) (e.g. <em>venia</em>/<em>venivo </em>o <em>veniva</em>; <em>diverria/diverrebbe</em>). Ci sono innumerevoli altri esempi di cose simili, tra cui: <em>morieno</em>/<em>morivano</em>; <em>sarieno</em>/<em>sarebbero</em>; <em>rimanean</em>/<em>rimanevano</em>; <em>ponieno</em>/<em>ponevano</em>; <em>aveono</em>/<em>avevano</em>/<em>avevano</em>; <em>parea</em>/<em>pareva</em>. Nonostante ci&ograve;, sono tutti facilmente riconoscibili dopo un po&rsquo; di pratica.</li>

<li>&Egrave; sparito il futuro del congiuntivo (ancora presente ad esempio in portoghese), pi&ugrave; comune nel verbo <em>essere</em>. <em>Fia </em>(&lt; lat. &lsquo;fiat&rsquo;) = sia, sar&agrave;. Cfr. <em>fiero/fieno</em> (plurale).</li>

<li>Si &egrave; diminuito l&rsquo;uso del participio presente come aggettivo. E.g. <em>la gente ascoltante</em> = la gente che ascolta.<br>
</li>
</ol>

<div align="center"><p class="headerstyle">LESSICO</p></div>

<ol class="bodynoindent">
<li>Si sono ridotti i suffissi in -<em>anza</em>. <em>fidanza</em> &lsquo;fiducia&rsquo;; <em>prestanza </em>&lsquo;prestito.&rsquo; &Egrave; una terminazione che si adoperava spesso (quando la forma italiana gi&agrave; esisteva) per aggiungere un po&rsquo; di &laquo;colore&raquo; francese.<br></li>

<li>Si sono ridotti i suffissi in -<em>mento</em>. <em>salvamento</em> &lsquo;salvezza&rsquo;; <em>cominciamento</em> &lsquo;inizio.&rsquo;<br></li>

<li>Si sono ridotti i prefissi in <em>dis</em>-. <em>disparve </em>&lsquo;spar&igrave;&rsquo;; <em>discoperto </em>&lsquo;scoperto;&rsquo;<br></li>

<li>Si &egrave; ridotto l&rsquo;uso della <em>i</em> epentetica (e spesso della <em>i</em> del prefisso <em>is</em>- che deriva dal latino <em>ex</em>-) e, per contagio, &egrave; sparita la <em>i </em>iniziale di molte parole latine che cominciavano per <em>hi</em>-, nonch&eacute; di qualche parola derivata da altre lingue (e.g. <em>ismarrire</em> [dal antico francese <em>esmarrir</em>] &lsquo;smarrire&rsquo;); <em>istare</em> &lsquo;stare&rsquo;; <em>isposare</em> &lsquo;sposare&rsquo;; <em>ispiacevole</em> &lsquo;spiacevole&rsquo;; <em>ispendere</em> &lsquo;spendere&rsquo; / <em>istoria</em> &lsquo;storia; <em>Ispagna</em> &lsquo;Spagna&rsquo;.<br></li>

<li>Si &egrave; aumentata la facilit&agrave; di creazione di nuove parole composte da un verbo e un sostantivo (attaccapanni, lavastoviglie, ecc.). Le poche combinazioni trecentesche di questo genere si riferivano di solito a persone e si utilizzavano in modo comico o con una connotazione negativa (e.g. <em>tagliaborse</em>).<br></li>

<li>Si sono regolarizzati alcuni fonemi che erano ancora in uno stato di oscillazione (e.g. <em>scovrire</em>/<em>scoprire</em>; <em>cognoscere</em>/<em>conoscere</em>; <em>fedito/ferito</em>; <em>romore/rumore</em>; <em>rugale/rurale</em>; <em>savere/sapere</em>; <em>notare/nuotare</em>; <em>leofante/elefante</em>; <em>loico/logico</em> [filosofo]; <em>giovine/giovane</em>), tra cui molti che si derivano dalla <em>e</em> o dalla <em>o</em> breve del latino (<em>priego/prego</em>; <em>foco/fuoco</em>; <em>for/fora/fuori</em>; <em>omo/uomo</em>;<em> loco/luogo</em>;<em> cor/cuore</em>;<em> puosi/posi</em>;[cfr. <em>fur/fuor/furono</em>]).<br></li>

<li>Il significato moderno di alcune parole non &egrave; pi&ugrave; uguale a quello medievale. Un ottimo esempio &egrave; la parola <em>per&ograve;</em>, la quale corrispondeva sia a <em>ma</em>, sia a <em>perci&ograve;</em>. Notare anche che <em>per&ograve; che</em> vuol dire <em>perch&eacute;</em>. Vedasi sotto un elenco di lemmi comuni.</li>

<li>Si &egrave; diminuito il numero di parole con forme irregolari derivanti dal neutro latino (e.g. <em>il peccato</em>, <em>le peccata</em>; <em>il pugno, le pugna</em>).</li>
<li>Si sono regolarizzate alcune forme di parole comuni (e.g. <em>giuso/gi&ugrave;</em>;<em> suso/s&ugrave;</em>).</li>

<li>Si sono regolarizzate alcune forme che si prestavano spesso alla sincope (e.g. <em>merto</em>, <em>tosco</em>, <em>spirto</em>) o all&rsquo;apocope (e.g. <em>cacciar/cacciarono</em>, <em>dier/diedero</em>). E.g. <em>vedest&ugrave;</em> = vedesti (tu).</li>
</ol>

<div align="center"><p class="headerstyle">SINTASSI</p></div>

<ol class="bodynoindent">
<li>Si &egrave; modificata la sistemazione dei pronomi. I linguisti si riferiscono all&rsquo;uso medievale di attaccare i pronomi (cio&egrave; l&rsquo;enclisi) dove ora non &egrave; pi&ugrave; possibile come la legge Tobler-Mussafia. (Ogni tanto questo fenomeno crea confusione per chi non sta attento; &ldquo;parti,&rdquo; per esempio, pu&ograve; voler dire &ldquo;ti pare.&rdquo;) Secondo questo principio, un pronome atono era quasi obbligatoriamente enclitico nei seguenti casi:<br><br>

i. all'inizio di un periodo:<br><br>

	&quot;Risposele adunque il re, pi&ugrave; nella mente che nel viso o che nelle parole turbatoâ€¦&quot;<br><br>

ii. dopo le congiunzioni <em>e</em>, <em>ma </em>ed <em>o</em>:<br><br>

	&quot;...non ci ha mandato candela niuna, ed emmi convenuto mangiare al buio.&quot;<br><br>

iii. all'inizio della reggente quando questa segue una frase subordinata:<br><br>

	&quot;Quando la giovane il vide, presso fu che di letizia non mor&igrave;, e non potendosene tenere, subitamente con le braccia aperte gli corse al collo e abbracciollo.&quot;<br><br></li>

<li>Si sono standardizzate le forme dei pronomi di oggetto diretto e indiretto.<br><br>

Es. &quot;e come il videro, maravigliandosi forte, il domandarono chi del pozzo l'avesse tratto.&quot;<br><br></li>

<li>Si &egrave; modificato l&rsquo;ordine dei costituenti. Nel fiorentino antico la successione dei pronomi atoni in combinazione con i pronomi di oggetti diretto e con <em>ne</em> era spesso inversa rispetto a quella dell&rsquo;italiano moderno.<br><br>

Es. &quot;...pianamente levatosi se n'and&ograve; al letticello dove la giovane amata da lui si giaceva, e miselesi a giacere allato.&quot;<br><br></li>

<li>&Egrave; diventata pi&ugrave; rara la cosiddetta &quot;risalita&quot; del pronome atono.<br><br>

Es. &quot;Egli la venne a annunziare in Nazarette&quot; = &quot;venne ad annunziarla&quot;<br><br></li>

<li>Non si usa pi&ugrave; il pronome <i>egli</i> (o <i>e'</i> in forma troncata) per soggetti impersonali.<br><br>

Es. &quot;Ohim&egrave;, Gianni mio, or non sai tu quello ch'egli &egrave;?&quot;<br><br></li>

<li>Si &egrave; diminuita la tendenza (di origine latina) di collocare il verbo alla fine del periodo.<br><br>

Es. &quot;E s&igrave; come la estremit&agrave; della allegrezza il dolore occupa, cos&igrave; le miserie da sopravegnente letizia sono terminate.&quot;<br><br></li>

<li>Si &egrave; diminuita la tendenza (anche questa di origine latina) di spezzare il nesso &quot;ausiliare + participio&quot; e quello &quot;verbo servile + infinito.&quot;<br><br>

Es. &quot;...fu da molte immondizie purgata la citt&agrave; da oficiali sopra ci&ograve; ordinati e vietato l'entrarvi dentro.&quot;<br><br>

&quot;Cominci&ograve; i suoi dolorosi effetti, e in miracolosa maniera, a dimostrare.&quot;<br><br></li>

<li>Si &egrave; regolarizzato l'accordo del participio passato con il verbo e del verbo con il soggetto.<br><br>

Es. &quot;Aveva la luna, essendo nel mezzo del cielo, perduti i raggi suoi...&quot;<br><br>

&quot;Questa liberalit&agrave; d'Aldobrandino piacque molto ai fratelli di Tedaldo, e a ciascuno uomo e donna che quivi era.&quot;<br><br></li>

<li>Non si dovrebbe pi&ugrave; usare il condizionale presente nel discorso indiretto.<br><br>

Es. &quot;disse che egli il sicurerebbe della mercatantia la quale aveva in dogana.&quot;<br><br></li>

<li>Il trapassato remoto non indica pi&ugrave; il compiersi immediato di un'azione.<br><br>

Es. &quot;La giovane cominci&ograve; la sua medicina, e in brieve anzi il termine l'ebbe condotto a sanit&agrave;.&quot;<br><br></li>

<li>&Egrave; quasi sparito l'uso della paraipotassi, cio&egrave; la tendenza di inserire prima di una subordinata parole come <i>s&igrave;</i> ed <i>e</i> che per il lettore moderno non sembrano avere nessun significato preciso.<br><br>

Es. &quot;Poich&eacute; voi di questo mi fate sicuro, e io il vi dir&ograve;.&quot;<br><br>

&quot;Oh! gli uomini bestemmiano tutto 'l giorno Iddio, e s&igrave; perdona egli volentieri a chi si pente d'averlo bestemmiato.&quot;<br><br></li>

<li>Non si usa pi&ugrave; l' &quot;accusativo con l'infinito&quot;, una costruzione grammaticale ripescata dagli autori latini.<br><br>

Es. &quot;Morbide donne, niun con ragione dir&agrave; messer Gentile non aver magnificamente operato.&quot;<br><br></li>

<li>Si &egrave; diminuita la tendenza ad usare un participio passato secondo le norme dell' &quot;ablativo assoluto&quot; del latino.<br><br>

Es. &quot;Il quale, udendo la sua donna a lui esser venuta, si maravigli&ograve; forte, e levatosi e fatto il nigromante chiamare, gli disse...&quot;<br><br></li>

<li>Non si usa pi&ugrave; il gerundio per espressioni di percezione.<br><br>

Es. &quot;la figliuola di Bernardo, il cui nome era Lisa, da una finestra dove ella era con altre donne, il vide correndo.&quot; = lo vide correre<br><br></li>

<li>Usiamo pi&ugrave; spesso la forma participiale di un verbo per derivarne un aggettivo (tranne che in rari casi di fossilizzazione linguistica, come &ldquo;la pistola &egrave; carica&rdquo;).<br><br>

Es. &quot;Dico, - disse Gianni - ch'e' pare che l'uscio nostro sia tocco.&quot;<br><br></li>

<li>&Egrave; ora meno flessibile la sequenza dei tempi.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Es. &ldquo;Non par ch&rsquo;i&rsquo; ti vedessi mai.&rdquo;<br><br></li>
<li>Si sono standardizzate le preposizioni articolate e semplici.<br><br>
Es. &ldquo;La dannosa colpa de la gola,&rdquo; &ldquo;a la pioggia mi fiacco,&rdquo; &ldquo;lo &rsquo;nferno,&rdquo; &ldquo;l&rsquo;altre stelle,&rdquo; &ldquo;li beni vani.&rdquo;<br><br></li>

<li>Si &egrave; standardizzato l&rsquo;uso dell&rsquo;articolo determinativo in espressioni di possesso.<br><br>
  Es. &ldquo;Bont&agrave; non &egrave; che sua memoria fregi.&rdquo; &ldquo;Questi non vene ammaestrato da la tua sorella.&rdquo;<br><br></li>
<li>Il pronome relativo <em>che</em>, quando oggetto d&rsquo;una preposizione, ora diventa &ldquo;cui.&rdquo;<br><br>

 Es. &ldquo;&hellip;&rsquo;l sacro amore in che io veglio.&rdquo;<br><br></li>
<li>Bisogna ora usare <em>ci</em> o <em>vi</em> in espressioni d&rsquo;esistenza dove prima lo si poteva omettere.<br><br> 
Es. &ldquo;&Egrave; chi creda pi&ugrave; volte il mondo in ca&ograve;sso converso.&rdquo;<br>

</li>
</ol>
	
<div align="center"><p class="headerstyle">ALTRE CONSIDERAZIONI</p></div>

<ol class="bodynoindent">
<li>C&rsquo;era spesso un netto divario tra il tessuto narrativo e il parlato, specialmente quando parlano personaggi di condizione non elevata. Nel parlato troviamo <em>be&rsquo;</em> &lsquo;bene&rsquo;, <em>te&rsquo;</em> &lsquo;tieni&rsquo; e cos&igrave; via.<br><br></li>

<li>Ci sono anche frequenti espressioni tipicamente toscane. E.g. <em>fo</em> &lsquo;faccio&rsquo;, <em>vo&rsquo;</em> &lsquo;voglio&rsquo;, <em>vo</em> &lsquo;vado&rsquo;.<br><br></li>

<li>L&rsquo;ortografia, una convenzione di origini relativamente moderne, non era ancora stata standardizzata nel Trecento. Perci&ograve;, si vedono spesso forme separate quali <em>s&igrave; fatto</em> &lsquo;siffatto&rsquo; e <em>pi&ugrave; tosto</em>, &lsquo;piuttosto.&rsquo; Inoltre, si pu&ograve; notare nella stessa opera l&rsquo;allotropia grafica, cio&egrave; un&rsquo;oscillazione di forme ortografiche. Sebbene sia il compito di ogni curatore scegliere le variazioni appropriate secondo i testi a sua disposizione, non &egrave; infine possibile imporre le nostre idee concernenti i vantaggi inerenti nella standardizzazione su testi antichi. E.g. <em>oppinione</em>/<em>opinione</em>, <em>ninferno</em>/<em>inferno</em>, ecc.<br><br></li>

<li>Molte antiche congiunzioni non sono pi&ugrave; usate, tra cui: <em>come che</em> &lsquo;bench&eacute;&rsquo;; <em>quantunque</em> &lsquo;per quanto&rsquo; (derivata analogamente dall&rsquo;avverbio quanto, come ad esempio le forme moderne <em>dovunque</em> o <em>chiunque</em>), <em>ancora che</em> &lsquo;bench&eacute;&rsquo;; <em>con ci&ograve; fosse cosa che</em> &lsquo;bench&eacute;&rsquo;; <em>con ci&ograve; sia cosa che</em> &lsquo;bench&eacute;&rsquo;; <em>come che</em> &lsquo;bench&eacute;&rsquo;; <em>per ci&ograve; che</em> o <em>&nbsp;per quel che </em>&lsquo;siccome&rsquo; o &lsquo;perci&ograve;&rsquo;; <em>secondo che</em> &lsquo;secondo quello che&rsquo; o &lsquo;a seconda di&rdquo;; <em>acci&ograve; che</em> &lsquo;affinch&eacute;&rsquo;; <em>con tutto che </em>&lsquo;nonostante.&rsquo;</li>

</ol>

