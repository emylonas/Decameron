<h2>Reviewing a Critical Article</h2>
<p>The first and most important step in writing a review of a critical article is reading it carefully a number of times until you come to a full understanding of the author's position. Your first reading will give you a preliminary impression of the author's thesis and will help you understand the basic structure of argument. When reading the article a second time, it may be helpful to consider some of the following questions and jot down notes to clarify the author's position:</p>
<ul class="links">
	<li>What is the author's primary thesis?</li>
	<li>What is the intent of the article?</li>
	<li>If the author's thesis were correct, how would this influence current interpretations of the <em>Decameron</em>?</li>
	<li>What arguments or examples does the author use to support his/her position?</li>
	<li>Are these arguments/examples adequate to defend his/her thesis?</li>
</ul>
<p>Once you understand the author's position and the arguments he/she uses to support it, decide whether or not you think his/her position is convincing. If so, explain how and why the author's arguments successfully lead to his conclusion; if you can think of any further points in support of the thesis, present them and explain why they are significant. If you disagree with the author, determine why you feel his/her arguments are inadequate. Are the examples he/she uses not concrete enough? Is he/she misinterpreting the text? Are there factors which the author has failed to take into consideration in his/her critical analysis? In either case, familiarize yourself with the parts of the <em>Decameron</em> treated by the author, examining them carefully for anything he/she may have missed.</p>

