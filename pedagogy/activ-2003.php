<p class="bodystyle">The subjects for the weekly textual analysis have been chosen for their importance to the text of the <i>Decameron</i> as a whole, and their relation to the themes of the various days. The textual analyses will thus serve several important functions: to allow the students to familiarize themselves with the unique facilities offered by the <i>Decameron Web</i> and this course; to navigate through the text in an investigative, non-linear way; and to build their own psycho-geography of the text based on their individual findings. In a broader sense, these exercises will also allow the students to gain experience in computer-assisted literary analysis, which may be applied to other texts they will encounter throughout their studies (the explosion of out-of-copyright texts and other electronic resources online should ensure that students will find other core texts). For non-literature or language specialists, the electronic medium facilitates a new way in to a sometimes dauntingly dense book.</p>

<p class="bodystyle">The <i>Decameron Web</i> allows students to investigate the text either in the original Italian or in English translation. The basic keywords for each themed exercise will be supplied in both languages, and qualified students are urged to use the Italian text rather than the Rigg translation on the site. The presence of both texts allows a comparison of the lexical choices made by the English translator, and students may wish to investigate further this aspect of the text in their weekly lab sessions. The instructor will suggest the basic keywords for each theme, but students are encouraged to follow their intuition and their findings to see where their research takes them. After the weekly reports are submitted for grading, the students will post their findings on an open-access folder on the server where they may access each other's work and contribute to a collaborative electronic environment.</p>



