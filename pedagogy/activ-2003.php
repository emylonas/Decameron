<p class="bodystyle">The online exercises are meant to help the class with textual analysis. Every week, the students will investigate the text of the <i>Decameron</i> through the electronic environment of the Decameron Web, via a series of guided textual analyses. These exercises will complement the students' readings and the discussions in class, and will provide raw data which can be integrated into more traditional discussions of the text (in papers or in class).</p>

<p class="bodystyle">Each student is required to write a short report describing their weekly electronic textual analysis, which is to be submitted in the Thursday class. Reports will be returned to the students and will be discussed briefly in group before the next activity.</p>

<p class="bodystyle">We recognize that this type of textual analysis will be new to most, if not all, of the students, and will therefore provide guidelines and suggestions at all times. In the same spirit of creative investigation, we do not want to prescribe the length of the reports, and will thus merely suggest desirable word limits. However, we hope that as the semester progresses, and as the students become more familiar both with the technology and the text of the Decameron, that they will move beyond a simple delineation of their results to a more integrated and personal study of the text. The weekly reports will be marked on a simple check-plus/check/check-minus scale.</p>

