<h2>Why did Boccaccio choose Santa Maria Novella?</h2>
<p>Why does Boccaccio have the members of his <em>brigata</em>&nbsp;meet in the church of <a href="../images/floor.gif">Santa Maria Novella</a> instead of another location in the city of Florence? It is perhaps because the Church represents a refuge from the <a href="../../plague/">plague</a> and the new and strange environment that has developed with it. In the church there is order and calm. It is also the first step of withdrawal from the chaos of Florence, but it is a place that is not in the &quot;wilderness&quot; of Nature, which because of the lack of human influence and control, could be a dangerous place.</p>
<p>When the <em>brigata</em>&nbsp;returns to Florence after their ten days in the countryside, they return first to Santa Maria Novella, the spot from which they departed. It seems that they need to find an environment that will not immediately toss them back into what was still at that time a chaotic city. After the relative freedom they have enjoyed, it would perhaps be traumatic to return directly to the city. The church is once again the mid-way point between Nature and the city. It functions as a sort of &quot;decompression chamber&quot; that allows the <em>brigata</em>&nbsp;some time to adapt before returning to reality.</p>
